//! The name of this module for use by the Log module.
#define MODULE_NAME MODULE_RINSE_AID_DISPENSE


//! The types of RINSE_AID_DISPENSE variations that are available.
typedef enum RINSE_AID_DISPENSE_OPTION_ENUM
{
	RINSE_AID_DISPENSE,                          //!< F1=0, F0=0
	TRIDGE_DETERGENT_DISPENSE,
	RINSE_AID_DISPENSE_OPTION_COUNT
} RINSE_AID_DISPENSE_OPTION_TYPE;

//! A list of log message IDs that are generated by this module.
typedef enum MODULE_RINSE_AID_DISPENSE_LOG_MESSAGE_ID_ENUM
{
	PROCESS_CONTROL_IS_NULL = 1,
	PROCESS_CONTROL_PARAMS_EXCEED_LIMITS,
} MODULE_RINSE_AID_DISPENSE_LOG_MESSAGE_ID_TYPE;



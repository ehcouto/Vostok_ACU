/**
 * @brief
 */
static void UpdateDispensingTimer(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	// Dispensing time in sec x 10 (rounded)
	TD_Process_NV_Data_Ptr->Dispensing_Timer = GetDispensingTime(GetAmountToEmptyDispenser());
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void CheckTagStatusRead(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	switch (TD_Process_Data_Ptr->Tag_Read_Status)
	{
		case TAG_READ_STATUS_WAITING:
			// Nothing to do (waiting for first tag status data)
			break;

		case TAG_READ_STATUS_GOOD:
			DISPATCH_FSM_EVENT(FSM_EVENT_TAG_STATUS_READ);
			break;

		case TAG_READ_STATUS_KO:
		default:
			DISPATCH_FSM_EVENT(FSM_EVENT_NO_CARTRIDGE_DATA);
			break;
	}
}


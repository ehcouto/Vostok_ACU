/**
 * Restore references to the setting file.
 * Do not assume that the references were recovered over a warm/cold reset.
 */
static void RestoreSfReferences(void)
{
    // Get the list of all configured faults.
    SETTINGFILE_LOADER_TYPE loader;
    if (SettingFile__BasicLoader(SF_PTR_ACU_CROSS_PRODUCT_CONFIG, SF_DISPL_SORTED_FAULT_ID_LIST, &loader) == FAIL)
    {
        // The list of sorted faults is not available.
        LOG_ADD_EXCEPTION(MISSING_SORTED_FAULT_LIST, 0);
        Fault.List = NULL;
    }
    else
    {
        // Save a pointer to the list of sorted faults.
        Fault.List = (void*)loader.Data;
    }

    // Restore setting file references for faults that were being observed before the reset.
    FdmTimer__DebounceDataSize(2 * sizeof(FAULT_DEBOUNCE_TYPE));
    for (Fault.Instance = 0; Fault.Instance < MAX_PARALLEL_FAULT_OBSERVERS; Fault.Instance++)
    {
        if (Fault_Nv.Observers[Fault.Instance].State != STATE_FAULT_OBSERVER_NONE)
        {
            Fault.Config[Fault.Instance] = GetConfigParameters(Fault_Nv.Observers[Fault.Instance].Priority);
            if (Fault.Config[Fault.Instance] != NULL)
            {
                if (GetActivePreFaultObserver(Fault.Config[Fault.Instance]->Compartment) == Fault.Instance)
                {
                    // Check the High Priority Pre-fault Observer in Compartement.
                    Prefault_Id[Fault.Config[Fault.Instance]->Compartment] = Fault.Config[Fault.Instance]->Id;
                }

                if (GetActiveFaultObserver(Fault.Config[Fault.Instance]->Compartment) == Fault.Instance)
                {
                    // Check the High Priority Fault Observer in Compartement.
                    Fault_Id[Fault.Config[Fault.Instance]->Compartment] = Fault.Config[Fault.Instance]->Id;
                }

                Fault.Debounce[Fault.Instance] = GetDebounceParameters(Fault.Config[Fault.Instance]->Debounce_Method_Displacement);
                if (Fault_Nv.Observers[Fault.Instance].State == STATE_FAULT_OBSERVER_PRE_FAULT)
                {
                    Fault.Debounce[Fault.Instance] = &Fault.Debounce[Fault.Instance][1];
                }
            }
            else
            {
                Fault_Nv.Observers[Fault.Instance].State = STATE_FAULT_OBSERVER_NONE;
            }
        }
    }
}



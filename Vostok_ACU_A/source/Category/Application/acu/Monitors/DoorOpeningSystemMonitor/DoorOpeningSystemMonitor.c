/**
 *  @file		DoorOpeningSystemMonitor.c
 *
 *  @brief		A monitor that manages the Door Opening System device, together with the DOS Push and DOS Pull tasks.
 *
 *  @details    Door Opening System (briefly DOS) is a device used to assist with the drying by opening the door of the dishwasher
 *  			releasing the moisture out of the tub at selected moment given by the cycle design.
 *  			Door Opening System monitor is used for the DOS overall management, and exchanges data with the DOS Push
 *  			and DOS Pull tasks, that are used to trigger the activation of the Push and of the Pull sequences.
 *
 */


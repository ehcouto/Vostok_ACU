/**
 * @brief 	Detect if user refill RinseAid
 */
void ReadRinsAidLevel(void)
{
	CYCLE_PHASE_STATEMENT_TYPE cps = Interpreter__GetCurrentStatement(COMPARTMENT_DEFAULT);
	CYCLE_STATE_TYPE cycle_state = (Selector__GetCycleState(0, COMPARTMENT_DEFAULT, cps.Cycle));

	if (DOOR_STATUS_CLOSE == DoorMonitor__GetDoorStatus())
	{
		if ((cycle_state == CYCLE_STATE_PROGRAMMING) && (cps.Phase != (uint8)0))
		{
			if ((Millis__GetElapsedSeconds(&Rinse_Aid_Timer) >= (uint32)PROGRAMMING_MEASURE_DELAY_SEC) &&
				(Programming_Measurment_Done == FALSE))
			{
				uint8 *ra_activation_state_ptr;
				uint8 ra_activation_state;
				RINSE_AID_SENSOR_TYPE temp;

				ra_activation_state_ptr = (uint8*)Hbl__GetLoadStatusByLoadID(HBL_LOAD_RINSE_AID_INPUT_ACTIVATION);

				if (ra_activation_state_ptr != NULL)
				{
					ra_activation_state = *ra_activation_state_ptr;
				}
				else
				{
					ra_activation_state = (uint8)ON;
				}

				if (ra_activation_state == (uint8)ON)
				{
					temp = (RINSE_AID_SENSOR_TYPE)Hbl__GetGIDataConvertedByIndex(Hbl__GetGIIndexByGIID(HBL_GI_RINSE_AID));

					//Update variables only if we read RinseAid not MSA
					if(temp < RINSE_AID_SENSOR_INVALID_VALUE)
					{
						//Detect Rising Edge
						if((Last_Rinse_Aid_Status == RINSE_AID_SENSOR_EMPTY) &&
						   (temp == RINSE_AID_SENSOR_FULL))
						{
							RinseAid_Dispenser_Use_Cnt = 0;
							//Unlock RinseAid status display after first refill
							RinseAid_IsUnlocked = TRUE;

							Last_Rinse_Aid_Status = temp; //Update this value only if we have falling edge (refill retection)
						}

						Programming_Measurment_Done = TRUE;
					}
				}
			}
		}
		else
		{
			Programming_Measurment_Done = FALSE;

			if ((cycle_state == CYCLE_STATE_RUN) || (cycle_state == CYCLE_STATE_DELAY))
			{
				if ((Millis__GetElapsedSeconds(&Rinse_Aid_Timer) >= Sf_RinseAid_Data.Door_Close_Timer) &&
					((uint8)Check_RinsAid_State == (uint8)TRUE))
				{
					uint8 *ra_activation_state_ptr;
					uint8 ra_activation_state;
					RINSE_AID_SENSOR_TYPE temp;

					ra_activation_state_ptr = (uint8*)Hbl__GetLoadStatusByLoadID(HBL_LOAD_RINSE_AID_INPUT_ACTIVATION);

					if (ra_activation_state_ptr != NULL)
					{
						ra_activation_state = *ra_activation_state_ptr;
					}
					else
					{
						ra_activation_state = (uint8)ON;
					}

					if (ra_activation_state == (uint8)ON)
					{
						temp = (RINSE_AID_SENSOR_TYPE)Hbl__GetGIDataConvertedByIndex(Hbl__GetGIIndexByGIID(HBL_GI_RINSE_AID));

						//Update variables only if we read RinseAid not MSA
						if(temp < RINSE_AID_SENSOR_INVALID_VALUE)
						{
							//Detect Rising Edge
							if((Last_Rinse_Aid_Status == RINSE_AID_SENSOR_EMPTY) &&
							   (temp == RINSE_AID_SENSOR_FULL))
							{
								RinseAid_Dispenser_Use_Cnt = 0;
								//Unlock RinseAid status display after first refill
								RinseAid_IsUnlocked = TRUE;
							}

							Last_Rinse_Aid_Status = temp;
							Check_RinsAid_State = FALSE;
						}
					}
				}
			}
		}
	}
	else
	{
	    Millis__Start(&Rinse_Aid_Timer);
		Programming_Measurment_Done = FALSE;
	}
}


/**
 * 	@brief 	Detect if dispenser was used and increment RinseAid usage counter
 */
void DetectRinseAidInjection(void)
{
    uint8 *load_state_ptr;

    //Detect dispenser injection
    load_state_ptr = (uint8*)(Hbl__GetLoadStatusByLoadID(HBL_LOAD_DISPENSER));
    if (load_state_ptr != NULL)
    {
		Dispenser_State = (load_state_ptr[0] == (uint8)ON) ? ON:OFF;
    }

	//Detect Rising Edge
	if (Dispenser_State != Dispenser_State_Prev)
	{
		Dispenser_State_Prev = Dispenser_State;

		if((Dispenser_State == ON) &&
		   (RinseAid_Dispenser_Use_Cnt < DISPENSER_MAX_USAGE))
		{
			if(Last_Rinse_Aid_Status == RINSE_AID_SENSOR_EMPTY)
			{
				RinseAid_Dispenser_Use_Cnt++;
			}
		}
	}
}


/**
 *  @brief	Updates status of RinseAid regulation
 */
void UpdateRinseAidStatus(void)
{
	//We want to show status until user will activate module by first refill
	if(RinseAid_IsUnlocked == FALSE)
	{
		Rinse_Aid_Status = RINSE_AID_STATUS_LED_OFF;
	}
	else
	{
		Rinse_Aid_Status = EvaluateRinseAid_State(Sf_RinseAid_Data.Threshold_1, Sf_RinseAid_Data.Threshold_2, RinseAid_Dispenser_Use_Cnt);
	}
}


/**
 *
 *  @brief	Function which evaluate current state of Rinse_Aid_Status regulation
 *
 *   ------------------------------------------------------------------------------------------------------------------------
 *	|					CONDITION	 	 	 	 	    0                     THR1      			 THR2					 |
 *	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 = 0) AND (THR2 > 0):      	  			    | LED_OFF 			   | LED_OFF 		      | LED_BLINK			 |
 *	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 > 0) AND (THR2 = 0):      				    | LED_OFF 			   | LED_SOLID_ON 		  | LED_SOLID_ON		 |
 *	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 = 0) AND (THR2 = 0):      				    | LED_OFF 			   | LED_OFF 		  	  | LED_OFF		 		 |
 *	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 < THR2) AND ((THR1 > 0) AND (THR2 > 0)):   | LED_OFF 			   | LED_SOLID_ON 		  | LED_BLINK			 |
 *	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 > THR2) AND ((THR1 > 0) AND (THR2 > 0)):   | LED_OFF 			   | LED_BLINK 		      | LED_SOLID_ON		 |
 * 	|---------------------------------------------------|----------------------|----------------------|----------------------|
 *	|  (THR1 = THR2) AND ((THR1 > 0) AND (THR2 > 0)):   | LED_OFF 			   | LED_SOLID_ON 		  | LED_SOLID_ON		 |
 *	 ---------------------------------------------------|----------------------|----------------------|----------------------
 *
 *
 * @param Thr1 - Setting File threshold1
 * @param Thr2 - Setting File threshold2
 * @param current_val - Amount of dispenser usage
 * @return RinseAid status
 *
 */
RINSE_AID_STATUS_TYPE EvaluateRinseAid_State(uint8 Thr1, uint8 Thr2, uint8 current_val)
{
	RINSE_AID_STATUS_TYPE retval;
	if((Thr1 == (uint8)0) || (Thr2 == (uint8)0))
	{
		if(Thr1 > (uint8)0)
		{
			if(current_val < Thr1)
			{
				retval = RINSE_AID_STATUS_LED_OFF;
			}
			else
			{
				retval = RINSE_AID_STATUS_LED_SOLID_ON;
			}
		}
		else if(Thr2 > (uint8)0)
		{
			if(current_val < Thr2)
			{
				retval = RINSE_AID_STATUS_LED_OFF;
			}
			else
			{
				retval = RINSE_AID_STATUS_LED_BLINK;
			}
		}
		else // if((Thr1 == 0) && (Thr2 == 0))
		{
			retval = RINSE_AID_STATUS_LED_OFF;
		}
	}
	else
	{
		if(Thr1 < Thr2)
		{
			if(current_val < Thr1)
			{
				retval = RINSE_AID_STATUS_LED_OFF;
			}
			else if((current_val >= Thr1) && (current_val < Thr2))
			{
				retval = RINSE_AID_STATUS_LED_SOLID_ON;
			}
			else
			{
				retval = RINSE_AID_STATUS_LED_BLINK;
			}
		}
		else if(Thr1 > Thr2)
		{
			if(current_val < Thr2)
			{
				retval = RINSE_AID_STATUS_LED_OFF;
			}
			else if((current_val >= Thr2) && (current_val < Thr1))
			{
				retval = RINSE_AID_STATUS_LED_BLINK;
			}
			else
			{
				retval = RINSE_AID_STATUS_LED_SOLID_ON;
			}
		}
		else //if(Thr1 == Thr2)
		{
			if(current_val < Thr1)
			{
				retval = RINSE_AID_STATUS_LED_OFF;
			}
			else
			{
				retval = RINSE_AID_STATUS_LED_SOLID_ON;
			}
		}
	}

	return retval;
}




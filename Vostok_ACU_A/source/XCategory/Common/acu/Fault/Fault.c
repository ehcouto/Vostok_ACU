/**
 * Find and return the existing observer of the specified fault.
 * @param fault_id = The fault ID to search for.
 * @return The fault observer that is handling the specified fault_id.
 *         Return UCHAR_MAX if no fault observer is handling the specified fault_id.
 */
static uint8 FindExistingObserver(FAULT_ID_TYPE fault_id)
{
    // If there is already an observer for this fault, then return it.
    for (uint8 observer = 0; observer < MAX_PARALLEL_FAULT_OBSERVERS; observer++)
    {
        if ((Fault_Nv.Observers[observer].State != STATE_FAULT_OBSERVER_NONE) &&
            (Fault.Config[observer]->Id == fault_id))
        {
            return observer;
        }
    }

    return UCHAR_MAX;
}



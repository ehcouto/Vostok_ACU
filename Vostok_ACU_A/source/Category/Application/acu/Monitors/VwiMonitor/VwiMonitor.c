/**
 * @brief Function read VWI Data period value from SettingFile and set
 *
 */
static void ReadSfData()
{
	SETTINGFILE_LOADER_TYPE loader;
	uint16 Data_Period;

	if (SettingFile__BasicLoader(VWI_SETTING_FILE_PTR, VWI_SETTING_FILE_DISPL_DEFAULT, &loader) == PASS)
	{
		uint8 sf_data;
		sf_data = ((VWI_PARAMETER_TYPE*)(loader.Data))->VWI_Data_Channel_Period; //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]

		//Multiply SettingFile parameter x100
		Data_Period = (sf_data*100);
	}
	else
	{
		Data_Period = 1000;
		LOG_ADD_EXCEPTION(ERROR_LOADING_VWI_PARAMETERS, 0);
	}
	
	Vwi__RemoteChannelPeriod(0xFF, Data_Period);
}


/**
 * Handle events published through API221.
 * This function is called by the communication thread.
 * @param event = The event that was published through API221.
 */
static void API221HandleMotorEvent(API221_EVENT_TYPE event)
{
	switch (event.Event)
	{
		case API221_EVENT_ERROR:
		case API221_EVENT_STOPPED:
			Vwi__RemoteChannelEnable(event.Motor, MCISENSORSDW_AI_VWI_CHANNEL_0, FALSE);
			Vwi__RemoteChannelEnable(event.Motor, MCISENSORSDW_AI_VWI_CHANNEL_1, FALSE);
			break;

		case API221_EVENT_RAMP_STARTED:
		case API221_EVENT_REACHED_TARGET:
			Vwi__RemoteChannelEnable(event.Motor, MCISENSORSDW_AI_VWI_CHANNEL_0, TRUE);
			Vwi__RemoteChannelEnable(event.Motor, MCISENSORSDW_AI_VWI_CHANNEL_1, TRUE);
			break;

		default:
			// Ignore all other events
			break;
	}
}


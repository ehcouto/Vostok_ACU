/**
 *  @brief      Check if a compartment exists based on setting file selector data.
 *
 *  @param      uint8 sf_index - setting file to check.
 *  @param      compartment = The compartment number to check for.
 *
 *  @return     TRUE if the compartment data exists in the setting file selector data.
 */
BOOL_TYPE Selector__DoesCompartmentExist(uint8 sf_index, uint8 compartment)
{
    BOOL_TYPE compartment_exists;

    compartment_exists = FALSE;
    if (compartment < ELEMENTS_IN_ARRAY(Selector_Data_Pointers))
    {
        if (SettingFile__GetNumDisplacements(sf_index, Selector_Data_Pointers[compartment]) > 0)
        {
            compartment_exists = TRUE;
        }
    }

    return compartment_exists;
}


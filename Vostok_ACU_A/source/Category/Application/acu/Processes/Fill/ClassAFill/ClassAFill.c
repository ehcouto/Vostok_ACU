/**
 * Initialize the ClassAFill module's variables.
 */
void ClassAFill__Initialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
		{
			Class_A_Fill_State = FSM_STATE_NONE;
			Fill_NV_Data.Liters_Requested = 0;
			Fill_NV_Data.Fill_Timer = 0;
			Fill_NV_Data.H2O_Pause_Executed = FALSE;
		}

		memset(&Fill_Data, 0, sizeof(Fill_Data));

		Fill_Data.Process_Parameters_Loading = FAIL;
		Fill_Data.Subcycle_Executed = FALSE;
		Fill_Data.Flow_Rate_Calibration_Enabled = FALSE;
	}
}


/**
 * It implements the Class A Fill process
 */
PROCESS_RESULT_TYPE ClassAFill__Manager(PROCESS_CONTROL_TYPE* control)
{
	PROCESS_RESULT_TYPE result = PROCESS_COMPLETE;

	if ((control->Step_Initializing != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Reset != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Pause != (BOOL_TYPE)FALSE))
	{
		Fill_Data.Process_Parameters_Loading = ReadProcessParameters(control);
	}

	if (Fill_Data.Process_Parameters_Loading == PASS)
	{
		if (control->Step_Initializing != (BOOL_TYPE)FALSE)
		{
			// Initialize this instance of the ClassAFill process.
			ClassAFillFsmInitialize(&Class_A_Fill_State);
		}

		if (control->Resume_From_Reset == (BOOL_TYPE)FALSE)
		{
			// ClassAFillExecute() not called in first iteration after a cold reset to avoid a spurious Fill Valve turning on in case of
			// long power fail: actually, in this case, Fill process would be called for just one iteration before going in PAUSE.
			ClassAFillFsmExecute(&Class_A_Fill_State);
		}

		result = ((Class_A_Fill_State == FSM_STATE_FILL_COMPLETE) ? PROCESS_COMPLETE : PROCESS_BUSY);
	}

	return result;
}


/**
 * Called in SUB_CYCLE_END(), it checks if the ended subcycle is the Fill Check Water subcycle and,
 * if it is, it marks it as executed.
 */
void ClassAFill__SetSubcycleExecuted(uint8 compartment, uint8 subcycle_id, SUB_CYCLE_END_TYPE subcycle_end_code)
{
	if (Selector__GetCycleNameId(0, compartment , subcycle_id) == CYCLE_NAME_SPECIFIC_FILL_CHECK_WATER)
	{
		if ((subcycle_end_code == SUB_CYCLE_END_COMPLETED) || (subcycle_end_code == SUB_CYCLE_END_SET_STATE_PROCESS))
		{
			Fill_Data.Subcycle_Executed = TRUE;
		}
	}
}


/**
 * Called in PAUSE_EXIT(), it informs the process that the cycle has exited from PAUSE state.
 */
void ClassAFill__SetH2OPauseExecuted(void)
{
		Fill_NV_Data.H2O_Pause_Executed = TRUE;
}


/**
 * Called in SUB_CYCLE_START() and in PAUSE_ENTRY(), it disables the Flow Rate calibration
 */
void ClassAFill__DisableFlowRateCalibration(void)
{
	Fill_Data.Flow_Rate_Calibration_Enabled = FALSE;
}


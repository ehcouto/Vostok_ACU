/**
 * It initializes the Door Opening System Pull task and its data.
 *
 * @param  compartment = compartment inside which the task is running
 * @param  sf_index = the index of the setting file from which to load the task data
 * @param  displacement = the displacement for the task data in the setting file (unused, since all the instances
 * 		   of the Door Opening System Pull Task use the same data table).
 * @return the status of the task (ACTIVE or INACTIVE)
 *
 */
ACTIVE_TYPE DoorOpeningSystemPull__Initialize(uint8 compartment, uint8 sf_index, uint8 displacement)
{
    SETTINGFILE_LOADER_TYPE loader;
	ACTIVE_TYPE ret_value = INACTIVE;

	if (Hbl__GetLoadIndexByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM) != (uint8)HBL_INDEX_INVALID)
	{
		// Get the DOS Push parameters from the setting file.
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM, &loader) == PASS)
		{
			DoorOpeningSystemMonitor__StartPull(((DOOR_OPENING_SYSTEM_SF_PARAMS_TYPE *)(void*)loader.Data)->Pull_Off_Time); //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
			ret_value = ACTIVE;
		}
		else
		{
			// Algorithm parameters missing, allow step advancement
			LOG_ADD_EXCEPTION(ERROR_MISSING_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM);
		}
	}
	else
	{
		// Door Opening System not present, allow step advancement
	}

	return ret_value;
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Function called during the execution of the Door Opening System Pull task.
 *
 * @param  compartment = compartment inside which the task is running
 * @return the status of the task (ACTIVE or INACTIVE)
 */
ACTIVE_TYPE DoorOpeningSystemPull__Task(uint8 compartment)
{
	ACTIVE_TYPE ret_value;

	if (DoorOpeningSystemMonitor__GetDOSPullState() != DOS_PULL_COMPLETE )
	{
		// PULL procedure is not complete, the task doesn't allow step advancement.
		ret_value = ACTIVE;
	}
	else
	{
		// PULL procedure is complete, the microprogram allows step advancement.
		ret_value = INACTIVE;
	}

	return ret_value;
}



/**
 * @brief   this function is accumulation raw percentage value, taking average and the checking for error status
 * @param   input: data related to the instance of the LLI
 */
#if (HBL_LLI_NUM_I2C_RHT_HUMIDITY_SENSOR > 0)
static void ProcessRHValue(HBL_LLI_TYPE * input)
{
	if (input->Data_Index < HBL_LLI_NUM_I2C_RHT_HUMIDITY_SENSOR)
	{
        I2C_RHT_SENSOR_PARAM_TYPE *param_ptr = (I2C_RHT_SENSOR_PARAM_TYPE *) (void *)(input->Parameter_Ptr.Data);

        if((SHT2XI2C_Forced_Reset == FALSE) && (SHT2X_After_Reset_Stable_Counter == 0))  //  This check is to maker sure not to take the reading when RHT chip is reseting
        {																																					 // The RHT after reset delay time is running
			if (SHT2XHumidity_Data[input->Data_Index].Index_Average < param_ptr->Automic_Reading)
			{
				SHT2XHumidity_Data[input->Data_Index].Index_Average++;    // Increment number of sample @ 1sec
				SHT2XHumidity_Data[input->Data_Index].AD_RH_Buffer += SHT2XHumidity_Data[input->Data_Index].Sensor_I2C_RH_Percentage; // accumulate sample
			}
			else
			{   // take average of accumulated samples
				SHT2XHumidity_Data[input->Data_Index].Sensor_I2C_Relative_Humidity = (uint8)(SHT2XHumidity_Data[input->Data_Index].AD_RH_Buffer / param_ptr->Automic_Reading); //


				CheckRHNeedToReset(input);

				SHT2XHumidity_Data[input->Data_Index].Index_Average = 0; // clear counter
				SHT2XHumidity_Data[input->Data_Index].AD_RH_Buffer = 0; // clear previous value
			}
		}
	}
}
#endif


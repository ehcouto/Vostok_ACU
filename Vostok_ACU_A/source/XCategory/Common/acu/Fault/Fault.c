/**
 * Initialize the Fault module variables.
 */
void Fault__Initialize(void)
{
    BOOL_TYPE nvdata_corrupted = (BOOL_TYPE)(NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != NVDATA_RESTORE_OKAY);
    if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
    {
        if (nvdata_corrupted == TRUE)
        {
            // Nonvolatile data was lost -- reset all state machines.
            memset(&Fault_Nv, 0, sizeof(Fault_Nv));
        }
        #if (MAX_FAULT_HANDLERS > 0)
            Callback__Initialize(&Callback_Control_Fault);
        #endif
        #if (MAX_PRE_FAULT_HANDLERS > 0)
            Callback__Initialize(&Callback_Control_Pre_Fault);
        #endif
    }

    // Reset and rebuild all volatile data.
    memset(Prefault_Id, FAULT_ID_NONE, sizeof(Prefault_Id));
    memset(Fault_Id, FAULT_ID_NONE, sizeof(Fault_Id));
    memset(&Fault, 0, sizeof(Fault));
    RestoreSfReferences();

    // On a normal cold reset, terminate any state machines with the CCR bit set.
    if ((Micro__GetResetMode() == MICRO_RESET_MODE_COLD) &&
        (nvdata_corrupted == FALSE))
    {
        for (Fault.Instance = 0; Fault.Instance < MAX_PARALLEL_FAULT_OBSERVERS; Fault.Instance++)
        {
            if ((Fault_Nv.Observers[Fault.Instance].State != STATE_FAULT_OBSERVER_NONE) &&
                ((Fault.Config[Fault.Instance] == NULL) ||
                 (Fault.Config[Fault.Instance]->Clear_On_Cold_Reset == TRUE)))
            {
                Fault_Nv.Observers[Fault.Instance].State = STATE_FAULT_OBSERVER_NONE;
            }
            if (Fault.Debounce[Fault.Instance] == NULL)
            {
                Fault.Debounce[Fault.Instance] = (void*)FAULT_DEFAULT_DEBOUNCE_CONFIG;
            }
        }
    }
    UpdateFaultIDs();
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Background function for the Fault module.
 */
void Fault__Background(void)
{
    if (Fault.Reset_All_Observers == TRUE)
    {
        // Faults were cleared so remove all active faults.
        for (uint8 instance = 0; instance < MAX_PARALLEL_FAULT_OBSERVERS; instance++)
        {
            Fault_Nv.Observers[instance].State = STATE_FAULT_OBSERVER_NONE;
        }
        RESET_FAULT_DETECTORS();
        Fault.Reset_All_Observers = FALSE;
    }
    for (Fault.Instance = 0; Fault.Instance < MAX_PARALLEL_FAULT_OBSERVERS; Fault.Instance++)
    {
        // Execute all the fault observer state machines.
        FaultObserverExecute();
    }
    UpdateFaultIDs();
}



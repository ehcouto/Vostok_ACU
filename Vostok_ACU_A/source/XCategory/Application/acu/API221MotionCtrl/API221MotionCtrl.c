/**
 * Implement Auto-Stop feature on the server.
 * Automatically stop the motor if timeout occurs before expected messages from the client.
 * @param server_motor = The server-based motor index.
 */
static void AutoStopCheck(uint8 server_motor)
{
    uint8 timeout = Api221.Stop_Timeout[server_motor];
    if (timeout == 0)
    {
        // Do nothing.
    }
    else if (timeout == 1)
    {
        if (API221_DEVICE_STOP(server_motor, API221_DEFAULT_DECELERATION) == TRUE)
        {
            LOG_ADD_EVENT(MOTOR_STOPPED_DUE_TO_TIMEOUT, server_motor);
            Api221.Stop_Timeout[server_motor] = 0;
        }
        else
        {
            LOG_ADD_EXCEPTION(MOTOR_REJECTED_STOP_FROM_TIMEOUT, server_motor);
        }
    }
    else
    {
        Api221.Stop_Timeout[server_motor] = timeout - 1;
    }
}
#endif      // #if (TIMEOUT_TO_STOP > 0)


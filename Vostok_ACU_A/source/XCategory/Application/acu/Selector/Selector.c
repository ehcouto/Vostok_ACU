/**
 *  @brief      Check program header to see if it is OK to pause cycle.
 *
 *  @param      uint8 setting_file - setting file index for where to find program header
 *  @param      uint8 compartment - identifier appliance compartment (0 based, 0 for single compartment appliances).
 *  @param      uint8 cycle - displacement of label in settings file program header
 *
 *  @return     BOOL_TYPE ok_to_pause
 *  @retval     TRUE - Cycle is allowed to pause.
 *  @retval     FALSE - Cycle is not allowed to pause.
*/
BOOL_TYPE Selector__OkToPauseCycle(uint8 sf_index, uint8 compartment, uint8 cycle)
{
    BOOL_TYPE ok_to_pause;
    uint8 const *cycle_header;

    ok_to_pause = FALSE;

    if (GetProgramHeadingPointer(sf_index, compartment, cycle, &cycle_header) == PASS)
    {
        #define CYCLE_HEADING_PAUSE_BYTE (0)
        #define CYCLE_HEADING_PAUSE_BIT (0x10)

        // 0 = Disable pause for this cycle.
        // 1 = Enable pause for this cycle.
        if ((cycle_header[CYCLE_HEADING_PAUSE_BYTE] & CYCLE_HEADING_PAUSE_BIT) == CYCLE_HEADING_PAUSE_BIT)
        {
            ok_to_pause = TRUE;
        }
    }

    return (ok_to_pause);
}


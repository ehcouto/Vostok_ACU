/**
 * this function run the Sub Cycle if the conditions exist
 * @param priority
 */
static void VoltageOutRangeManageSubCycle(uint8 priority)
{
    if (priority > 0U)
    {
        if ((uint8)CheckConditionToRunCycle() == (uint8)TRUE)
        {
            (void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, priority);
        }
    }
}

/**
 *
 * @return
 */
static BOOL_TYPE CheckConditionToRunCycle(void)
{
    BOOL_TYPE result;
    INTERPRETER_STATUS_TYPE interpreter_status;
    CYCLE_NAME_TYPE cycle_name,subcycle_name;
    uint8 sf_index;

    result = FALSE;

    interpreter_status = Interpreter__GetStatus();

    sf_index = Interpreter__GetCurrentSFIndex(COMPARTMENT_DEFAULT);

    cycle_name = Selector__GetCycleNameId(sf_index, COMPARTMENT_DEFAULT, (uint8)(interpreter_status.Main_Cycle[COMPARTMENT_DEFAULT]));

    subcycle_name = Selector__GetCycleNameId(sf_index, COMPARTMENT_DEFAULT,	(uint8)interpreter_status.Sub_Cycle[COMPARTMENT_DEFAULT]);

    if ((cycle_name != CYCLE_NAME_DEMO_0) && (subcycle_name != CYCLE_NAME_DEMO_0) &&
    	(cycle_name != CYCLE_NAME_EOL_0) &&
    	(cycle_name != CYCLE_NAME_EOL_1) &&
    	(cycle_name != CYCLE_NAME_EOL_2) &&
    	(cycle_name != CYCLE_NAME_SERVICE_0) &&
    	(cycle_name != CYCLE_NAME_SERVICE_1) )
    {
        result = TRUE;
    }

    return result;
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief		Returns TRUE if ACLINE is present, PILOT RELAY is closed, RELAY ENABLE is closed, DOOR is closed and ZeroCross is present
 *
 */
static BOOL_TYPE IsMonitorEnabled(void)
{
	BOOL_TYPE result = FALSE;
	uint8 pilot_relay_state;
	uint8 relay_enable_state;

	if (Hbl__GetLoadIndexByLoadID(HBL_LOAD_PILOT_RELAY) != (uint8)HBL_INDEX_INVALID)
	{
		pilot_relay_state = ((uint8*)(Hbl__GetLoadStatusByLoadID(HBL_LOAD_PILOT_RELAY)))[0];
	}
	else
	{
		pilot_relay_state = ON;
	}

	if (Hbl__GetLoadIndexByLoadID(HBL_LOAD_RELAY_ENABLE) != (uint8)HBL_INDEX_INVALID)
	{
		relay_enable_state = ((uint8*)(Hbl__GetLoadStatusByLoadID(HBL_LOAD_RELAY_ENABLE)))[0];
	}
	else
	{
		relay_enable_state = ON;
	}

	if ((Hbl__GetGIIndexByGIID(HBL_GI_AC_LINE) != (uint8)HBL_INDEX_INVALID) &&
		((uint8)Hbl__IsZeroCrossPresent() == (uint8)TRUE) &&
		(DoorMonitor__GetDoorStatus() == DOOR_STATUS_CLOSE) &&
		(pilot_relay_state == (uint8)ON) &&
		(relay_enable_state == (uint8)ON))
	{
		result = TRUE;
	}

	return result;
}


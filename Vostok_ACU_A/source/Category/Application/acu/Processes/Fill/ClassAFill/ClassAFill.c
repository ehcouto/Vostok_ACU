/**
 * @brief
 */
static void WaitAFSubcycleExecution(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Fill_Data.Subcycle_Executed == FALSE)
	{
		(void)SubCycle__RequestStart(0, Fill_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority);
	}
	else
	{
		VWI_WATER_SENSE_TYPE last_valid_water_status;

		last_valid_water_status = Vwi__GetLastValidWaterStatus();

		if ((last_valid_water_status == WS_NOT_VALID) || (last_valid_water_status == WS_NO_WATER) || (last_valid_water_status == WS_UNSTABLE_NO_WATER))
		{
			// No Water
			NoWaterFoundInAfterFillChecks();
		}
		else
		{
			// Water is present, do nothing
		}

		DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void WaitForPauseEnd(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Fill_NV_Data.H2O_Pause_Executed == FALSE)
	{
		if (Fault__IsInFaultState(FAULT_ID_NO_WATER_FILL_VALVE_PROBLEM) == FALSE)
		{
			Fault__ConditionDetected(FAULT_ID_NO_WATER);
		}

		(void)Supervisor__SetStart(0, FS_PAUSE_CONDITION);
	}
	else
	{
		DISPATCH_FSM_EVENT(FSM_EVENT_RESTARTFILL);
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void WaitSubcycleExecution(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Fill_Data.Subcycle_Executed == FALSE)
	{
		(void)SubCycle__RequestStart(0, Fill_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority);
	}
	else
	{
		VWI_WATER_SENSE_TYPE last_valid_water_status;

		last_valid_water_status = Vwi__GetLastValidWaterStatus();

		if ((last_valid_water_status == WS_WATER) || (last_valid_water_status == WS_UNSTABLE_WATER))
		{
			// Water is present
			uint32 sum;
			uint16 water_since_last_regen = DishAuxVar__GetWaterLoadedSinceLastRegen();

			if (Fill_NV_Data.Liters_Requested > Liters_In_Tub_Before_Fill)
			{
				Liters_Current_Fill = Fill_NV_Data.Liters_Requested - Liters_In_Tub_Before_Fill;
			}
			else
			{
				Liters_Current_Fill = 0;
			}
			Liters_In_Tub = Fill_NV_Data.Liters_Requested;

			// Since flow meter is not working, it is not able to update Water_Loaded_Since_Last_Regen, so the variable
			// is updated adding to it the amount of water that is assumed to have been loaded in the current Fill.
			sum = (uint32)water_since_last_regen + Liters_Current_Fill;
			if (sum > (uint16)__UNSIGNED_SHORT_MAX__)
			{
				sum = __UNSIGNED_SHORT_MAX__;
			}
			water_since_last_regen = (uint16)sum;

			DishAuxVar__SetWaterLoadedSinceLastRegen(water_since_last_regen);

			// Update statistics with estimated water loaded
			StatisticsDish__IncreaseWaterCounter(Liters_Current_Fill);

			Fault__ConditionDetected(FAULT_ID_FLOW_METER);

			// After fill checks
			switch (AfterFillCheck())
			{
				case AFTER_FILL_WATER_CHECK_NEEDED:
					DISPATCH_FSM_EVENT(FSM_EVENT_AFTERFILLWATERCHECKNEEDED);
					break;

				default:
					DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
					break;
			}
		}
		else
		{
			// No Water
			DISPATCH_FSM_EVENT(FSM_EVENT_NOWATERDETECTED);
		}

	}
}


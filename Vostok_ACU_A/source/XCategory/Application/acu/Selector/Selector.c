/**
 *  @brief      Get the Cycle Name ID label for sf_index, compartment and cycle.
 *
 *  @param      uint8 setting_file - setting file index for where to find program header
 *  @param      uint8 compartment - identifier appliance compartment (0 based, 0 for single compartment appliances).
 *  @param      uint8 cycle - displacement of Cycle Name ID in settings file program header
 *
 *  @return     CYCLE_NAME_TYPE cycle_name_id - Cycle name enumeration id for compartment cycle.
 *  @retval     0xFFFF  - Invalid Cycle (not found).
 *  @retval     1-65534 - Cycle name enumeration id for compartment cycle.
 *
*/
CYCLE_NAME_TYPE Selector__GetCycleNameId(uint8 sf_index, uint8 compartment, uint8 cycle)
{
    CYCLE_NAME_TYPE cycle_name_id = CYCLE_NAME_INVALID;

    uint8 const *cycle_header;

    if (GetProgramHeadingPointer(sf_index, compartment, cycle, &cycle_header) == PASS)
    {
        #define CYCLE_NAME_ID_OFFSET 4
        cycle_name_id = *(CYCLE_NAME_TYPE *)(void *)&cycle_header[CYCLE_NAME_ID_OFFSET];
    }

    return (cycle_name_id);
}


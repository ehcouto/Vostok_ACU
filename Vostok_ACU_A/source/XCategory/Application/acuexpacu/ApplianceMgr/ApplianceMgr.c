/**
 *  @brief      It Initializes the module ApplianceMgr and its variables.
 *              Initialize top application layer modules in a cascading manner.
 *
 */
void ApplianceMgr__Initialize(void)
{
    INIT_MONITORS();

    #if (SYSTEM_ACU_HANDLING == ENABLED)
        Millisecond_Counter = Millis__GetFreeRunningCounter();
        Deciseconds_Since_Power_Up = 0;
        Supervisor__Initialize();                   // Calls interpreter initialize which calls all process initializers.
    #endif

    #if (TIME_ESTIMATION_FEATURE == ENABLED)
        Estimator__Initialize();
    #endif

    #if (SYSTEM_ACU_EXPANSION_HANDLING == ENABLED)
        ACUExpLoadMonitor__Initialize();
    #endif
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      Implements Appliance Manager Behavior.
 *
 */
void ApplianceMgr__Handler(void)
{
    if (Mode__GetSubMode() == SUBMODE_NORMAL_SUPERVISOR)
    {
        Hbl__FlushRequestBuffer();

        #if (SYSTEM_ACU_HANDLING == ENABLED)
            CALL_PRE_MONITORS();

            SystemStable();
            if (System_Stable == TRUE)
            {
                CALL_PRE_MONITORS_STABLE();
                if (Supervisor__IsTestCycleActive() == FALSE)
                {
                    for (uint8 compartment = 0; compartment < APPLIANCE_MAX_COMPARTMENTS; compartment++)
                    {
                        Supervisor__Run(compartment);
                    }
                }
                else
                {
                    // Run only compartment 0 while test cycle is active.
                    Supervisor__Run(0);
                }
                CALL_POST_MONITORS_STABLE();
            }
        #endif

        CALL_POST_MONITORS();

        #if (SYSTEM_ACU_EXPANSION_HANDLING == ENABLED)
            ACUExpLoadMonitor__Execute();
        #endif

        #if (SYSTEM_ACU_HANDLING == ENABLED)
            // Accept all regulations that should always be accepted but have not yet been accepted.
            RegulationAccept__Background();
        #endif

        #if (TIME_ESTIMATION_FEATURE == ENABLED)
            Estimator__Background();
        #endif
    }
}

#if (SYSTEM_ACU_HANDLING == ENABLED)
//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      Public API to indicate if system inputs have settled and are ready to run cycles.
 *
 *  @retval     TRUE - System inputs are stable and ready for use.
 *  @retval     FALSE - System inputs are not ready for use.
 */
BOOL_TYPE ApplianceMgr__IsStable(void)
{
    return(System_Stable);
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      Initializes the stability status for the system.
 *
 *  @details    This information is initialized separately from the ApplianceMgr__Initialize() function because
 *              the system must be initialized as "not stable" before any module requests the status of the stability.
 *              This function must be called during the initialization of the communication thread to ensure that the
 *              system is initialized to "not stable", until it can be determined that it is stable.
 */
void ApplianceMgr__InitializeStable(void)
{
    System_Stable = FALSE;
}
#endif



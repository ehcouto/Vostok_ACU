void UpdatSaltAlarmLedStatus(void)
{
	SALT_ALARM_LED_STATE_TYPE salt_alm_led;

	salt_alm_led = SALT_ALARM_LED_STATE_OFF; // Default assignment to save code

	if ((uint8)Supervisor__IsTestCycleActive() == (uint8)TRUE)
	{
		if (Salt_Sensor_Data.Salt_GI_Unfiltered == SALT_PRESENT)
		{
			salt_alm_led = SALT_ALARM_LED_STATE_SOLID_ON;
		}
		else
		{
			// SALT_ALARM_LED_STATE_OFF;
		}
	}
	else
	{
		uint8 water_hardness_func_pos = Regulations__GetPositionByFunctionID(FUNC_ID_WATER_HARDNESS_LEVEL, COMPARTMENT_DEFAULT);
		BOOL_TYPE force_salt_alm_led_off = FALSE;
		SALT_SENSOR_STATE_TYPE salt_GI_value;
		uint8 n_reg_max_on = Salt_Sensor_Data.Water_Softener_Params_Ptr->N_Reg_Max_On;
		uint8 n_reg_max_blink = Salt_Sensor_Data.Water_Softener_Params_Ptr->N_Reg_Max_Blink;
		uint8 n_reg_max = Regenerate__GetNumRegeneration();

		if (water_hardness_func_pos < Regulations__GetTotalSize())
		{
			// Water Hardness function is present in setting file

			if (Regulations__GetMainValueByPosition(water_hardness_func_pos) == 0)
			{
				force_salt_alm_led_off = TRUE;
			}
		}

		if (Salt_Sensor_Data.Salt_GI_Filtered != SALT_SENSOR_INVALID_VALUE)
		{
			// First filtered read of Salt GI available
			salt_GI_value = Salt_Sensor_Data.Salt_GI_Filtered;
		}
		else
		{
			// First filtered read of Salt GI not available, use last filtered read
			salt_GI_value = Salt_Sensor_NV_Data.Prev_Salt_GI_Filtered;
		}


		if ((salt_GI_value == SALT_NOT_PRESENT) && (force_salt_alm_led_off == FALSE))
		{
			// Check thresholds
			if (n_reg_max_on == (uint8)0)
			{
				if (n_reg_max_blink != (uint8)0)
				{
					if (n_reg_max >= n_reg_max_blink)
					{
						salt_alm_led = SALT_ALARM_LED_STATE_BLINK;
					}
					else
					{
						// SALT_ALARM_LED_STATE_OFF;
					}
				}
				else
				{
					// Both thresholds == 0, SALT_ALARM_LED_STATE_OFF;
				}
			}
			else if (n_reg_max_blink == (uint8)0)
			{
				// n_reg_max_on for sure != 0
				if (n_reg_max >= n_reg_max_on)
				{
					salt_alm_led = SALT_ALARM_LED_STATE_SOLID_ON;
				}
				else
				{
					// SALT_ALARM_LED_STATE_OFF;
				}
			}
			else if (n_reg_max_on >= n_reg_max_blink)
			{
				if (n_reg_max >= n_reg_max_on)
				{
					salt_alm_led = SALT_ALARM_LED_STATE_SOLID_ON;
				}
				else if (n_reg_max >= n_reg_max_blink)
				{
					salt_alm_led = SALT_ALARM_LED_STATE_BLINK;
				}
				else
				{
					// SALT_ALARM_LED_STATE_OFF;
				}
			}
			else
			{
				// n_reg_max_blink > n_reg_max_on
				if (n_reg_max >= n_reg_max_blink)
				{
					salt_alm_led = SALT_ALARM_LED_STATE_BLINK;
				}
				else if (n_reg_max >= n_reg_max_on)
				{
					salt_alm_led = SALT_ALARM_LED_STATE_SOLID_ON;
				}
				else
				{
					// SALT_ALARM_LED_STATE_OFF;
				}
			}
		}
		else
		{
			// SALT_ALARM_LED_STATE_OFF;
		}
	}

	Salt_Alarm_Led = salt_alm_led;
}


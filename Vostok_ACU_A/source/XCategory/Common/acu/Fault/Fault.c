/**
 * Get the fault observer handling the highest priority active fault in a specified compartment.
 * @param compartment = The compartment to search for an active fault.
 * @return The fault observer that is handling the highest priority active fault in the specified compartment.
 *         Return UCHAR_MAX if no fault is active in the specified compartment.
 */
static uint8 GetActiveFaultObserver(uint8 compartment)
{
    uint8 observer = UCHAR_MAX;
    uint8 clear_without_remove_priority = UCHAR_MAX;
    uint8 clear_after_remove_priority = UCHAR_MAX;
    for (uint8 observer_candidate = 0; observer_candidate < MAX_PARALLEL_FAULT_OBSERVERS; observer_candidate++)
    {
        if ((Fault_Nv.Observers[observer_candidate].State == STATE_FAULT_OBSERVER_FAULT) &&
			(Fault.Config[observer_candidate]->Compartment == compartment))
        {
            uint8 priority = Fault_Nv.Observers[observer_candidate].Priority;
            if (Fault.Config[observer_candidate]->Allow_Clear_Without_Remove == FALSE)
            {
                // This fault observer is observing a fault that can only be cleared after
                // removing the error condition. Any fault in this group has priority over any
                // fault in the group below represented by 'clear_without_remove_priority'.
                if (clear_after_remove_priority > priority)
                {
                    observer = observer_candidate;
                    clear_after_remove_priority = priority;
                }
            }
            else if (clear_after_remove_priority == UCHAR_MAX)
            {
                // This fault observer is observing a fault that can be cleared without first
                // removing the error condition.
                if (clear_without_remove_priority > priority)
                {
                    observer = observer_candidate;
                    clear_without_remove_priority = priority;
                }
            }
        }
    }
    return observer;
}



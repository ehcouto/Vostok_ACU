#define FLOW_RATE_SAMPLES_TO_SAVE		12U

#define FLOW_RATE_SAMPLES_TO_REJECT		2U

// DishAuxVar_AverageWithRejection(), used in Class A Fill and in Tank fill, support a maximum of AVEARAGE_WITH_REJ_MAX_SUPPORTED_SAMPLES_NUM,
// so the number of samples on which calculate Flow Rate Calibrated has to be less than or equal to that limit.
CT_ASSERT(FLOW_RATE_SAMPLES_TO_SAVE <= AVEARAGE_WITH_REJ_MAX_SUPPORTED_SAMPLES_NUM);

// Average with rejection can only be calculated if number of samples to reject is less than total number of samples
CT_ASSERT(FLOW_RATE_SAMPLES_TO_REJECT < FLOW_RATE_SAMPLES_TO_SAVE);



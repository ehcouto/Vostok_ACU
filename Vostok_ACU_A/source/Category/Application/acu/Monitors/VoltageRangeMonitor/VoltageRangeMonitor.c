#ifndef VOLTAGERANGE_GETREFERENCE
#define VOLTAGERANGE_GETREFERENCE()			0
#endif

#ifndef VOLTAGERANGE_EXEC_TIME_MS
#define VOLTAGERANGE_EXEC_TIME_MS			100
#endif

#define VOLTAGERANGE_PARAM_TIME_CONVERT(s)		( ((uint16) (s)) * (uint16)(1000 / VOLTAGERANGE_EXEC_TIME_MS))

typedef struct
{
	uint16 Low_Voltage_Low_Thres;
	uint16 Low_Voltage_High_Thres;
	uint16 High_Voltage_Low_Thres;
	uint16 High_Voltage_High_Thres;
	uint8 Out_Range_Time;
	uint8 In_Range_Time;
	uint8 Unused;
	uint8 Voltage_Out_Range_Cycle_Priority;
} VOLTAGERANGE_PARAM_TYPE;

//! The name of this module for use by the Log module.
#define MODULE_NAME MODULE_VOLTAGE_RANGE

//! A list of log message IDs that are generated by this module.
typedef enum MODULE_VOLTAGE_RANGE_LOG_MESSAGE_ID_ENUM
{
    VOLTAGERANGE_STATUS_CHANGED       = 1,
    VOLTAGERANGE_INSTABILITY_COUNTER  = 2,
} MODULE_VOLTAGE_RANGE_LOG_MESSAGE_ID_TYPE;

static uint16 VRM_Good_Range_Counter;
static uint16 VRM_Low_Range_Counter;
static uint16 VRM_High_Range_Counter;
static uint16 VRM_Instability_Counter;

VOLTAGERANGE_STATE_TYPE Voltage_Range_Status;
static VOLTAGERANGE_STATE_TYPE VRM_Instant_Voltage_Range_Status;

//! The volatile data required by the Voltage Range monitor.
static VOLTAGERANGE_PARAM_TYPE *VRM_Data;

//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------
static void VoltageOutRangeManageSubCycle(uint8 priority);
static BOOL_TYPE CheckConditionToRunCycle(void);
static BOOL_TYPE IsMonitorEnabled(void);

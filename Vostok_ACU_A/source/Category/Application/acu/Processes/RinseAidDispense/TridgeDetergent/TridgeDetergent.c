static void ReadProcessParameters(PROCESS_CONTROL_TYPE* control)
{
	SETTINGFILE_LOADER_TYPE loader;

	TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters = NULL;
	TD_Process_Data_Ptr->Tridge_Detergent_Parameters = NULL;

	// Get the Tridge Detergent process parameters from the setting file.
	if (SettingFile__MSettingBasicLoader(control->Setting_File_Index,
										 SF_PTR_ACU_PROCESS_RINSE_AID_DISPENSE,
										 control->Data_Field_Index,
										 &loader) == FAIL)
	{
		LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, control->Data_Field_Index);
	}
	else
	{
		TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters = (TRIDGE_DETERGENT_PROCESS_PARAMETER_TYPE *)(void *)loader.Data;

		if ( FAIL == SettingFile__BasicLoader(SF_PTR_ACU_PRODUCT_CONFIGURATION_DATA,
											  DSPL_TRIDGE_SYSTEM_PARAMS,
											  &loader) )
		{
			LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)SF_PTR_ACU_PRODUCT_CONFIGURATION_DATA);
		}
		else
		{
			TD_Process_Data_Ptr->Tridge_Detergent_Parameters = (TRIDGE_DETERGENT_PARAMETER_TYPE *)(void *)loader.Data;
		}
	}
}

static uint8 GetReleaseTime(void)
{
	uint8 release_time = 0;

	switch (TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Detergent_Type)
	{
		case DETERGENT_TYPE_ENZIME:
			release_time = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Release_Time_Enzime;
			break;

		case DETERGENT_TYPE_BLEACH:
			release_time = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Release_Time_Bleach;
			break;

		case DETERGENT_TYPE_RINSE_AID:
			release_time = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Release_Time_RinseAid;
			break;

		default:
			// Keep release_time = 0;
			break;
	}

	return release_time;
}

static uint16 GetDispensingTime(uint16 ml_to_dispense_x10)
{
	// // Dispensing time in sec x 10 (rounded)
	uint32 dispensing_time = (((uint32)ml_to_dispense_x10 * GetReleaseTime()) + 5U) / 10U;

	if (dispensing_time > (uint32)__UNSIGNED_SHORT_MAX__)
	{
		dispensing_time = __UNSIGNED_SHORT_MAX__;
	}

	return (uint16)dispensing_time;
}

// Check if dispenser has to be emptied due to remaining detergent under last cycle threshold and return the amount of detergent to empty the dispenser.
static uint16 GetAmountToEmptyDispenser(void)
{
	uint8 last_cycle_threshold = 0;
	uint16 last_cycle_theshold_ml_x10;
	uint16 remaining_ml_x10;
	uint16 ml_to_dispense_x10 = 0;

	// Check if dispenser has to be emptied due to remaining detergent under last cycle threshold.
	switch (TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Detergent_Type)
	{
		case DETERGENT_TYPE_ENZIME:
			last_cycle_threshold = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Last_Cycle_Threshold_Enzime;
			break;

		case DETERGENT_TYPE_BLEACH:
			last_cycle_threshold = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Last_Cycle_Threshold_Bleach;
			break;

		case DETERGENT_TYPE_RINSE_AID:
			last_cycle_threshold = TD_Process_Data_Ptr->Tridge_Detergent_Parameters->Last_Cycle_Threshold_RinseAid;
			break;

		default:
			// Keep last_cycle_threshold = 0;
			break;
	}

	// Last Cycle Threshold in ml x 10 (since Last_Cycle_Threshold_X parameter is x 10)
	last_cycle_theshold_ml_x10 = (uint16)last_cycle_threshold * TD_Process_Data_Ptr->Tag_Detergent_Data.DoseAmount_ML;

	if (((uint32)TD_Process_Data_Ptr->Tag_Detergent_Data.ML_Remaining * 10U) < (uint32)__UNSIGNED_SHORT_MAX__)
	{
		remaining_ml_x10 = (uint16)(TD_Process_Data_Ptr->Tag_Detergent_Data.ML_Remaining * 10U);
	}
	else
	{
		remaining_ml_x10 = __UNSIGNED_SHORT_MAX__;
	}

	if (remaining_ml_x10 < last_cycle_theshold_ml_x10)
	{
		// Empty dispenser, dispense all the remaining detergent.
		ml_to_dispense_x10 = remaining_ml_x10;

		// Todo Add extra amount to be sure dispenser is emptied?
	}
	else
	{
		// Do nothing
	}

	return ml_to_dispense_x10;
}


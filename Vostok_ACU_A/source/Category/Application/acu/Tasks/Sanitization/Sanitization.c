/**
 *  @brief      It Initializes the module Sanitization and its variables
 *
 */
ACTIVE_TYPE Sanitization__Initialize(uint8 compartment, uint8 sf_index, uint8 displacement)
{
    SETTINGFILE_LOADER_TYPE loader;
    sint32 wash_probe_value;
    ACTIVE_TYPE return_value = ACTIVE;
    Stop_Task = FALSE;

    Sanitization_Result = TASK_SANITIZATION_SUCCESSFUL;

	if ( FAIL == SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DISPL_SANITIZATION, &loader) )
	{
		LOG_ADD_EXCEPTION(0, DISPL_SANITIZATION);
		Sanitization_Result = TASK_SANITIZATION_FAILED;
		return_value = INACTIVE;
	}
	else
	{
		Sanitization_Minimum_Temperature = ENDIANU16_STON(*((uint16*)((void*)(loader.Data))));  // Read the data from the setting file

		(void)Variable__GetValue(VARIABLE_WATER_TEMPERATURE,0,(sint32*)((void*)(&wash_probe_value)));

		if (wash_probe_value < (sint32)Sanitization_Minimum_Temperature)
		{
			Sanitization_Result = TASK_SANITIZATION_FAILED;
			return_value = INACTIVE;
		}
	}



    return ( return_value );
}


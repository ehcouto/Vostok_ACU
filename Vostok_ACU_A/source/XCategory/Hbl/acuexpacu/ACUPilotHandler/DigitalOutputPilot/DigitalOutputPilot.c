/**
 *  @brief      Update virtual pin according to processed value.
 *
 *  @param      HBL_LOAD_TYPE *load - Pointer to digital output load instance.
 */
static void DoPinUpdate(HBL_LOAD_TYPE * load)
{
	uint8 virtual_pin;
	if ((GetVirtualPinNumber(load, &virtual_pin) == TRUE) &&
	    (VIRTUALPIN_TABLE[virtual_pin].port != PORT_NULL))
	{
	    uint8 pin_val = (DigitalOutputPilot_Data[load->Pilot_Data_Index].Processed != 0)?1:0;

		// Invert pin value according to Hi Low Activation Bitmap
		pin_val ^= ~load->Hi_Low_Activation_Bitmap;
		pin_val &= 0x01;

		Gpio__PinWrite(VIRTUALPIN_TABLE[virtual_pin].port, VIRTUALPIN_TABLE[virtual_pin].pin, (BOOL_TYPE)pin_val);
	}
}


//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      Determines if the Virtual pin specified in the load is valid.
 *
 *  @param      HBL_LOAD_TYPE *load - Pointer to associated digital output load instance.
 *
 *  @param      *virtual_pin - returns the virtual pin number specified in the load instance to the calling function
 *
 *  @return     TRUE - the load instance defined a valid virtual pin
 *  @retval     FALSE - the load instance defined an invalid virtual pin
 */
static BOOL_TYPE GetVirtualPinNumber(HBL_LOAD_TYPE *load, uint8 *virtual_pin)
{
    BOOL_TYPE ret_val = TRUE;
    *virtual_pin = load->Pin_Ptr[0];
    if ( *virtual_pin >= VirtualPin__GetNumOf() )
    {
        LOG_ADD_EXCEPTION(INVALID_VIRTUAL_PIN, *virtual_pin);
        ret_val = FALSE;
    }
    return (ret_val);
}



/**
 * @brief - This function manages the Crossloads of type Master Load
 *
 * @param CROSSLOAD_DATA_TYPE * , CROSSLOAD_PARAMETERS_TYPE *
 */
static void MasterLoadHandler(HBL_CROSS_LOAD_TYPE *cfg_ptr, CROSSLOAD_DATA_TYPE * data_ptr, uint8 *hbl_prio_ptr)
{
    CROSSLOAD_PARAMETERS_TYPE *param_ptr = cfg_ptr->Parameter_Ptr;
    uint8 my_prio = GetLoadPriority(cfg_ptr->Crossload_Position, hbl_prio_ptr);
    uint8 load_check_index;
    uint8 load_clear_index;
    uint8 new_processed;
    uint8 position_index;

    switch (data_ptr->Generic_State)
    {
        case CROSSLOAD_STEADY_STATE:
        {
            uint8 *load_process_ptr;
            uint8 load_request_len;
            uint8 load_check_prio;
            uint8 max_check_prio = 0;
            BOOL_TYPE full_delay = FALSE;

            for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
            {
                if (data_ptr->Processed == OFF)
                {
                    load_clear_index = param_ptr->Load_Position[position_index].On;
                    load_check_index = param_ptr->Load_Position[position_index].Off;
                }
                else
                {
                    load_clear_index = param_ptr->Load_Position[position_index].Off;
                    load_check_index = param_ptr->Load_Position[position_index].On;
                }

                // Perform clear only if Master load priority is higher than load to clear
                // otherwise load should override that behavior
                // Clear here only when no transition occur
                if ((data_ptr->Processed == data_ptr->Requested) &&
                    (PASS == Hbl__SetLoadRequestByIndex(load_clear_index, (HBL_PRIORITY_TYPE)my_prio,
                    (uint8 *)Hbl__GetFlushBuffer())))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                {
                    ResetCrossLoadTransition(load_clear_index);
                }

                load_check_prio = GetLoadPriority(load_check_index, hbl_prio_ptr);

                if (my_prio >= load_check_prio)
                {
                    load_process_ptr = Hbl__GetProcessedLoad(load_check_index);
                    if (load_process_ptr != NULL)
                    {
                        load_request_len = Hbl__GetLoadDataSize(load_check_index);
                        if (memcmp(load_process_ptr, Hbl__GetFlushBuffer(), load_request_len) != 0)
                        {
                            full_delay = TRUE;
                        }

                        if (load_check_prio > max_check_prio)
                        {
                            max_check_prio = load_check_prio;
                        }
                    }
                }
            }

            // Perform Master load transition only if priorty is higher than maximum of other loads *
            if ((my_prio >= max_check_prio) &&
                (data_ptr->Processed != data_ptr->Requested))
            {
                new_processed = data_ptr->Processed;
                data_ptr->Generic_State = (data_ptr->Requested == OFF) ? CROSSLOAD_OFF_TRANSITION :
                    CROSSLOAD_ON_TRANSITION;

                if (full_delay)
                {
                    data_ptr->Timer = param_ptr->On_Delay + param_ptr->Off_Delay;
                    if(data_ptr->Timer == 0)
                    {
                        new_processed = data_ptr->Requested;
                    }
                }
                else
                {
                    data_ptr->Timer = param_ptr->On_Delay;
                    new_processed = data_ptr->Requested;
                }

                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    if (data_ptr->Processed == OFF)
                    {
                        load_clear_index = param_ptr->Load_Position[position_index].Off;
                        load_check_index = param_ptr->Load_Position[position_index].On;
                    }
                    else
                    {
                        load_clear_index = param_ptr->Load_Position[position_index].On;
                        load_check_index = param_ptr->Load_Position[position_index].Off;
                    }
                    // Perform clear only if Master load priorty is higher than load to clear
                    // otherwise load should override that behavior
                    if (PASS == Hbl__SetLoadRequestByIndex(load_clear_index, (HBL_PRIORITY_TYPE)my_prio,
                        (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                    {
                        ResetCrossLoadTransition(load_clear_index);
                    }

                    // Perform clear only if Delay Timer > 0 and Master load priorty is higher than load to clear
                    // otherwise load should override that behavior
                    if ((data_ptr->Timer > 0) &&
                        (PASS == Hbl__SetLoadRequestByIndex(load_check_index, (HBL_PRIORITY_TYPE)my_prio,
                        (uint8 *)Hbl__GetFlushBuffer())))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                    {
                        ResetCrossLoadTransition(load_check_index);
                    }

                }

                data_ptr->Processed = new_processed;
                if(data_ptr->Timer == 0)
                {
                    data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                }
            }

        }
        break;

        case CROSSLOAD_ON_TRANSITION:
        case CROSSLOAD_OFF_TRANSITION:
        {
            BOOL_TYPE timer_wait = IsLoadStatusOnTransition(cfg_ptr->Crossload_Position);
            uint8 new_requested = (data_ptr->Generic_State == CROSSLOAD_ON_TRANSITION) ? ON : OFF;
            new_processed = new_requested;

            //------------------------------------------------------------
            if(data_ptr->Timer > param_ptr->On_Delay)
            {
                new_processed = (new_processed == OFF) ? ON : OFF;
            }

            //------------------------------------------------------------
            if (data_ptr->Timer > 0)
            {
                //----------- Load-Off & Load-On Keep Off Process ------------
                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    timer_wait |= IsLoadStatusOnTransition(param_ptr->Load_Position[position_index].Off);
                    timer_wait |= IsLoadStatusOnTransition(param_ptr->Load_Position[position_index].On);
                }

                /* Request changed during transition: it resets transition if turning OFF loads before CrossLoad switch
                 * and updates ON/OFF transition according to the new request.
                 */
                if(data_ptr->Requested != new_requested)
                {
                    if(data_ptr->Timer > param_ptr->On_Delay)
                    {
                        data_ptr->Timer = 0;
                        timer_wait = TRUE;
                    }
                    new_processed = data_ptr->Requested;
                    data_ptr->Generic_State = (new_processed == OFF) ?
                        CROSSLOAD_OFF_TRANSITION : CROSSLOAD_ON_TRANSITION;
                }
                /* No request changes, but Processed is not the expected value: an Override has been performed after processing it.
                 * If this override happens before CrossLoad switch (Timer higher than On_Delay) it shorten time to On_Delay to prevent glitches
                 * If this override happens when turning OFF loads after CrossLoad switch it waits until the action stops
                 */
                else if(data_ptr->Processed != new_processed)
                {
                    if(data_ptr->Timer > param_ptr->On_Delay)
                    {
                        data_ptr->Timer = param_ptr->On_Delay;
                        new_processed = data_ptr->Processed;
                    }
                    timer_wait = TRUE;
                }

                if (timer_wait == FALSE)
                {
                    data_ptr->Timer--;
                    if(data_ptr->Timer == param_ptr->On_Delay)
                    {
                        new_processed = (new_processed == OFF) ? ON : OFF;
                    }
                }

                //----------- Load-Off & Load-On Keep Off Process ------------
                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    // Clear OFF load index except on last iteration going to OFF
                    if((data_ptr->Generic_State == CROSSLOAD_ON_TRANSITION) ||
                        (data_ptr->Timer > 0))
                    {
                        load_clear_index = param_ptr->Load_Position[position_index].Off;
                        if (PASS == Hbl__SetLoadRequestByIndex(load_clear_index, (HBL_PRIORITY_TYPE)my_prio, (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                        {
                            ResetCrossLoadTransition(load_clear_index);
                        }

                    }

                    // Clear ON load index except on last iteration going to ON
                    if((data_ptr->Generic_State == CROSSLOAD_OFF_TRANSITION) ||
                        (data_ptr->Timer > 0))
                    {
                        load_clear_index = param_ptr->Load_Position[position_index].On;
                        if (PASS == Hbl__SetLoadRequestByIndex(load_clear_index, (HBL_PRIORITY_TYPE)my_prio, (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                        {
                            ResetCrossLoadTransition(load_clear_index);
                        }
                    }
                }
            }

            if (data_ptr->Timer == 0)
            {
                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            }
            data_ptr->Processed = new_processed;
        }
        break;

        default:
            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            break;
    }
}

/**
 * @brief
 */
static void WaitAFSubcycleExecution(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Tank_Fill_Data.Subcycle_Executed == FALSE)
	{
		(void)SubCycle__RequestStart(0, Tank_Fill_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority);
	}
	else
	{
		VWI_WATER_SENSE_TYPE last_valid_water_status;

		last_valid_water_status = Vwi__GetLastValidWaterStatus();

		if ((last_valid_water_status == WS_WATER) || (last_valid_water_status == WS_UNSTABLE_WATER))
		{
			// Water is present
			Liters_In_Water_Tank = 0;
			Tank_State_Fill = TANK_STATE_FAIL;
			Tank_State_Drain = TANK_STATE_UNKNOWN;

			Fault__ConditionDetected(FAULT_ID_WATER_TANK_VALVE_NOT_FUNCTIONING);
		}
		else
		{
			// No Water
			NoWaterFoundInAfterFillChecks();
		}

		DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void WaitForPauseEnd(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Tank_Fill_NV_Data.H2O_Pause_Executed == FALSE)
	{
		if (Fault__IsInFaultState(FAULT_ID_NO_WATER_FILL_VALVE_PROBLEM) == FALSE)
		{
			Fault__ConditionDetected(FAULT_ID_NO_WATER);
		}

		(void)Supervisor__SetStart(0, FS_PAUSE_CONDITION);
	}
	else
	{
		DISPATCH_FSM_EVENT(FSM_EVENT_RESTARTFILL);
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void StartDelayTimer(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	Tank_Fill_NV_Data.Fill_Timer = 0;
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void WaitForTankFillDelay(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (Tank_Fill_NV_Data.Fill_Timer < ((uint16)Tank_Fill_Data.Class_A_Fill_Parameters->Tank_Fill_Delay_Time * (uint16)(5 * FILL_CALLS_PER_SECOND)))
	{
		Tank_Fill_NV_Data.Fill_Timer ++;
	}
	else
	{
		if (Liters_In_Tub_Before_Fill <	((uint16)Tank_Fill_Data.Class_A_Fill_Parameters->Liters_For_WI_Empty_Max * (uint16)5))
		{
			if ((Tank_Fill_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority != 0U) && (Interpreter__GetStatus().Sub_Cycle[COMPARTMENT_DEFAULT] == 0U))
			{
				DISPATCH_FSM_EVENT(FSM_EVENT_AFTERFILLWATERCHECKNEEDED);
			}
			else
			{
				// Fill Check subcycle is not present in setting file, or Fill is being executed inside a subcycle:
				// it is not possible to start Fill Check Subcycle, recovery action is to assume water is not present
				NoWaterFoundInAfterFillChecks();

				DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
			}

		}
		else
		{
			if (Liters_In_Tub > Liters_In_Tub_Before_Fill)
			{
				Liters_In_Water_Tank = Liters_In_Tub - Liters_In_Tub_Before_Fill;
			}
			else
			{
				Liters_In_Water_Tank = 0;
			}
			Liters_In_Tub = Liters_In_Tub_Before_Fill;
			Tank_State_Fill = TANK_STATE_UNKNOWN;
			Tank_State_Drain = TANK_STATE_UNKNOWN;
			Fill__SetBitmapBit(TANK_STATE_CLEAN);

			DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
		}
	}
}


/**
 * Initialize the Fill module and its variables.
 */
void Fill__Initialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		SETTINGFILE_LOADER_TYPE loader;

		// Discriminate if setting file is using Normal Fill or Class A Fill
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DISPL_CLASS_A_FILL, &loader) == PASS)
		{
			Class_A_Fill_Project = TRUE;

			// Copy parameters into variables to be used in cycle design
			Liters_For_Tank_Empty = (((CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data)->Liters_For_Tank_Empty) * (uint16)5;
			Liters_For_Tank_Full = (((CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data)->Liters_For_Tank_Full) * (uint16)5;
		}
		else
		{
			Class_A_Fill_Project = FALSE;

			Liters_For_Tank_Empty = 0;
			Liters_For_Tank_Full = 0;
		}

		if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
		{
			Fill_Sequence_ID = 0;
			memset(&Fill_Sequence_ID_Reference[0], 0, sizeof(Fill_Sequence_ID_Reference));

			Class_A_Fill_Bitmap = 0;
			Fill_Flow_Rate_Calibrated = 0; // It means calibration has never been completed

			memset(&Flow_Rate_Buffer[0], 0, sizeof(Flow_Rate_Buffer));

			if ((BOOL_TYPE)Class_A_Fill_Project != (BOOL_TYPE)FALSE)
			{
				Flow_Rate_Buffer[0] = ENDIANU16_STON(((CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data)->Flow_Rate_Nominal); //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
				Flow_Rate_Samples = 1;
			}
			else
			{
				Flow_Rate_Samples = 0;
			}

			Liters_In_Water_Tank = 0;
			Liters_In_Tub_Before_Fill = 0;
			Liters_In_Tank_Before_Fill = 0;
			Liters_Unchecked = 0;
			Tank_State_Fill = TANK_STATE_UNKNOWN;
			Tank_State_Drain = TANK_STATE_UNKNOWN;
		}
	}

	// Call specific processes initialization functions
	NormalFill__Initialize();
	ClassAFill__Initialize();
	TankFill__Initialize();
	TankDrain__Initialize();
}


PROCESS_RESULT_TYPE Fill__Process(PROCESS_CONTROL_TYPE* control)
{
    PROCESS_RESULT_TYPE result = PROCESS_COMPLETE;

    if (control == NULL)
    {
        LOG_ADD_EXCEPTION(PROCESS_CONTROL_IS_NULL, 0);
    }
    else if ((control->Compartment >= APPLIANCE_MAX_COMPARTMENTS) ||
    	/*	 (control->F1_F0_Flags >= (uint8)FILL_OPTION_COUNT) || */ //No need to check F1_F0_Flags since Fill Process support all F1_F0_Flags combinations
			 (control->Process_Instance > 0U))
    {
        LOG_ADD_EXCEPTION(PROCESS_CONTROL_PARAMS_EXCEED_LIMITS, 0);
    }
    else
    {
		FILL_CYCLE_TYPE fill_type;
    	CYCLE_STATE_TYPE cycle_state = Selector__GetCycleState(control->Setting_File_Index,
															   control->Compartment,
															   Interpreter__GetStatus().Main_Cycle[COMPARTMENT_DEFAULT]);

        if (cycle_state == CYCLE_STATE_RUN)
        {
            if (control->Sub_Cycle_Running != (BOOL_TYPE)FALSE)
            {
            	fill_type = FILL_SUB_CYCLE;
            }
            else
            {
            	fill_type = FILL_MAIN_CYCLE;
            }
        }
        else
        {
        	fill_type = FILL_PAUSE_CYCLE;
        }

        if (control->Step_Initializing != (BOOL_TYPE)FALSE)
    	{
    		// Increased each time a new Fill process is started
    		Fill_Sequence_ID ++;

    		Fill_Sequence_ID_Reference[fill_type] = Fill_Sequence_ID;
    	}

    	if (Fill_Sequence_ID_Reference[fill_type] == Fill_Sequence_ID)
    	{
			switch (control->F1_F0_Flags)
			{
				case FILL_OPTION_NORMAL_FILL:
					result = NormalFill__Manager(control);
					break;

				case FILL_OPTION_CLASS_A_FILL:
					result = ClassAFill__Manager(control);
					break;

				case FILL_OPTION_TANK_FILL:
					result = TankFill__Manager(control);
					break;

				case FILL_OPTION_TANK_DRAIN:
					result = TankDrain__Manager(control);
					break;
				default:
					// No other options
					break;
			}
    	}
    	else
    	{
    		// Another Fill Process instance has been started in a different cycle state, in this case the process
    		// execution is skipped because the process data may be no longer meaningful.
    	}
    }

    return result;
}

/**
 * It sets the required bit in Class_A_Fill_Bitmap variable
 *
 * @param  bit_index = the index of the bit to be set
 *
 * @return  None
 */
void Fill__SetBitmapBit(uint8 bit_index)
{
	if (bit_index < (sizeof(Class_A_Fill_Bitmap) * 8U))
	{
        BIT_SET8(Class_A_Fill_Bitmap, bit_index);
	}
}


/**
 * It clears the required bit in Class_A_Fill_Bitmap variable
 *
 * @param  bit_index = the index of the bit to be set
 *
 * @return  None
 */
void Fill__ClearBitmapBit(uint8 bit_index)
{
	if (bit_index < (sizeof(Class_A_Fill_Bitmap) * 8U))
	{
		BIT_CLR(Class_A_Fill_Bitmap, bit_index);
	}
}


/**
 * It returns whether the required bit of Class_A_Fill_Bitmap variable is set or not
 *
 * @param  bit_index = the index of the bit to be set
 *
 * @return  The bit status
 * @retval TRUE/FALSE if the bit is 1/0
 *
 */
BOOL_TYPE Fill__GetBitmapBit(uint8 bit_index)
{
	BOOL_TYPE ret_value = FALSE;

	if (bit_index < (sizeof(Class_A_Fill_Bitmap) * 8U))
	{
		ret_value = (BIT_TEST((uint32)Class_A_Fill_Bitmap, bit_index) != 0U) ? (TRUE) : (FALSE);
	}

	return ret_value;
}



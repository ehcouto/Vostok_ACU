/**
 *  @brief      Get cold reset behavior from cycle heading.
 *
 *  @param      uint8 setting_file - setting file index for where to find program header
 *  @param      uint8 compartment - identifier appliance compartment (0 based, 0 for single compartment appliances).
 *  @param      uint8 cycle - displacement of label in settings file program header
 *
 *  @return     CYCLE_COLD_RESET_BEHAVIOR_TYPE  restore_data
*/
CYCLE_COLD_RESET_BEHAVIOR_TYPE Selector__GetColdResetBehavior(uint8 sf_index, uint8 compartment, uint8 cycle)
{
    uint8  restore_data;
    uint8 const* cycle_header;

    restore_data = CYCLE_COLD_RESET_BEHAVIOR_CANCEL;


    if (GetProgramHeadingPointer(sf_index, compartment, cycle, &cycle_header) == PASS)
    {
        #define RESET_BEHAVIOR_OFFSET   (0)
        #define RESET_BEHAVIOR_MASK (0x07)

        restore_data = cycle_header[RESET_BEHAVIOR_OFFSET] & RESET_BEHAVIOR_MASK;
    }

    return ((CYCLE_COLD_RESET_BEHAVIOR_TYPE)restore_data);
}


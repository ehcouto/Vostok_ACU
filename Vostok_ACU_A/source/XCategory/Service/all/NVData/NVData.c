/**
 * @brief It Initializes the module NVData and its variables
 *
 */
void NVData__Initialize(void)
{
	MICRO_DECLARE_INTERRUPT_CONTEXT_LOCAL();
    uint32 segment_total_size;
    NVDATA__INITIALIZEDEVICE();

    #if (NVDATA_STORE_STATIC_FEATURE == ENABLED)
        segment_total_size = (DATAFLASH_STATICDATA_LIMIT - (uint32)DATAFLASH_STATICDATA_START);     //lint !e923 Lint: 923 cast from pointer to unsigned long [Encompasses MISRA 2004 Rule 11.1], [MISRA 2004 Rule 11.3]

        //Check if the allocated non volatile memory is sufficient to store the data
        if (NVDATA_STATIC_PAGE_SIZE < segment_total_size)
        {
            LOG_ADD_ERROR(INSUFFICIENT_STATIC_NONVOLATILE_MEMORY_ALLOCATED, segment_total_size);
            Mode__SetModeExtended(MODE_NORMAL, SUBMODE_NORMAL_ERROR);
        }
    #endif

    #if (NVDATA_STORE_APPLIANCE_AND_STATISTICS_FEATURE == ENABLED)
        NVDATA_HEADER_TYPE header1;
        NVDATA_HEADER_TYPE header2;

        segment_total_size = (DATAFLASH_APPLIANCE_LIMIT - (uint32)DATAFLASH_APPLIANCE_START) + (uint16)(DATAFLASH_STATISTICS_LIMIT - (uint32)DATAFLASH_STATISTICS_START); //lint !e923 Lint: 923 cast from pointer to unsigned long [Encompasses MISRA 2004 Rule 11.1], [MISRA 2004 Rule 11.3]

        //Check if the allocated non volatile memory is sufficient to store the data
        if (NVDATA_PAGE_SIZE < segment_total_size)
        {
            LOG_ADD_ERROR(INSUFFICIENT_APPLIENCE_OR_STATISTICS_NONVOLATILE_MEMORY_ALLOCATED, segment_total_size);
            Mode__SetModeExtended(MODE_NORMAL, SUBMODE_NORMAL_ERROR);
        }

    #endif

	// Disable all interrupts -------------------------------------------------------
	MICRO_SAVE_INTERRUPT_CONTEXT_LOCAL();

	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
    {

        #if (NVDATA_STORE_APPLIANCE_AND_STATISTICS_FEATURE == ENABLED)
			Micro__ServiceWatchdog();
            Next_Store_Address = 0xFFFFFFFF;

            DataFlash_Appliance_Filler = 0;
            DataFlash_Statistics_Filler = 0;
            NVData_Header.Signature = NVDATA_HEADER_SIGNATURE;

            header1.Signature = 0xFFFFFFFF;
            header2.Signature = 0xFFFFFFFF;

            NVData_Status.Byte = 0;
            NVDATA__READ((uint8 *)&header1, 0, sizeof(header1)); //lint !e928 Lint: 928 cast from pointer to pointer [MISRA 2004 Rule 11.4]
            NVDATA__READ((uint8 *)&header2, NVDATA_PAGE_SIZE, sizeof(header2)); //lint !e928 Lint: 928 cast from pointer to pointer [MISRA 2004 Rule 11.4]

            NVData_Status.Bit.Appliance_Data_Recovered = NVDATA_RESTORE_CORRUPTED;
            NVData_Status.Bit.Statistics_Data_Recovered = NVDATA_RESTORE_CORRUPTED;

            // Header 1 is good
            if (header1.Signature == NVDATA_HEADER_SIGNATURE)
            {
                if (header2.Signature == NVDATA_HEADER_SIGNATURE)
                {
                    /////////
                    if ((header2.Signature_Counter > header1.Signature_Counter) &&
                        (header2.Signature_Counter > 0))
                    {
                        // Restore from 2
                        Restore(NVDATA_PAGE_SIZE,0);
                        Next_Store_Address = 0;

                    }
                    else
                    {
                        // Restore from 1
                        Restore(0,NVDATA_PAGE_SIZE);
                        Next_Store_Address = NVDATA_PAGE_SIZE;
                    }
                }
                else if (header2.Signature == NVDATA_HEADER_SIGNATURE_INIT)
                {
                    ////////
                    // header 1 good, header 2 initialized
                    // Restore from 1
                    Restore(0,0xFFFFFFFF);
                    Next_Store_Address = NVDATA_PAGE_SIZE;
                }
                else
                {
                    ////////
                    // header 1 good, header 2  corrupted
                    Restore(0,0xFFFFFFFF);
                    Next_Store_Address = NVDATA_PAGE_SIZE;

                    // consider restored data as restored from backup
                    if (NVData_Status.Bit.Appliance_Data_Recovered  == NVDATA_RESTORE_OKAY)
                    {
                        NVData_Status.Bit.Appliance_Data_Recovered = NVDATA_RESTORE_OKAY_BACKUP;
                    }

                    if (NVData_Status.Bit.Statistics_Data_Recovered  == NVDATA_RESTORE_OKAY)
                    {
                        NVData_Status.Bit.Statistics_Data_Recovered = NVDATA_RESTORE_OKAY_BACKUP;
                    }

                }
            }
            else
            {
                // Header 1 is corrupted
                if (header2.Signature == NVDATA_HEADER_SIGNATURE)
                {
                    ////////
                    // header 1 bad, header 2 good
                    // Restore from 2
                    Restore(NVDATA_PAGE_SIZE,0xFFFFFFFF);
                    Next_Store_Address = 0;

                    // consider restored data as restored from backup
                    if (NVData_Status.Bit.Appliance_Data_Recovered  == NVDATA_RESTORE_OKAY)
                    {
                        NVData_Status.Bit.Appliance_Data_Recovered = NVDATA_RESTORE_OKAY_BACKUP;
                    }

                    if (NVData_Status.Bit.Statistics_Data_Recovered  == NVDATA_RESTORE_OKAY)
                    {
                        NVData_Status.Bit.Statistics_Data_Recovered = NVDATA_RESTORE_OKAY_BACKUP;
                    }
                }
                else
                {
                	// Blank NVData: get current Firmware and Setting file CRC for future reference
                	StoreNewFirmwareAndSettingFile();
                }
            }
        #endif

        #if (NVDATA_STORE_STATIC_FEATURE == ENABLED)
            // Static Board Data -----------------------------------------------------------
            RestoreStaticData();
        #endif

    }
	#if (NVDATA_STORE_APPLIANCE_AND_STATISTICS_FEATURE == ENABLED)

        NvdataInitialize();
        NVData_BrownOut = FALSE;

        if (Next_Store_Address == 0xFFFFFFFF)
        {
            FlushPage(NVDATA_PAGE_SIZE);
            header1.Signature =NVDATA_HEADER_SIGNATURE_INIT;
            NVDATA__WRITE(NVDATA_PAGE_SIZE,(uint8 *)&header1,sizeof(header1)); //lint !e928 Lint: 928 cast from pointer to pointer [MISRA 2004 Rule 11.4]

            // Not Consistent
            FlushPage(0);
            Next_Store_Address = 0;
            NVData_Header.Signature_Counter = 0;
        }
        else
        {
            // Consistent
            if (Next_Store_Address == 0)
            {
                // Make page 0 ready
                FlushPage(0);
            }
            else
            {
                // Make page 1 ready
                FlushPage(NVDATA_PAGE_SIZE);
            }
        }
		Micro__ServiceWatchdog();
    #endif
	MICRO_RESTORE_INTERRUPT_CONTEXT_LOCAL();
}


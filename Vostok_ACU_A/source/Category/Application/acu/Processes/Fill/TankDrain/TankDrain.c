/**
 * @brief
 */
static void ResetSubcycleExecution(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	TankDrain_Data.Subcycle_Executed = FALSE;
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void TankDrain(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	ON_OFF_TYPE valve_state = ON;

	if (TankDrain_NV_Data.Tank_Drain_Timer < ((uint16)TankDrain_Data.Class_A_Fill_Parameters->Time_For_Tank_Drain * (uint16)(5 * FILL_CALLS_PER_SECOND)))
	{
		(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_UPPER_TANK_VALVE, HBL_PRIORITY_APPLICATION_HI, (uint8 *) &valve_state);	//lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
		TankDrain_NV_Data.Tank_Drain_Timer ++;
	}
	else
	{
		if ((TankDrain_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority != 0U) && (Interpreter__GetStatus().Sub_Cycle[COMPARTMENT_DEFAULT] == 0U))
		{
			DISPATCH_FSM_EVENT(FSM_EVENT_WATERCHECK);
		}
		else
		{
			// Fill Check subcycle is not present in setting file, or Tank Drain is being executed inside a subcycle:
			// it is not possible to start Fill Check Subcycle, recovery action is to assume water is not present
			Tank_State_Fill = TANK_STATE_UNKNOWN;
			Tank_State_Drain = TANK_STATE_FAIL;
			Fill__ClearBitmapBit(TANK_STATE_CLEAN);
			Fault__ConditionDetected(FAULT_ID_WATER_TANK_VALVE_NOT_FUNCTIONING);

			Fill__SetBitmapBit(SENSED_DRAIN_REQUESTED_BY_TANK_DRAIN);

			DISPATCH_FSM_EVENT(FSM_EVENT_TANKDRAINCOMPLETED);
		}

	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void TankDrainInitialCheck(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	Liters_In_Tub_Before_Fill = Liters_In_Tub;
	Liters_In_Tank_Before_Fill = Liters_In_Water_Tank;

	Fill__ClearBitmapBit(SENSED_DRAIN_REQUESTED_BY_TANK_DRAIN);

	if ((Liters_In_Water_Tank < ( (uint16)TankDrain_Data.Class_A_Fill_Parameters->Liters_For_Tank_Empty_Max *(uint16)5 )) ||
		(Liters_In_Tub >= ( (uint16)TankDrain_Data.Class_A_Fill_Parameters->Liters_For_WI_Empty_Max * (uint16)5 )))

	{
		DISPATCH_FSM_EVENT(FSM_EVENT_TANKDRAINCOMPLETED);
	}
	else
	{
		Fill__ClearBitmapBit(DRAIN_CONFIRMED);
		Fill__ClearBitmapBit(TANK_STATE_CLEAN);
		TankDrain_NV_Data.Tank_Drain_Timer = 0;
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void WaitSubcycleExecution(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (TankDrain_Data.Subcycle_Executed == FALSE)
	{
		(void)SubCycle__RequestStart(0, TankDrain_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority);
	}
	else
	{
		VWI_WATER_SENSE_TYPE last_valid_water_status;

		last_valid_water_status = Vwi__GetLastValidWaterStatus();

		if ((last_valid_water_status == WS_WATER) || (last_valid_water_status == WS_UNSTABLE_WATER))
		{
			// Water is present
			uint32 sum;
			sum = (uint32)Liters_In_Tub + Liters_In_Water_Tank;
			if (sum > (uint16)__UNSIGNED_SHORT_MAX__)
			{
				sum = __UNSIGNED_SHORT_MAX__;
			}
			Liters_In_Tub = (uint16)sum;

			Liters_In_Tub_Before_Fill = Liters_In_Tub;
			Liters_In_Water_Tank = 0;
			Tank_State_Drain = TANK_STATE_PASSED;
			Fill__SetBitmapBit(TANK_STATE_CLEAN);
			Liters_Unchecked = 0;
		}
		else
		{
			// No Water
			Tank_State_Fill = TANK_STATE_UNKNOWN;
			Tank_State_Drain = TANK_STATE_FAIL;
			Fill__ClearBitmapBit(TANK_STATE_CLEAN);
			Fault__ConditionDetected(FAULT_ID_WATER_TANK_VALVE_NOT_FUNCTIONING);

			Fill__SetBitmapBit(SENSED_DRAIN_REQUESTED_BY_TANK_DRAIN);
		}

		DISPATCH_FSM_EVENT(FSM_EVENT_TANKDRAINCOMPLETED);
	}
}


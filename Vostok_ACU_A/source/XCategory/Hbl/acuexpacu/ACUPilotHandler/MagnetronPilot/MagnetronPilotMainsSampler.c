/**
 *  @brief      It Initializes the module MagnetronPilotMainsSampler and its variables
 */
void MagnetronPilotMainsSampler__Initialize(void)
{
    Pilot_Index = 0;
    memset(Mains_Sampler_Data, 0, sizeof(Mains_Sampler_Data));
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      Initialize magnetron pilot Mains Sampler instance.
 *
 *  @param      uint8 Pilot_Index - Index to associated magnetron Pilot instance.
 *              uint8 AC_Mains_Same_Phase - Flag to indicate AC and mains phase: True: Same Phase / False: Reversed Phaser.
 *              uint8 Mains_Feedback_Pin - Index into virtual pin table to find the Mains feedback signal.
 *
 *  @return     void
 */
void MagnetronPilotMainsSampler__InitializeInstance(uint8 Parent_Pilot_Index, uint8 AC_Mains_Same_Phase, uint8 Mains_Feedback_Pin)
{
    uint8 table_index;

    Pilot_Index = Parent_Pilot_Index;

    Mains_Sampler_Data[Pilot_Index].AC_Mains_Same_Phase = AC_Mains_Same_Phase;
    Mains_Sampler_Data[Pilot_Index].Mains_Feedback_Pin = Mains_Feedback_Pin;

    //! Initialize mains pin
    table_index = Mains_Sampler_Data[Pilot_Index].Mains_Feedback_Pin;
    if (VIRTUALPIN_TABLE[table_index].port != PORT_NULL)
    {
        Gpio__PinConfig(VIRTUALPIN_TABLE[table_index].port, VIRTUALPIN_TABLE[table_index].pin, VIRTUALPIN_TABLE[table_index].pin_config);
    }

    //! Initialize mains parameter
    Mains_Sampler_Data[Pilot_Index].Mains_Period = ONE_PERIOD_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].Counter = MAX_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].High_Counter = 0;
    Mains_Sampler_Data[Pilot_Index].Low_Counter = MAX_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].Max_Count = MAX_COUNT_50HZ;
    Mains_Sampler_Data[Pilot_Index].Min_High_Level_Count = MIN_HIGH_LEVEL_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].Min_Low_Level_Count = MIN_LOW_LEVEL_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].Max_L_Pos_Count = MAX_L_POS_COUNT_50HZ;
    Mains_Sampler_Data[Pilot_Index].Min_L_Pos_Count = MIN_L_POS_COUNT_60HZ;
    Mains_Sampler_Data[Pilot_Index].High_Pulse = 0;
    Mains_Sampler_Data[Pilot_Index].Mains_Detect_State = MAINS_DETECT_STATE_UNKNOWN;
    Mains_Sampler_Data[Pilot_Index].Mains_Detect_State_Changed = FALSE;
    Mains_Sampler_Data[Pilot_Index].Mains_Detect_Early_Warning = FALSE;
    Mains_Sampler_Data[Pilot_Index].Mains_Freq_State = MAINS_FREQ_ERROR;
    Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter = 0;
    Mains_Sampler_Data[Pilot_Index].Mains_Period_Resume_Counter = 0;
    MainsSamplerRecalcEvents(DEFAULT_L_POS_COUNT_60HZ);
}


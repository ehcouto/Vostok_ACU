/**
 *  @brief      Handler called at every 200us.
 */
void MagnetronPilotMainsSampler__200usHandler(uint8 Parent_Pilot_Index)
{
    //! Save Pilot_Index before entering interrupt handler
    uint8 Pilot_Index_Bak = Pilot_Index;

    Pilot_Index = Parent_Pilot_Index;

    uint8 mains_pinState = 0;
    uint8 table_index = Mains_Sampler_Data[Pilot_Index].Mains_Feedback_Pin;
    if (VIRTUALPIN_TABLE[table_index].port != PORT_NULL)
    {
        mains_pinState = Gpio__PinRead(VIRTUALPIN_TABLE[table_index].port, VIRTUALPIN_TABLE[table_index].pin);
    }

    if (Mains_Sampler_Data[Pilot_Index].High_Pulse == TRUE)
    {
        Mains_Sampler_Data[Pilot_Index].High_Counter++;

        if (mains_pinState == 0)
        {
            if (Mains_Sampler_Data[Pilot_Index].High_Counter > Mains_Sampler_Data[Pilot_Index].Min_High_Level_Count) // the condition of accepting the fall edge coming.
            {
                MainsSamplerRecalcEvents(Mains_Sampler_Data[Pilot_Index].Counter);
                Mains_Sampler_Data[Pilot_Index].Low_Counter = 0;
                Mains_Sampler_Data[Pilot_Index].High_Pulse = FALSE;
            }
        }
    }
    else
    {
        Mains_Sampler_Data[Pilot_Index].Low_Counter++;

        if (mains_pinState == 1)
        {
            if (Mains_Sampler_Data[Pilot_Index].Low_Counter > Mains_Sampler_Data[Pilot_Index].Min_Low_Level_Count) // the condition of accepting the rise edge coming.
            {
                if (Mains_Sampler_Data[Pilot_Index].Mains_Period < Mains_Sampler_Data[Pilot_Index].Counter)
                {
                    ++Mains_Sampler_Data[Pilot_Index].Mains_Period;
                }
                else if (Mains_Sampler_Data[Pilot_Index].Mains_Period > Mains_Sampler_Data[Pilot_Index].Counter)
                {
                    --Mains_Sampler_Data[Pilot_Index].Mains_Period;
                }

                if (((Mains_Sampler_Data[Pilot_Index].Mains_Period >= MAINS_LOW_LIMIT_60HZ) && (Mains_Sampler_Data[Pilot_Index].Mains_Period <= MAINS_HIGH_LIMIT_60HZ)) ||
                    ((Mains_Sampler_Data[Pilot_Index].Mains_Period >= MAINS_LOW_LIMIT_50HZ) && (Mains_Sampler_Data[Pilot_Index].Mains_Period <= MAINS_HIGH_LIMIT_50HZ)))
                {
                    Mains_Sampler_Data[Pilot_Index].Mains_Freq_State = MAINS_FREQ_OK;
                    MAINS_PERIOD_SYNC(Pilot_Index, Mains_Sampler_Data[Pilot_Index].Mains_Period);
                }
                else
                {
                    Mains_Sampler_Data[Pilot_Index].Mains_Freq_State = MAINS_FREQ_ERROR;
                }

                Mains_Sampler_Data[Pilot_Index].Counter = 0;
                Mains_Sampler_Data[Pilot_Index].High_Counter = 0;
                Mains_Sampler_Data[Pilot_Index].High_Pulse = TRUE;
            }
        }
    }

    MAINS_EVENT_TYPE mains_event = MAINS_NO_EVENT;

    MainsSamplerMainsLostDetection();

    if (Mains_Sampler_Data[Pilot_Index].Counter == Mains_Sampler_Data[Pilot_Index].Zero_Cross_LH_Cnt1)
    {
        mains_event = ((Mains_Sampler_Data[Pilot_Index].AC_Mains_Same_Phase == TRUE) ? MAINS_ANGLE_0_EVENT : MAINS_ANGLE_180_EVENT);
        MainsSamplerMainsFoundDetection();
    }
    else if (Mains_Sampler_Data[Pilot_Index].Counter == Mains_Sampler_Data[Pilot_Index].L_Pos_Cnt && Mains_Sampler_Data[Pilot_Index].High_Pulse == TRUE)
    {
        mains_event = ((Mains_Sampler_Data[Pilot_Index].AC_Mains_Same_Phase == TRUE) ? MAINS_ANGLE_90_EVENT : MAINS_ANGLE_270_EVENT);
    }
    else if (Mains_Sampler_Data[Pilot_Index].Counter == Mains_Sampler_Data[Pilot_Index].Zero_Cross_HL_Cnt2)
    {
        mains_event = ((Mains_Sampler_Data[Pilot_Index].AC_Mains_Same_Phase == TRUE) ? MAINS_ANGLE_180_EVENT : MAINS_ANGLE_0_EVENT);
        MainsSamplerMainsFoundDetection();
    }
    else if (Mains_Sampler_Data[Pilot_Index].Counter == Mains_Sampler_Data[Pilot_Index].L_Neg_Cnt && Mains_Sampler_Data[Pilot_Index].High_Pulse == FALSE)
    {
        mains_event = ((Mains_Sampler_Data[Pilot_Index].AC_Mains_Same_Phase == TRUE) ? MAINS_ANGLE_270_EVENT : MAINS_ANGLE_90_EVENT);
    }

    MAINS_EVENT_HANDLER(Pilot_Index, mains_event);

    if (Mains_Sampler_Data[Pilot_Index].Counter < Mains_Sampler_Data[Pilot_Index].Max_Count)
    {
        Mains_Sampler_Data[Pilot_Index].Counter++;
    }

    if (Mains_Sampler_Data[Pilot_Index].Low_Counter > Mains_Sampler_Data[Pilot_Index].Max_Count)
    {
        Mains_Sampler_Data[Pilot_Index].Low_Counter = Mains_Sampler_Data[Pilot_Index].Max_Count;
    }

    if (Mains_Sampler_Data[Pilot_Index].High_Counter > Mains_Sampler_Data[Pilot_Index].Max_Count)
    {
        Mains_Sampler_Data[Pilot_Index].High_Counter = Mains_Sampler_Data[Pilot_Index].Max_Count;
    }

    //! Restore Pilot_Index before exiting interrupt handler
    Pilot_Index = Pilot_Index_Bak;
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief      Handler called at every 5ms.
 * @param      load
 * @return
 */
//---------------------------------------------------------------------------------------------------------------------
void MagnetronPilotMainsSampler__AsynchHandler(uint8 Parent_Pilot_Index)
{
    Pilot_Index = Parent_Pilot_Index;

    //! Check if there is mains error msg needed to be published
    if (Mains_Sampler_Data[Pilot_Index].Mains_Detect_State_Changed == TRUE)
    {
        Mains_Sampler_Data[Pilot_Index].Mains_Detect_State_Changed = FALSE;
        if (Mains_Sampler_Data[Pilot_Index].Mains_Detect_State == MAINS_DETECT_STATE_FOUND)
        {
            LOG_ADD_EXCEPTION(MAINS_SIGNAL_FOUND, Pilot_Index);
        }
        else
        {
            LOG_ADD_EXCEPTION(MAINS_SIGNAL_LOST, Pilot_Index);
        }
    }

    if (Mains_Sampler_Data[Pilot_Index].Mains_Detect_Early_Warning == TRUE)
    {
        Mains_Sampler_Data[Pilot_Index].Mains_Detect_Early_Warning = FALSE;
        LOG_ADD_EXCEPTION(MAINS_SIGNAL_EARLY_WARNING, Pilot_Index);
    }
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief      It return the mains detection status
 * @param      Pilot
 * @return
 */
//---------------------------------------------------------------------------------------------------------------------
MAINS_FREQUENCY_TYPE MagnetronPilotMainsSampler__GetMainsFrequencyStatus(uint8 Parent_Pilot_Index)
{
    MAINS_FREQUENCY_TYPE state = MAINS_FREQ_ERROR;

    if (Mains_Sampler_Data[Pilot_Index].Mains_Detect_State == MAINS_DETECT_STATE_FOUND)
    {
        state = Mains_Sampler_Data[Parent_Pilot_Index].Mains_Freq_State;
    }

    return state;
}


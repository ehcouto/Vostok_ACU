/**
 * Initialize the TankDrain module's variables
 */
void TankDrain__Initialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
		{
			Tank_Drain_State = FSM_STATE_NONE;
			TankDrain_NV_Data.Tank_Drain_Timer = 0;
		}

		memset(&TankDrain_Data, 0, sizeof(TankDrain_Data));

		TankDrain_Data.Process_Parameters_Loading = FAIL;
		TankDrain_Data.Subcycle_Executed = FALSE;
	}
}


/**
 * It implements the Tank Drain process
 */
PROCESS_RESULT_TYPE TankDrain__Manager(PROCESS_CONTROL_TYPE* control)
{
	PROCESS_RESULT_TYPE result = PROCESS_COMPLETE;

	if ((control->Step_Initializing != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Reset != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Pause != (BOOL_TYPE)FALSE))
	{
		TankDrain_Data.Process_Parameters_Loading = ReadProcessParameters(control);
	}

	if (TankDrain_Data.Process_Parameters_Loading == PASS)
	{
		if (control->Step_Initializing != (BOOL_TYPE)FALSE)
		{
			// Initialize this instance of the Tank Drain process.
			TankDrainFsmInitialize(&Tank_Drain_State);
		}

		if (control->Resume_From_Reset == (BOOL_TYPE)FALSE)
		{
			// TankDrainFsmExecute() not called in first iteration after a cold reset to avoid a spurious Valve turning on in case of
			// long power fail: actually, in this case, Tank Drain process would be called for just one iteration before going in PAUSE.
			TankDrainFsmExecute(&Tank_Drain_State);
		}

		result = ((Tank_Drain_State == FSM_STATE_TANK_DRAIN_COMPLETE) ? PROCESS_COMPLETE : PROCESS_BUSY);
	}

	return result;
}

void TankDrain__SetSubcycleExecuted(uint8 compartment, uint8 subcycle_id, SUB_CYCLE_END_TYPE subcycle_end_code)
{
	if (Selector__GetCycleNameId(0, compartment , subcycle_id) == CYCLE_NAME_SPECIFIC_FILL_CHECK_WATER)
	{
		if ((subcycle_end_code == SUB_CYCLE_END_COMPLETED) || (subcycle_end_code == SUB_CYCLE_END_SET_STATE_PROCESS))
		{
			TankDrain_Data.Subcycle_Executed = TRUE;
		}
	}
}

//=====================================================================================================================
//-------------------------------------- Private Functions ------------------------------------------------------------
//=====================================================================================================================
static PASS_FAIL_TYPE ReadProcessParameters(PROCESS_CONTROL_TYPE* control)
{
	PASS_FAIL_TYPE result = FAIL;
	SETTINGFILE_LOADER_TYPE loader;

	if ( FAIL == SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS,
										  DISPL_CLASS_A_FILL,
										  &loader) )
	{
		LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)SF_PTR_ACU_ALGORITHM_PARAMETERS);
	}
	else
	{
		TankDrain_Data.Class_A_Fill_Parameters = (CLASS_A_FILL_PARAMETER_TYPE *)(void*)loader.Data;
		result = PASS;
	}

	return result;
}



//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void SaveTagSerialNumber(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	TD_Process_NV_Data_Ptr->Tag_Saved_Serial_Number = TD_Process_Data_Ptr->Tag_Serial_Number;
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void CheckCartridge(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	switch (TD_Process_Data_Ptr->Tag_Read_Status)
	{
		case TAG_READ_STATUS_WAITING:
			// Nothing to do (coming from Reset or Pause, waiting for first tag status data)
			break;
		case TAG_READ_STATUS_GOOD:
			if (CartridgeDetectionMonitor__GetFstTagStatusReadDone() == FALSE)
			{
				// Cartridge switch opened during process execution: terminate process.
				DISPATCH_FSM_EVENT(FSM_EVENT_NO_CARTRIDGE_DATA);
			}
			else
			{
				if (TD_Process_NV_Data_Ptr->Tag_Saved_Serial_Number != TD_Process_Data_Ptr->Tag_Serial_Number)
				{
					TD_Process_NV_Data_Ptr->Tag_Saved_Serial_Number = TD_Process_Data_Ptr->Tag_Serial_Number;

					if ((uint8)TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Empty_Dispenser != 0U)
					{
						// If cartridge has been changed, we need to check again the emptying conditions
						DISPATCH_FSM_EVENT(FSM_EVENT_CARTRIDGE_CHANGED);
					}
				}
				else
				{
					// Normal operation
				}
			}
			break;

		case TAG_READ_STATUS_KO:
		default:
			DISPATCH_FSM_EVENT(FSM_EVENT_NO_CARTRIDGE_DATA);
			break;
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void StartDispensingTimer(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	uint16 ml_to_dispense_x10;
	if ((uint8)TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Empty_Dispenser == 0U)
	{
		// Normal detergent dispensing
		uint8 dose_to_dispense = TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Dose_To_Dispense;

		if (TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Modifier_Table_Index != (uint8)SF_DISPL_INVALID_VALUE)
		{
			sint32 modified_value;

			modified_value = Modifier__GetValue(TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Dose_To_Dispense,
												TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Modifier_Table_Index);
			if (modified_value < 0)
			{
				modified_value = 0;
			}
			else if (modified_value > __UNSIGNED_CHAR_MAX__)
			{
				modified_value = __UNSIGNED_CHAR_MAX__;
			}
			else
			{
				// Do nothing, modified_value fits in uint8
			}

			dose_to_dispense = (uint8)modified_value;
		}

		// Amount of detergent to dispense in ml x 10 (since Dose_To_Dispense is x 10)
		ml_to_dispense_x10 = (uint16)dose_to_dispense * TD_Process_Data_Ptr->Tag_Detergent_Data.DoseAmount_ML;
	}
	else
	{
		// Check if dispenser has to be emptied due to remaining detergent under last cycle threshold.
		ml_to_dispense_x10 = GetAmountToEmptyDispenser();
	}

	// Dispensing time in sec x 10 (rounded)
	TD_Process_NV_Data_Ptr->Dispensing_Timer = GetDispensingTime(ml_to_dispense_x10);
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief
 */
static void DispenseDetergent(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	if (TD_Process_Data_Ptr->Tag_Read_Status == TAG_READ_STATUS_GOOD)
	{
		if (TD_Process_NV_Data_Ptr->Dispensing_Timer > 0U)
		{
			if (TD_Process_Data_Ptr->Tag_Detergent_Data.PumpNumber < ELEMENTS_IN_ARRAY(Pumps_Id_Array))
			{
				uint8 pump_index = Hbl__GetLoadIndexByLoadID(Pumps_Id_Array[TD_Process_Data_Ptr->Tag_Detergent_Data.PumpNumber]);

				if (pump_index != HBL_INDEX_INVALID)
				{
					uint8 pump_state = (uint8)ON;
					(void)Hbl__SetLoadRequestByIndex(pump_index, HBL_PRIORITY_APPLICATION_HI, &pump_state);
				}
			}

			TD_Process_NV_Data_Ptr->Dispensing_Timer --;
		}
		else
		{
			DISPATCH_FSM_EVENT(FSM_EVENT_TIMER_EXPIRED);
		}
	}
	else
	{
		// Nothing to do
	}
}

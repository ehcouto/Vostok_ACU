/**
 *  @brief      Get the Cycle User Phase Name for sf_index compartment cycle phase.
 *
 *  @param      uint8 sf_index - Setting file index for where to find program header.
 *  @param      uint8 compartment - Identifier appliance compartment (0 based, 0 for single compartment appliances).
 *  @param      uint8 cycle - Displacement of Cycle in settings file program header pointer.
 *  @param      uint8 phase - Cycle Phase of requested User Phase Name.
 *
 *  @return     USER_PHASE_NAME_TYPE user_phase_name_id - User phase name enumeration id for compartment cycle phase.
 *  @retval     0xFFFF  - Invalid Cycle (not found).
 *  @retval     0x0000  - Default, cycle developer did not set a user phase name for this or previous phases.
 *  @retval     1-65534 - User phase name enumeration id for compartment cycle phase.
 *
*/
USER_PHASE_NAME_TYPE Selector__GetUserPhaseName(uint8 sf_index, uint8 compartment, uint8 cycle, uint8 phase)
{
    USER_PHASE_NAME_TYPE user_phase_name = USER_PHASE_NAME_INVALID;

    if (Selector__GetCycleState(sf_index, compartment, cycle) == CYCLE_STATE_RUN)
    {
        SETTINGFILE_LOADER_TYPE sf_loader;
        PASS_FAIL_TYPE load_success = SettingFile__MSettingBasicLoader(sf_index, Program_Heading_Pointers[compartment],
                                                                       cycle, &sf_loader);
        if (load_success == PASS)
        {
            user_phase_name = USER_PHASE_NAME_DEFAULT;
            uint8 user_phase_name_data_size = sf_loader.Length - USER_PHASE_NAMES_OFFSET;
            for (uint8 index = 0; index < user_phase_name_data_size; index += 3)
            {
                if (sf_loader.Data[USER_PHASE_NAMES_OFFSET + index] == 0xFF)
                {
                    // if padded for word alignment or if displacement data structure extended in the future.
                    break;
                }
                if (sf_loader.Data[USER_PHASE_NAMES_OFFSET + index] <= phase)
                {
                    user_phase_name = (USER_PHASE_NAME_TYPE)COMBINE_BYTES(sf_loader.Data[USER_PHASE_NAMES_OFFSET + index + 2],
                                                                             sf_loader.Data[USER_PHASE_NAMES_OFFSET + index + 1]);
                }
            }
        }
    }

    return (user_phase_name);
}


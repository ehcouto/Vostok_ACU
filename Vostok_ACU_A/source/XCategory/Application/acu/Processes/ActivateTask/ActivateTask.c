// There must be exactly TASK_COUNT tasks defined in the TASK_LIST[] array.
#define TASK_COUNT ELEMENTS_IN_ARRAY(TASK_LIST)

// The number of compartments multiplied by the number of tasks must not exceed 255.
CT_ASSERT(APPLIANCE_MAX_COMPARTMENTS * TASK_COUNT < 256);

//  --- Private Properties --------------------------------------------------------------------------------------------

// -- Private Constant Declarations --

//! The name of this module for use by the Log module.
#define MODULE_NAME MODULE_ACTIVATE_TASK


// -- Private Enumerated Constants Declarations --

//! List of values to compare with the F1_F0_Flags.
typedef enum ACTIVATE_TASK_OPTION_ENUM
{
    TASK_OPTION_ACTIVATE,                           //!< F1=0, F0=0
    TASK_OPTION_DEACTIVATE,                         //!< F1=0, F0=1
    TASK_OPTION_COUNT
} ACTIVATE_TASK_OPTION_TYPE;


//! A list of log message IDs that are generated by this module.
typedef enum MODULE_ACTIVATE_TASK_LOG_MESSAGE_ID_ENUM
{
    INVALID_PROCESS_CONTROL = 1,
    INVALID_PROCESS_PARAMETERS,
    INVALID_TASK_MANIPULATION_PARAMETER,
    NON_EXISTENT_TASK
} MODULE_ACTIVATE_TASK_LOG_MESSAGE_ID_TYPE;



// -- Private Type Declarations --

typedef struct ACTIVATE_TASK_PARAMETER_STRUCT
{
    uint8 Task_Code;
    uint8 Task_Displacement;
} ACTIVATE_TASK_PARAMETER_TYPE;


// -- Private Flash-Based Constant Definitions --

//! A data structure that can be compared to a task definition to see if it is disabled.
static const void* DISABLED_TASK[] = { NULL, NULL, NULL, NULL };


// -- Private Variable Definitions --
#pragma location = "_appliance_segment"
static uint8 Task_Active[BIT_STORAGE_SIZE(APPLIANCE_MAX_COMPARTMENTS * TASK_COUNT)];
static uint8 Task_Paused[BIT_STORAGE_SIZE(APPLIANCE_MAX_COMPARTMENTS * TASK_COUNT)];

//  --- Private Function Prototypes -----------------------------------------------------------------------------------

static void ExecuteTaskManipulation(uint8 compartment, uint8 task, BOOL_TYPE resume, TASK_EVENT_ACTION_TYPE action);


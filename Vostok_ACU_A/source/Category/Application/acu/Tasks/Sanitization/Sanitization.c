/**
 * The function called during the execution of the task
 * @param compartment that run the task (inside)
 * @return the status of the task (ACTIVE or INACTIVE)
 */
ACTIVE_TYPE Sanitization__Task(uint8 compartment)
{
	DOOR_STATUS_TYPE door_status;
	ACTIVE_TYPE return_value = ACTIVE;
    door_status = DoorMonitor__GetDoorStatus();

    //if ((wash_probe_value < Sanitization_Minimum_Temperature)||(door_status == DOOR_STATUS_OPEN) || (Stop_Task == TRUE))
    if ((door_status == DOOR_STATUS_OPEN) || ((uint8)Stop_Task == (uint8)TRUE))
    {
    	Sanitization_Result = TASK_SANITIZATION_FAILED;
    	return_value = INACTIVE;
    }
    else
    {
    	Sanitization_Result = TASK_SANITIZATION_SUCCESSFUL;
    }
    return(return_value);
}


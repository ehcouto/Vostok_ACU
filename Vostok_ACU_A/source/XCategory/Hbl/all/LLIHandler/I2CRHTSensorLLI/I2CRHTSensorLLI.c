/**
 * @brief   Asynchronous processing of a the LLI
 * @param   input: data related to the instance of the LLI
 * @return  PASS if the LLI was successfully processed, else FAIL
 */
BOOL_TYPE I2CRHTSensorLLI__AsynchProcess(HBL_LLI_TYPE * input)
{
	BOOL_TYPE retval = FALSE;

    if ((input->Data_Index < HBL_LLI_NUM_I2C_RHT_HUMIDITY_SENSOR) || (input->Data_Index < HBL_LLI_NUM_I2C_RHT_TEMPERATURE_SENSOR))
    {
		I2C_RHT_SENSOR_PARAM_TYPE *param_ptr = (I2C_RHT_SENSOR_PARAM_TYPE *) (void *)(input->Parameter_Ptr.Data);

        switch (param_ptr->Device_Type)
        {
			#if ((SHT2X_TOTAL_HUMIDITY_CHANNELS_USED != 0) || (SHT2X_TOTAL_TEMPERATURE_CHANNELS_USED != 0))
            case SHT2X_RHT_DEVICE_TYPE:
                if(SHT2X_Instance_Handler_ID == &(input->Parameter_Ptr))
                {
                    SHT2xCounterHandler();
                    SHT2XResetHandler(input);
                }
                break;
            #endif

            default:
                break;
        }
        retval = TRUE;
    }
	return (retval);
}


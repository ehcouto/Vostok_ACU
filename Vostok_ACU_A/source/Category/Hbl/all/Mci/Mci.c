/**
 * Set wash motor type. Call it after Mci__SetModuleType
 *
 */
void Mci__SetWashMotorType(APL_WASH_MOTOR_TYPE wash_type)
{
	Mci_Wash_Motor_Type = wash_type; /* Set Mci Wash Pump */

	if((wash_type == WM_SINGLE_SPEED_SYNCH_SPH_AW)  ||
	   (wash_type == WM_SINGLE_SPEED_SYNCH_SPH_PW))
	{
		if(Mci_Params_Loaded == FALSE)
		{
			Mci__ParameterLoading();
			if(Mci_Params_Loaded == TRUE)
			{
				Mcl__Initialize();
				Mcl__SetWashMotorType(wash_type);
				LOG_ADD_EVENT(MCI_EVENT_GWS_INITIALIZED, Mcl__GetParamsID());
			}
		}
	}
	else if(wash_type == WM_SINGLE_SPEED_ASYNCH_SPH_PSC)
	{
		MCI_INITIALIZE_ALT_MOTOR(wash_type); //Initialize Active Power for RIF Motor
		LOG_ADD_EVENT(MCI_EVENT_RIF_INITIALIZED, Mci_Wash_Motor_Type);
	}
}





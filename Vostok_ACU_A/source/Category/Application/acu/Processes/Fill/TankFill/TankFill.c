/**
 * @brief
 */
static void InitializeTankFillVariables(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	Liters_In_Tub_Before_Fill = Liters_In_Tub;
	Liters_In_Tank_Before_Fill = Liters_In_Water_Tank;

	if (Liters_In_Water_Tank >= ((uint16)Tank_Fill_Data.Class_A_Fill_Parameters->Liters_For_Tank_Empty_Max * (uint16)5))
	{
		DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
	}
	else
	{
		if ((Liters_In_Tub >= ((uint16)Tank_Fill_Data.Class_A_Fill_Parameters->Liters_For_WI_Empty_Max * (uint16)5)) &&
			((Tank_State_Fill != TANK_STATE_PASSED) || (Tank_State_Drain != TANK_STATE_PASSED)))
		{
			DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
		}
		else
		{
			Tank_State_Fill = TANK_STATE_RUNNING;
			Fill__ClearBitmapBit(TANK_STATE_CLEAN);
			DISPATCH_FSM_EVENT(FSM_EVENT_STARTTANKFILL);
		}
	}
}


/**
 * @brief
 */
static void FillWater(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	switch (Fill__ClassAFillCore(&Tank_Fill_Data, &Tank_Fill_NV_Data, FILL_OPTION_TANK_FILL))
	{
		case FILL_CORE_NO_WATER_DETECTED:
			DISPATCH_FSM_EVENT(FSM_EVENT_NOWATERDETECTED);
			break;

		case FILL_CORE_WATER_FILL_OVER:
			DISPATCH_FSM_EVENT(FSM_EVENT_WATERFILLOVER);
			break;

		default:	// Includes FILL_CORE_WATER_CHECK_NEEDED (that conn't be returned
					// in case of Tank Fill) and FILL_CORE_NO_STATE_CHANGE
			break;
	}
}


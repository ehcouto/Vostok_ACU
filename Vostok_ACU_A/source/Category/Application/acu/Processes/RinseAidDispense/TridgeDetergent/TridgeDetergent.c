static const HBL_LOAD_ENUM_TYPE Pumps_Id_Array[DETERGENT_TYPES_NUM] = {HBL_BULK_DISPENSER_1, HBL_BULK_DISPENSER_2, HBL_BULK_DISPENSER_3};


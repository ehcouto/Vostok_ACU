/**
 * It updates the state of the DOS Pin, based on the state of Push and Pull sequences
 *
 * @return  None
 */
static void UpdateDOSPinState(void)
{
	if (DOS_NV_Data.Pull_State == DOS_PULL_COMPLETE)
	{
		DOS_Data.DOS_Pin_State = DOS_PIN_FULLY_RETRACTED;
	}
	else if (DOS_NV_Data.Pull_State > DOS_PULL_REQUIRED)
	{
		DOS_Data.DOS_Pin_State = DOS_PIN_RETRACTING;
	}
	else
	{
		if (DOS_NV_Data.Push_State == DOS_PUSH_COMPLETE)
		{
			DOS_Data.DOS_Pin_State = DOS_PIN_FULLY_EXTRACTED;
		}
		else
		{
			DOS_Data.DOS_Pin_State = DOS_PIN_EXTRACTING;
		}
	}
}

    //! Counter for determining a millisecond has elapsed using Millis free running counter.
    static uint32 Millisecond_Counter;

    //! Number of deciseconds since power up. Used to wait for Input settling before running a cycle.
    static uint16 Deciseconds_Since_Power_Up;

    //! Maximum number of seconds to wait for system to stabilize before running cycles.
    #define POWER_UP_STABILIZATION_TIME_MAX (60.0)

    //! Default number of seconds to wait for system to stabilize before running cycles. Disables time feature if used.
    #define POWER_UP_STABILIZATION_TIME_DEFAULT (0.0)

    //! Set time feature to default (disabled) if not defined.
    #ifndef POWER_UP_STABILIZATION_TIME
        #define POWER_UP_STABILIZATION_TIME (POWER_UP_STABILIZATION_TIME_DEFAULT)
    #endif

    #define POWER_UP_STABILIZATION_DECISECONDS ((uint16)(POWER_UP_STABILIZATION_TIME * 10))
    #define POWER_UP_STABILIZATION_MAX_DECISECONDS ((uint16)(POWER_UP_STABILIZATION_TIME_MAX * 10))

    // Verify power up stabilization is less than or equal to the maximum allowed.
    CT_ASSERT (POWER_UP_STABILIZATION_DECISECONDS <= POWER_UP_STABILIZATION_MAX_DECISECONDS);

    //! Set default (disabled) power up stabilization guard.
    #ifndef POWER_UP_STABILIZATION_GUARD
        #define POWER_UP_STABILIZATION_GUARD (TRUE)
    #endif

    //! If macro is not defined, define as empty
    #ifndef CALL_PRE_MONITORS_STABLE
        #define CALL_PRE_MONITORS_STABLE()
    #endif

    //! If macro is not defined, define as empty
    #ifndef CALL_POST_MONITORS_STABLE
        #define CALL_POST_MONITORS_STABLE()
    #endif

    // --- LOG MODULE SETTINGS ---------------------------
    //! The name of this module for use by the Log module.
    #define MODULE_NAME MODULE_APPLIANCE_MANAGER

    //! A list of log message IDs that are generated by this module.
    typedef enum MODULE_APPLIANCE_MANAGER_LOG_MESSAGE_ID_ENUM
    {
        MAX_TIMEOUT_REACHED = 1,    //!< POWER_UP_STABILIZATION_TIME_MAX before timeout or guard reached.
    } MODULE_APPLIANCE_MANAGER_LOG_MESSAGE_ID_TYPE;

    //! Tracks whether the system is stable (TRUE) or not yet stable (FALSE)
    static BOOL_TYPE System_Stable;
#endif


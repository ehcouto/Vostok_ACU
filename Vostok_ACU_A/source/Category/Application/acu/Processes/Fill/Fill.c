/**
 * It implements the initializations required before starting Fill Water phase of Class A Fill and Tank Fill processes.
 *
 * @param  fill_data = pointer to the process volatile data
 * @param  fill_nv_data = pointer to the process non volatile data
 *
 * @return  none
 *
 */
void Fill__ClassAFillCoreInit(CLASS_A_FILL_VOLATILE_DATA_TYPE *fill_data, CLASS_A_FILL_NV_DATA_TYPE *fill_nv_data)
{
	uint16 liters_requested_by_setting;
	uint32 min;

	Liters_In_Tub_Before_Fill =	Liters_In_Tub;
	Liters_In_Tank_Before_Fill = Liters_In_Water_Tank;

	// Calculate Liters_Requested

	liters_requested_by_setting = fill_data->Class_A_Fill_Process_Parameters->Liters_Requested * (uint16)5;

	// Check if modifier is present
	if (fill_data->Class_A_Fill_Process_Parameters->Modifier_Table_Index != (uint8)UNUSED_FEATURE)
	{
		sint32 modified_value;

		modified_value = Modifier__GetValue(liters_requested_by_setting, fill_data->Class_A_Fill_Process_Parameters->Modifier_Table_Index);
		if (modified_value < 0)
		{
			modified_value = 0;
		}
		else if (modified_value > __UNSIGNED_SHORT_MAX__)
		{
			modified_value = __UNSIGNED_SHORT_MAX__;
		}
		else
		{
			// Do nothing, modified_value fits in uint16
		}

		liters_requested_by_setting = (uint16)modified_value;
	}

	if (fill_data->Class_A_Fill_Process_Parameters->Partial_Fill != (BOOL_TYPE)FALSE)
	{
		min = (uint32)liters_requested_by_setting + Liters_In_Tub_Before_Fill;
	}
	else
	{
		min = liters_requested_by_setting;
	}

	if ((fill_data->Class_A_Fill_Parameters->Liters_In_Tub_Max * (uint32)5) < min)
	{
		min = fill_data->Class_A_Fill_Parameters->Liters_In_Tub_Max * (uint32)5;
	}

	fill_nv_data->Liters_Requested = (uint16)min;


	Fill__ClearBitmapBit(DRAIN_CONFIRMED);
	fill_nv_data->Fill_Timer = 0;
	Liters_Current_Fill = 0;
	fill_data->Flow_Rate_Calibration_Enabled = TRUE;
	fill_data->No_Flow_Meter_Pulses_Timer = 0;
}


/**
 * It implements the Fill Water phase of Class A Fill and Tank Fill processes.
 *
 * @param  fill_data = pointer to the process volatile data
 * @param  fill_nv_data = pointer to the process non volatile data
 * @param  fill_option = used to distinguish between Class A Process and Tank Fill process
 *
 * @return  the action type required after this function's call
 * @retval  FILL_CORE_NO_STATE_CHANGE = continue the Fill Water phase;
			FILL_CORE_NO_WATER_DETECTED = activate H2O alarm and go to PAUSE state;
			FILL_CORE_WATER_CHECK_NEEDED = activate Fill Check Water Subcycle;
			FILL_CORE_WATER_FILL_OVER = Fill Water phase completed
 *
 *
 */
FILL_CORE_RET_TYPE Fill__ClassAFillCore(CLASS_A_FILL_VOLATILE_DATA_TYPE *fill_data, CLASS_A_FILL_NV_DATA_TYPE *fill_nv_data, FILL_OPTION_TYPE fill_option)
{
	FILL_CORE_RET_TYPE ret_value = FILL_CORE_NO_STATE_CHANGE;
	uint16 amount_of_water_to_be_filled;

	// Calculate Liters requested
	if (fill_nv_data->Liters_Requested > (fill_data->Class_A_Fill_Parameters->Liters_Offset * (uint16)2))
	{
		amount_of_water_to_be_filled = fill_nv_data->Liters_Requested - (fill_data->Class_A_Fill_Parameters->Liters_Offset * (uint16)2);
	}
	else
	{
		amount_of_water_to_be_filled = 0;
	}

	// Calculate Flow rate current in cl/min
	if (fill_nv_data->Fill_Timer > 0U)
	{
		uint32 flow_rate_current = (Liters_Current_Fill * (uint32)(FILL_CALLS_PER_SECOND  * 60)) / fill_nv_data->Fill_Timer;

		if (flow_rate_current > (uint16)__UNSIGNED_SHORT_MAX__)
		{
			flow_rate_current = __UNSIGNED_SHORT_MAX__;
		}
		fill_data->Flow_Rate_Current = (uint16)flow_rate_current;
	}
	else
	{
		fill_data->Flow_Rate_Current = __UNSIGNED_SHORT_MAX__;
	}

	// Check pulses from flow meter to disable Flow Rate Calibration
	if ((BOOL_TYPE)fill_data->Flow_Rate_Calibration_Enabled != (BOOL_TYPE)FALSE)
	{
		if ((BOOL_TYPE)DishAuxVar__AreFlowMeterPulsesPresent() != (BOOL_TYPE)FALSE)
		{
			// Flow Meter pulses detected, reset timer
			fill_data->No_Flow_Meter_Pulses_Timer = 0;
		}
		else
		{
			if (fill_data->No_Flow_Meter_Pulses_Timer >= (fill_data->Class_A_Fill_Parameters->Flow_Meter_Check_Time * (uint16)FILL_CALLS_PER_SECOND))
			{
				// No pulses for Flow_Meter_Check_Time, disable flow rate calibration
				fill_data->Flow_Rate_Calibration_Enabled = FALSE;
			}
			else
			{
				fill_data->No_Flow_Meter_Pulses_Timer ++;
			}
		}
	}
	else
	{
		// Flow Rate Calibration is already disabled, no need to check Flow Meter pulses
	}

	if (Liters_In_Tub < amount_of_water_to_be_filled)
	{
		if (fill_nv_data->Liters_Requested > Liters_In_Tub_Before_Fill)
		{
			uint16 liters_to_calculate_time = fill_nv_data->Liters_Requested - Liters_In_Tub_Before_Fill;
			uint16 flow_rate_calibrated;

			// Calculate Fill Time Max
			if (ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Fill_Flow_Rate_Min) > 0U)
			{
				// Calculate Fill Time Max in in process call rate units
				uint32 time = (liters_to_calculate_time * (uint32)(FILL_CALLS_PER_SECOND  * 60)) / ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Fill_Flow_Rate_Min);

				if (time > (uint16)__UNSIGNED_SHORT_MAX__)
				{
					time = __UNSIGNED_SHORT_MAX__;
				}
				fill_data->Fill_Time_Max = (uint16)time;
			}
			else
			{
				fill_data->Fill_Time_Max = __UNSIGNED_SHORT_MAX__;
			}

			// Check if Flow Rate calibration has ever been completed
			if (Fill_Flow_Rate_Calibrated > 0U)
			{
				flow_rate_calibrated = Fill_Flow_Rate_Calibrated;
			}
			else
			{
				// Flow rate calibration never done, use Flow_Rate_Nominal in place of Fill_Flow_Rate_Calibrated
				flow_rate_calibrated = ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Flow_Rate_Nominal);
			}

			// Calculate Fill Time Calibrated
			if (flow_rate_calibrated > 0U)
			{
				// Calculate Fill Time Calibrated in process call rate units
				uint32 time = (liters_to_calculate_time * (uint32)(FILL_CALLS_PER_SECOND  * 60)) / flow_rate_calibrated;

				// Convert Fill Time Calibrated in process call rate units
				if (time > (uint16)__UNSIGNED_SHORT_MAX__)
				{
					time = __UNSIGNED_SHORT_MAX__;
				}
				fill_data->Fill_Time_Calibrated = (uint16)time;
			}
			else
			{
				fill_data->Fill_Time_Calibrated = __UNSIGNED_SHORT_MAX__;
			}
		}
		else
		{
			fill_data->Fill_Time_Max = 0;
			fill_data->Fill_Time_Calibrated = 0;
		}

		// Check if Fill Valve has to be switched on
		if ((fill_nv_data->Fill_Timer < fill_data->Fill_Time_Max) &&
			((fill_nv_data->Fill_Timer < fill_data->Fill_Time_Calibrated) || (fill_data->Flow_Rate_Current >= ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Fill_Flow_Rate_Min))) )
		{
			// Switch on Fill Valve and increase timer for timeout

			ON_OFF_TYPE valve_state;

			fill_nv_data->Fill_Timer ++;

			valve_state = ON;
			(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_FILL_VALVE, HBL_PRIORITY_APPLICATION_HI, (uint8 *) &valve_state);	//lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]

			if (fill_option == FILL_OPTION_TANK_FILL)
			{
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_LOWER_TANK_VALVE, HBL_PRIORITY_APPLICATION_HI, (uint8 *) &valve_state);	//lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_UPPER_TANK_VALVE, HBL_PRIORITY_APPLICATION_HI, (uint8 *) &valve_state);	//lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
			}
		}
		else
		{
			if (fill_option == FILL_OPTION_TANK_FILL)
			{
				sint32 sum;

				sum = (sint32)Liters_In_Water_Tank + (sint32)Liters_Current_Fill;
				if (sum > __UNSIGNED_SHORT_MAX__)
				{
					sum = __UNSIGNED_SHORT_MAX__;
				}
				Liters_In_Water_Tank = (uint16)sum;

				sum = (sint32)Liters_Unchecked + (sint32)fill_nv_data->Liters_Requested - (sint32)Liters_In_Tub_Before_Fill;
				if (sum < 0)
				{
					sum = 0;
				}
				else if (sum > __UNSIGNED_SHORT_MAX__)
				{
					sum = __UNSIGNED_SHORT_MAX__;
				}
				else
				{
					// Do nothing, sum fits in uint16
				}
				Liters_Unchecked = (uint16)sum;
				Tank_State_Fill = TANK_STATE_UNKNOWN;

				// No Water
				ret_value = FILL_CORE_NO_WATER_DETECTED;
			}
			else
			{
				if ((BOOL_TYPE)Regenerate__GetBitmapBit(BACKRINSE_REQUIRED) != (BOOL_TYPE)FALSE)
				{
					// No Water
					ret_value = FILL_CORE_NO_WATER_DETECTED;
				}
				else
				{
					if ((fill_nv_data->Liters_Requested < (fill_data->Class_A_Fill_Parameters->Liters_For_WI_Full_Min * (uint16)5)) ||
						((fill_data->Class_A_Fill_Parameters->Test_Flow_Meter_Fault != (BOOL_TYPE)FALSE) && ((BOOL_TYPE)Fault__IsInFaultState(FAULT_ID_FLOW_METER) != (BOOL_TYPE)FALSE)) ||
						((fill_data->Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority == 0U) || (Interpreter__GetStatus().Sub_Cycle[COMPARTMENT_DEFAULT] != 0U)))
					{
						// It also cover the case in which Fill Check subcycle is not present in setting file, or Fill process
						// is being executed inside a subcycle, so it is not possible to start Fill Check Subcycle.
						if (fill_nv_data->Liters_Requested > Liters_In_Tub_Before_Fill)
						{
							Liters_Current_Fill = fill_nv_data->Liters_Requested - Liters_In_Tub_Before_Fill;
						}
						else
						{
							Liters_Current_Fill = 0;
						}

						Liters_In_Tub = fill_nv_data->Liters_Requested;

						ret_value = FILL_CORE_WATER_FILL_OVER;
					}
					else
					{
						ret_value = FILL_CORE_WATER_CHECK_NEEDED;
					}
				}
			}
		}
	}
	else
	{
		// Water filled

		// Update Flow rate calibrated
		if ((BOOL_TYPE)fill_data->Flow_Rate_Calibration_Enabled != (BOOL_TYPE)FALSE)
		{
			if (fill_nv_data->Fill_Timer >= (fill_data->Class_A_Fill_Parameters->Seconds_To_Calibrate_Min * (uint16)FILL_CALLS_PER_SECOND))
			{
				if (fill_data->Flow_Rate_Current >= ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Fill_Flow_Rate_Min))
				{
					uint8 i;

					if (fill_data->Flow_Rate_Current < ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Flow_Rate_Calib_Min))
					{
						fill_data->Flow_Rate_Current = ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Flow_Rate_Calib_Min);
					}
					else
					{
						if (fill_data->Flow_Rate_Current >= ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Flow_Rate_Max))
						{
							fill_data->Flow_Rate_Current = ENDIANU16_STON(fill_data->Class_A_Fill_Parameters->Flow_Rate_Max);
						}
					}

					// Shift FIFO array of Flow Rate historical values
					for (i = (ELEMENTS_IN_ARRAY(Flow_Rate_Buffer) - 1U); i > 0U; i --)
					{
						Flow_Rate_Buffer[i] = Flow_Rate_Buffer[i-1U];
					}
					Flow_Rate_Buffer[0] = fill_data->Flow_Rate_Current;

					if (Flow_Rate_Samples < FLOW_RATE_SAMPLES_TO_SAVE)
					{
						// New Flow Rate sample acquired
						Flow_Rate_Samples ++;
					}

					// Calculate average with rejection on Flow_Rate_Buffer elements
					if (DishAuxVar_AverageWithRejection(Flow_Rate_Buffer,
														ELEMENTS_IN_ARRAY(Flow_Rate_Buffer),
														Flow_Rate_Samples,
														(FLOW_RATE_SAMPLES_TO_SAVE - FLOW_RATE_SAMPLES_TO_REJECT),
														&Fill_Flow_Rate_Calibrated) == FAIL)
					{
						LOG_ADD_EXCEPTION(ERROR_INCONSISTENT_AVERAGE_DATA, Flow_Rate_Samples);
					}
				}
			}
		}

		// Clear Fill Valve Failures
		Fault__ConditionRemoved(FAULT_ID_NO_WATER_FILL_VALVE_PROBLEM);
		Fault__ConditionRemoved(FAULT_ID_NO_WATER);

		ret_value = FILL_CORE_WATER_FILL_OVER;
	}

	return ret_value;
}



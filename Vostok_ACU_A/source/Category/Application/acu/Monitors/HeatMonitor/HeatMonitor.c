/**
 * It returns if the heater has to be turned off due to the state of VWI
 *
 * @retval TRUE/FALSE
 */
static BOOL_TYPE IsVWIRequestingHeaterOff(void)
{
	BOOL_TYPE ret_value;
	VWI_WATER_SENSE_TYPE vwi_status;

	ret_value = FALSE;

	vwi_status = Vwi__GetWaterStatus();

	// VWI Instability detection
	if ((vwi_status == WS_NO_WATER) || (vwi_status == WS_UNSTABLE_NO_WATER) || (vwi_status == WS_NOT_VALID))
	{
		Heat_Monitor_Data.Stable_Water_Ctr = Heat_Monitor_Data.Stable_Water_Time;
		ret_value = TRUE;
	}
	else if (vwi_status == WS_UNSTABLE_WATER)
	{
		if ((uint8)Heat_Monitor_Data.VWI_Was_Forcing_Heater_Off == (uint8)TRUE)
		{
			Heat_Monitor_Data.Stable_Water_Ctr = Heat_Monitor_Data.Stable_Water_Time;
			ret_value = TRUE;
		}
	}
	else if (vwi_status == WS_WATER)
	{
		if (Heat_Monitor_Data.Stable_Water_Ctr > 0U)
		{
			Heat_Monitor_Data.Stable_Water_Ctr --;
			ret_value = TRUE;
		}
	}
	else
	{
		// Do nothing
	}

	Heat_Monitor_Data.VWI_Was_Forcing_Heater_Off = ret_value;

	return ret_value;
}


/**
 * It returns if the heater has to be turned off due to the state of wash pump
 *
 * @retval TRUE/FALSE
 */
static BOOL_TYPE IsWashPumpRequestingHeaterOff()
{
	BOOL_TYPE ret_value;
	uint8 wash_pump_index;
	uint8 *wash_pump_state_ptr;
	HBL_PILOT_ENUM_TYPE wash_pump_pilot_type;

	ret_value = FALSE;

	wash_pump_index = Hbl__GetLoadIndexByLoadID(HBL_LOAD_WASH_PUMP);
	wash_pump_pilot_type = Hbl__GetPilotIDByIndex(wash_pump_index);

	if (wash_pump_pilot_type != HBL_PILOT_INVALID)
	{
		if (wash_pump_pilot_type == HBL_PILOT_EXTERNAL_MOTOR)
		{
#if (API221_CLIENT_FEATURE == ENABLED)
			if ((McuGetDataMonitor__GetWashPumpSpeed() <= Heat_Monitor_Data.Heater_Turn_Off_Speed) ||
				((McuGetDataMonitor__GetWashPumpSpeed() < Heat_Monitor_Data.Heater_Turn_On_Speed) && ((uint8)Heat_Monitor_Data.Wash_Pump_Was_Forcing_Heater_Off == (uint8)TRUE)))
			{
				ret_value = TRUE;
			}
#endif /* (API221_CLIENT_FEATURE == ENABLED) */
		}
		else
		{
			wash_pump_state_ptr = (uint8 *)Hbl__GetProcessedLoad(wash_pump_index);
			if ((wash_pump_state_ptr != (uint8*)NULL) && (*wash_pump_state_ptr == 0U))
			{
				ret_value = TRUE;
			}
		}
	}

	Heat_Monitor_Data.Wash_Pump_Was_Forcing_Heater_Off = ret_value;

	return ret_value;
}

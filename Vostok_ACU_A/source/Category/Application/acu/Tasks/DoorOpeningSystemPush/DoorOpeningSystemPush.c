/**
 *  @file		DoorOpeningSystemPush.c
 *
 *  @brief		A task that implements the sequence to push out the DOS pin
 *
 *  @details    Door Opening System (briefly DOS) is a device used to assist with the drying by opening the door of the dishwasher
 *  			releasing the moisture out of the tub at selected moment given by the cycle design.
 *  			Door Opening System Push Task shall be activated in the point of the cycle when the DOS pin has to be extracted.
 *  			No Deactivate Task call is needed, the task automatically deactivate itself when it is completed.
 *  			The Push sequence consists of 3 sequential steps, whose duration is specified by setting file parameters:
 *  			- First On: the DOS output is driven on.
 *  			- Off: the DOS output is driven off.
 *  			- Second On: the DOS output is driven on.
 *  			This sequence is processed by the DOS device controller to push out the device pin.
 *
 */


/**
 * @brief   This method looks if a load is configured as a Cross Load and returns Cross Load index
 * @param   load_index  Load index to check
 * @return  CrossLoad index if found,  HBL_LOAD_INDEX_INVALID otherwise
 */
static uint8 GetCrossLoadIndex(uint8 load_index)
{
    uint8 cross_load_index = HBL_LOAD_INDEX_INVALID;

    for (uint8 index = 0 ; index < Crossloads_Num ; index++)
    {
        HBL_CROSS_LOAD_TYPE *cfg_ptr = &Crossload_Cfg[index];
        if(cfg_ptr->Crossload_Position == load_index)
        {
            cross_load_index = index;
            break;
        }
    }
    return(cross_load_index);
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief   This method gets current load priority
 * @param   load_index  Load index to check
 * @return  current priority value, 0 if load_index is out of range
 */
static uint8 GetLoadPriority(uint8 load_index, uint8 *hbl_prio_ptr)
{
    uint8 cur_prio = 0;

    if (load_index < Hbl__GetNumLoads())
    {
        cur_prio = hbl_prio_ptr[load_index];
    }

    return(cur_prio);
}


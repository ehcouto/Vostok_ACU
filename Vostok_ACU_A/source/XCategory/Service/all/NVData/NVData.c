/**
 * @brief This method stores the NVData variables in a blocking way without reseting.
 *
 * @return PASS_FAIL_TYPE
 *  @retval PASS(1) - Successfully able to store the data
 *  @retval FAIL(0) - Failed to store the data
 */
PASS_FAIL_TYPE NVData__ForceBlockingStore(void)
{
    PASS_FAIL_TYPE status = FAIL;
    uint32 starting;
    uint32 size;
    MICRO_DECLARE_INTERRUPT_CONTEXT_LOCAL();

    // If called when in Low Power Mode NVData could not be initialized
    if (Mode__GetMode() != MODE_NORMAL)
    {
        NVDATA__INITIALIZEDEVICE();
    }

    if ((Next_Store_Address == 0) ||
        (Next_Store_Address == NVDATA_PAGE_SIZE))
    {
        NVData_Header.Signature_Counter++;


        // Interrupts shall be DISABLED while in the process of storing the non volatile data
        // As we calculate the crc of data before storing on the data flash memory so once the crc is caculated disabling interrupt would avoid data changes caused by the interrupt
        MICRO_SAVE_INTERRUPT_CONTEXT_LOCAL();
        SRTaskMonitor__Suspend();

        // Update Crc Data;
        NVData_Header.Appliance_Crc = Crc16__RangeProcess((uint8 *)DATAFLASH_APPLIANCE_START,0xFFFF, (uint16)(DATAFLASH_APPLIANCE_LIMIT - (uint32)DATAFLASH_APPLIANCE_START)); //lint !e926 !e923 Lint: 926 cast from pointer to pointer [MISRA 2004 Rule 11.4]
        NVData_Header.Statistics_Crc = Crc16__RangeProcess((uint8 *)DATAFLASH_STATISTICS_START,0xFFFF, (uint16)(DATAFLASH_STATISTICS_LIMIT - (uint32)DATAFLASH_STATISTICS_START)); //lint !e926 !e923 Lint: 926 cast from pointer to pointer [MISRA 2004 Rule 11.4]

        // To ensure Watchdog ha time to store the data.
        Micro__ServiceWatchdog();

        // Start to store Data
        starting =  Next_Store_Address;
        if (NVDATA__WRITE(starting,(uint8 *)&NVData_Header,sizeof(NVData_Header)) == NVDATA_PROCESS_OKAY) //lint !e928 Lint: 928 cast from pointer to pointer [MISRA 2004 Rule 11.4]
        {
            starting = UpdatePosition(starting,sizeof(NVData_Header));
            size  = DATAFLASH_APPLIANCE_LIMIT - (uint32)DATAFLASH_APPLIANCE_START; //lint !e923 Lint: 923 cast from pointer to unsigned long [Encompasses MISRA 2004 Rule 11.1], [MISRA 2004 Rule 11.3]
            if (NVDATA__WRITE(starting,DATAFLASH_APPLIANCE_START,size) == NVDATA_PROCESS_OKAY)
            {
                starting = UpdatePosition(starting,size);

                size  = DATAFLASH_STATISTICS_LIMIT - (uint32)DATAFLASH_STATISTICS_START; //lint !e923 Lint: 923 cast from pointer to unsigned long [Encompasses MISRA 2004 Rule 11.1], [MISRA 2004 Rule 11.3]
                if (NVDATA__WRITE(starting,DATAFLASH_STATISTICS_START,size) == NVDATA_PROCESS_OKAY)
                {
                    status = PASS;
                }
            }
        }

        Micro__ServiceWatchdog();

        // flip the page
        if (Next_Store_Address == 0)
        {
            Next_Store_Address = NVDATA_PAGE_SIZE;
        }
        else
        {
            Next_Store_Address = 0;
        }

        if (FlushPage(Next_Store_Address) != PASS)
        {
            status = FAIL;
        }

        Micro__ServiceWatchdog();

        // Enable interrupts -------------------------------------------------------
        SRTaskMonitor__Resume();
        MICRO_RESTORE_INTERRUPT_CONTEXT_LOCAL();
    }

    return (status);
}



//! The name of this module to be used by the Log module.
#define MODULE_NAME MODULE_DOOR_OPENING_SYSTEM_MONITOR

//! A list of log message IDs that are generated by this module.
typedef enum MODULE_DOOR_OPENING_SYSTEM_MONITOR_LOG_MESSAGE_ID_ENUM
{
	ERROR_MISSING_PARAMETERS  = 1,
} MODULE_DOOR_OPENING_SYSTEM_MONITOR_LOG_MESSAGE_ID_TYPE;

//! The number of times that the DoorOpeningSystemMonitor__Execution function will be called every second while running.
#define DOS_MONITOR_CALLS_PER_SECOND 	10U

//! States of the Dos Pin State
typedef enum DOS_PIN_STATE_ENUM
{
	DOS_PIN_NOT_INIT = 0,
	DOS_PIN_FULLY_RETRACTED,
	DOS_PIN_EXTRACTING,
	DOS_PIN_RETRACTING,
	DOS_PIN_FULLY_EXTRACTED
} DOS_PIN_STATE_TYPE;

typedef struct DOOR_OPENING_SYSTEM_NV_DATA_STRUCT
{
	BOOL_TYPE Door_Opened_By_DOS; //! TRUE if, when the door is open, we can assume that it has been opened by the Door Opening System
	DOS_PUSH_STATE_TYPE Push_State; //! State of the DOS Push procedure
	DOS_PULL_STATE_TYPE Pull_State; //! State of the DOS Pull procedure
} DOOR_OPENING_SYSTEM_NV_DATA_TYPE;

typedef struct DOOR_OPENING_SYSTEM_DATA_STRUCT
{
	DOS_PIN_STATE_TYPE DOS_Pin_State; //! State of the DOS pin
	uint16 DOS_Pull_Counter; //! Timer used to manage the DOS PULL procedure
	DOOR_OPENING_SYSTEM_SF_PARAMS_TYPE *SF_DOS_Params_Ptr; //! Pointer to setting file parameters for DOS
} DOOR_OPENING_SYSTEM_DATA_TYPE;

//! Non Volatile data for Door Opening System management
#pragma location = "_appliance_segment"
static DOOR_OPENING_SYSTEM_NV_DATA_TYPE DOS_NV_Data;

//! Volatile data for Door Opening System management
static DOOR_OPENING_SYSTEM_DATA_TYPE DOS_Data;

//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------
static void UpdateDOSPinState(void);



/**
 * Request that the Fault module start observing (or continue observing) the specified fault.
 *
 * For pre-faults and faults whose debouncing is time-based, only one call to this function is
 * required to start the timers.
 *
 * For pre-faults and faults whose debouncing is event-based, this function must be called each
 * time the error condition is detected.
 *
 * @param fault_id = The ID of the fault to observe.
 */
void Fault__ConditionDetected(FAULT_ID_TYPE fault_id)
{
    // Get the instance of the existing observer for the specified fault_id (if there is one).
    uint8 observer = FindExistingObserver(fault_id);
    if (observer < MAX_PARALLEL_FAULT_OBSERVERS)
    {
        // The fault is already being observed.
        Fault.Instance = observer;
        Fault_Nv.Observers[Fault.Instance].Is_Condition_Removed = FALSE;
        ExecuteFaultDebounceMethod(FDM_CONDITION_DETECTED);
    }
    else
    {
        // The fault is not yet being observed.
        uint8 this_fault_priority;
        FAULT_CONFIG_TYPE* config = FindConfigParameters(fault_id, &this_fault_priority);
        if (config != NULL)
        {
            // Look for the first unused fault observer.
            uint8 lowest_priority_found = 0;
            for (uint8 observer_candidate = 0; observer_candidate < MAX_PARALLEL_FAULT_OBSERVERS; observer_candidate++)
            {
                if (Fault_Nv.Observers[observer_candidate].State == STATE_FAULT_OBSERVER_NONE)
                {
                    // Use the first unused fault observer.
                    observer = observer_candidate;
                    break;                          // Break out of for() loop.
                }
                else
                {
                    // Check if this lowest priority fault can be terminated and replaced with this fault (due to priorities).
                    // Remember: Lower priority values represent higher priority.
                    uint8 a_fault_priority = Fault_Nv.Observers[observer_candidate].Priority;
                    // Check if...
                    //  1) The iterated fault's priority is lower than this fault's priority, and...
                    //  2) The iterated fault's priority is lower than the lowest priority found so far.
                    if ((this_fault_priority   < a_fault_priority) &&
                        (lowest_priority_found < a_fault_priority))
                    {
                        // Update the lowest priority found so far.
                        lowest_priority_found = a_fault_priority;
                        // Remember the observer with the lowest priority fault found so far.
                        // Continue looking for an even lower priority fault.
                        observer = observer_candidate;
                    }
                }
            }

            // Check if a valid fault observer was found for the detected fault.
            if (observer < MAX_PARALLEL_FAULT_OBSERVERS)
            {
                // Setup the fault observer data for the specified fault.
                Fault.Instance = observer;
                Fault.Config[Fault.Instance] = config;
                Fault_Nv.Observers[Fault.Instance].Priority = this_fault_priority;
                Fault_Nv.Observers[Fault.Instance].Is_Condition_Removed = FALSE;

                // Initialize the associated Fault state machine.
                FaultObserverInitialize();
            }
            else
            {
                // A fault was ignored because there are not enough parallel fault observers.
                // To avoid this, increase the value of MAX_PARALLEL_FAULT_OBSERVERS.
                LOG_ADD_EXCEPTION(IGNORED_DEFINED_FAULT, fault_id);
            }
        }
        else
        {
            // A fault was ignored because it is not defined in the setting file.
            // To avoid this, add configuration data for the fault in the setting file.

            // Several platforms are detecting conditions regardless of whether the fault is
            // enabled or disbled. Therefore, we don't want to generate an exception as it will
            // flood the exception queue.
        }
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Inform the Fault module that the error condition is now absent.
 *
 * This call allows the Supervisor to exit the fault state if it wishes to in the future.
 *
 * If the fault observer is not yet in the fault state, or if the Supervisor fault macro is not
 * defined, then this call will be passed to the Debounce Method which may terminate the fault
 * observer state machine.
 *
 * @param fault_id = The ID of the fault being observed.
 */
void Fault__ConditionRemoved(FAULT_ID_TYPE fault_id)
{
    uint8 observer = FindExistingObserver(fault_id);
    if (observer < MAX_PARALLEL_FAULT_OBSERVERS)
    {
        Fault_Nv.Observers[observer].Is_Condition_Removed = TRUE;
        if ((Fault_Nv.Observers[observer].State != STATE_FAULT_OBSERVER_FAULT) ||
            (Fault.Config[observer]->Phase == SF_DISPL_INVALID_VALUE))
        {
            Fault.Instance = observer;
            if (Fault_Nv.Accelerate_Faults == TRUE)
            {
                FaultObserverTerminate();
            }
            else
            {
                ExecuteFaultDebounceMethod(FDM_CONDITION_REMOVED);
            }
        }
    }
}



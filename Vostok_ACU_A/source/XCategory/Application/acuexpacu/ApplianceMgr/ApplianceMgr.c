/**
 *  @brief      Indicates when system inputs have settled to a stable state using a configurable time and guard.
 *
 *  @details    Supervisor will start regardless of delay or guard after POWER_UP_STABILIZATION_TIME_MAX.
 *              An exception will be logged if POWER_UP_STABILIZATION_TIME_MAX reached.
 *
 *  @return     BOOL_TYPE system_stable
 *  @retval     TRUE - System inputs are stable and ready for use.
 *  @retval     FALSE - System inputs are not ready for use.
 */
static BOOL_TYPE SystemStable(void)
{
    if (Deciseconds_Since_Power_Up < POWER_UP_STABILIZATION_MAX_DECISECONDS)
    {
        if ((Millis__GetFreeRunningCounter() - Millisecond_Counter) >= 100)
        {
            Millisecond_Counter += 100;
            Deciseconds_Since_Power_Up++;
            if (Deciseconds_Since_Power_Up >= POWER_UP_STABILIZATION_MAX_DECISECONDS)
            {
                LOG_ADD_EXCEPTION(MAX_TIMEOUT_REACHED, Deciseconds_Since_Power_Up);
            }
        }

        if (POWER_UP_STABILIZATION_GUARD == TRUE)
        {
            if ((POWER_UP_STABILIZATION_DECISECONDS == 0) ||
                (Deciseconds_Since_Power_Up >= POWER_UP_STABILIZATION_DECISECONDS))
            {
                Deciseconds_Since_Power_Up = POWER_UP_STABILIZATION_MAX_DECISECONDS;
                System_Stable = TRUE;
            }
        }
    }
    else
    {
        System_Stable = TRUE;
    }

    return (System_Stable);
}
#endif // (SYSTEM_ACU_HANDLING == ENABLED)

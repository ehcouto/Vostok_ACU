/**
 * Inform the Fault module that the compartment is managing the fault.
 * This is a vague concept for this module to understand that the high-level system is taking
 * some action in response to the fault. Specifically, the Supervisor is in the FAULT state.
 * @param compartment = The compartment that is managing the fault.
 */
void Fault__CompartmentManaged(uint8 compartment)
{
    if (compartment < APPLIANCE_MAX_COMPARTMENTS)
    {
        BIT_SET(Fault.Managed_Compartments, compartment);
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Terminate fault observers for all faults that are active in the specified compartment.
 * @param compartment = The compartment in which the active faults should be reset.
 * @return Always returns TRUE.
 */
BOOL_TYPE Fault__CompartmentReset(uint8 compartment)
{
    if (compartment < APPLIANCE_MAX_COMPARTMENTS)
    {
        // Clear all fault observer state machines in the specified compartment.
        for (uint8 observer = 0; observer < MAX_PARALLEL_FAULT_OBSERVERS; observer++)
        {
            if ((Fault_Nv.Observers[observer].State == STATE_FAULT_OBSERVER_FAULT) &&
                (Fault.Config[observer]->Compartment == compartment))
            {
                Fault_Nv.Observers[observer].State = STATE_FAULT_OBSERVER_NONE;
            }
        }

        // Remove active pre-fault and fault information.
        BIT_CLR(Fault.Managed_Compartments, compartment);
        Prefault_Id[compartment] = FAULT_ID_NONE;
        Fault_Id[compartment] = FAULT_ID_NONE;
    }
    return TRUE;
}



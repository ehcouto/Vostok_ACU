#if (TIME_ESTIMATION_FEATURE == ENABLED)
//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief       Predict whether or not the JumpIf statement is expected to jump when executed.
 * @param control = The control parameters provided by the Estimator.
 * @param cps = A pointer to the current cycle, phase, and statement.
 *              It will be updated with the new cycle, phase, and statement if a jump is predicted.
 * @return TRUE if JumpIf statement is predicted to jump.
 */
BOOL_TYPE JumpIf__Estimate(uint8 sf_index, uint16 displacement, CYCLE_PHASE_STATEMENT_TYPE* cps)
{
    BOOL_TYPE predict_jump;
    SETTINGFILE_LOADER_TYPE loader;
    JUMPIF_PARAMETER_TYPE* parameter;

    predict_jump = FALSE;
    if (cps == NULL)
    {
        LOG_ADD_EXCEPTION(CPS_NULL_REFERENCE, 0);
    }
    // Get the JumpIf process parameters from the setting file.
    else if (SettingFile__MSettingBasicLoader(sf_index, SF_PTR_ACU_PROCESS_JUMP_IF, displacement, &loader) == FAIL)
    {
        LOG_ADD_EXCEPTION(INVALID_PROCESS_PARAMETERS, displacement);
    }
    else
    {
        parameter = (JUMPIF_PARAMETER_TYPE*)loader.Data;    //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
        if ((loader.Length >= sizeof(JUMPIF_PARAMETER_TYPE)) &&
            (parameter->Predict < PREDICT_CONDITIONAL_JUMP))
        {
            predict_jump = (BOOL_TYPE)(parameter->Predict);
        }
        else if (CONDITION_LEGACY_EVAL(parameter->Condition_Index) == CONDITION_RESULT_TRUE)
        {
            predict_jump = TRUE;
        }
        else
        {
            // predict_jump stays FALSE as set at the start of the function.
        }

        if (predict_jump == TRUE)
        {
            GetTarget(parameter, loader.Length, cps);
        }
    }

    return predict_jump;
}
#endif                                              // #if (TIME_ESTIMATION_FEATURE == ENABLED)


//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief       Get the target cycle, phase, and statement for the current JumpIf statement.
 *
 * If there is a problem determining the target, then the cycle, phase, and statement are left
 * unchanged. A problem can occur in any of the following cases:
 *      - There is no current JumpIf statement.
 *      - The resulting cycle is out of range.
 *      - The resulting phase is out of range.
 *      - The resulting statement is out of range.
 *
 * @param cps = A pointer to the current cycle, phase, and statement.
 *              It will be updated with the new cycle, phase, and statement.
 *
 * @retval TRUE = Valid target found.
 * @retval FALSE = Valid target not found.
 */
BOOL_TYPE JumpIf__GetTarget(CYCLE_PHASE_STATEMENT_TYPE* cps)
{
    BOOL_TYPE valid_target = FALSE;

    if (Parameter == NULL)
    {
        LOG_ADD_EXCEPTION(NO_TARGET_AVAILABLE, (cps->Phase << 8) + cps->Statement);
    }
    else
    {
        valid_target = GetTarget(Parameter, Length, cps);
    }

    return (valid_target);
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief       The JumpIf process.
 *
 * The JumpIf Process evaluates a condition and determines whether or not a jump should occur.
 *
 * @param control = The control parameters provided by the Interpreter.
 * @retval PROCESS_COMPLETE = The process is ready for the Interpreter to proceed to the next step.
 * @retval PROCESS_BUSY = The process is not ready for the Interpreter to proceed to the next step.
 */
PROCESS_RESULT_TYPE JumpIf__Process(PROCESS_CONTROL_TYPE* control)
{
    PROCESS_RESULT_TYPE result;

    result = PROCESS_BUSY;
    if ((control == NULL) ||
        (control->F1_F0_Flags >= (uint8)JUMPIF_OPTION_COUNT))
    {
        LOG_ADD_EXCEPTION(INVALID_PROCESS_CONTROL, (control != NULL));
    }
    else
    {
        // Read the JumpIf process parameters from the setting file.
        ReadProcessParameters(control);

        // Proceed only if the step was correctly initialized.
        if (Parameter != NULL)
        {
            if (CONDITION_LEGACY_EVAL(Parameter->Condition_Index) == CONDITION_RESULT_TRUE)
            {
                result = PROCESS_COMPLETE;
            }
        }
    }

    return result;
}



/**
 *  @brief calculate the zero cross point and peak point from mains high rise
 *  when L+ and mains signal is in same phase,
 *      "Zero_Cross_LH_Cnt1" means the zero cross point from N to L,
 *      "Zero_Cross_HL_Cnt2" means the zero cross point from L to N,
 *      "L_Pos_Cnt" means the peak point of L
 *      "L_Neg_Cnt" means the peak point of N
 * when L+ and mains signal is in reverse phase,
 *      "Zero_Cross_LH_Cnt1" means the zero cross point from L to N,
 *      "Zero_Cross_HL_Cnt2" means the zero cross point from N to L,
 *      "L_Pos_Cnt" means the peak point of N
 *      "L_Neg_Cnt" means the peak point of L
 */
static void MainsSamplerRecalcEvents(uint8 count)
{
    uint8 half_mains_period;
    sint8 zero_cross_slot;

    if ((count < Mains_Sampler_Data[Pilot_Index].Max_L_Pos_Count) && (count > Mains_Sampler_Data[Pilot_Index].Min_L_Pos_Count))
    {
        // recalc zero cross
        half_mains_period = Mains_Sampler_Data[Pilot_Index].Mains_Period / 2;

        zero_cross_slot = ((count - half_mains_period)/2);
        Mains_Sampler_Data[Pilot_Index].Zero_Cross_HL_Cnt2 = (half_mains_period + zero_cross_slot);

        if (count > half_mains_period) //mains high width > AC high width
        {
            Mains_Sampler_Data[Pilot_Index].Zero_Cross_LH_Cnt1 = zero_cross_slot;
        }
        else
        {
            Mains_Sampler_Data[Pilot_Index].Zero_Cross_LH_Cnt1 = (Mains_Sampler_Data[Pilot_Index].Zero_Cross_HL_Cnt2 + half_mains_period);
        }

        //recalc positive peak and negative peak
        Mains_Sampler_Data[Pilot_Index].L_Pos_Cnt = (count/2);
        Mains_Sampler_Data[Pilot_Index].L_Neg_Cnt = (Mains_Sampler_Data[Pilot_Index].L_Pos_Cnt + (Mains_Sampler_Data[Pilot_Index].Mains_Period / 2));
    }
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      It detect the mains lost state
 *
 */
static void MainsSamplerMainsLostDetection(void)
{
    Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter++;
    if (Mains_Sampler_Data[Pilot_Index].Mains_Detect_State != MAINS_DETECT_STATE_LOST)
    {
        if (Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter >= MAINS_LOST_COUNT)
        {
            Mains_Sampler_Data[Pilot_Index].Mains_Detect_State = MAINS_DETECT_STATE_LOST;
            Mains_Sampler_Data[Pilot_Index].Mains_Detect_State_Changed = TRUE;
            Mains_Sampler_Data[Pilot_Index].Mains_Period_Resume_Counter = 0;
        }

        if (Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter == MAINS_EARLY_WARNING_COUNT)
        {
            Mains_Sampler_Data[Pilot_Index].Mains_Detect_Early_Warning = TRUE;
        }
    }
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief      It detect the mains found state
 *
 */
static void MainsSamplerMainsFoundDetection(void)
{
    if(Mains_Sampler_Data[Pilot_Index].Mains_Detect_State == MAINS_DETECT_STATE_FOUND)
    {
        Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter = 0;
    }
    else
    {
        if(Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter <= MAINS_RESUME_COUNT)
        {
            Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter = 0;
            Mains_Sampler_Data[Pilot_Index].Mains_Period_Resume_Counter++;
            if(Mains_Sampler_Data[Pilot_Index].Mains_Period_Resume_Counter > 5) //!< 6 zero crosses needed to approve mains
            {
                Mains_Sampler_Data[Pilot_Index].Mains_Detect_State = MAINS_DETECT_STATE_FOUND;
                Mains_Sampler_Data[Pilot_Index].Mains_Detect_State_Changed = TRUE;
            }
        }
        else
        {
            Mains_Sampler_Data[Pilot_Index].Mains_Period_Wait_Counter = 0;
            Mains_Sampler_Data[Pilot_Index].Mains_Period_Resume_Counter = 0;
        }
    }
}


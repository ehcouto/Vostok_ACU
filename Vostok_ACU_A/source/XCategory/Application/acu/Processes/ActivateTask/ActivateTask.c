//=====================================================================================================================
//  --- Private Methods -----------------------------------------------------------------------------------------------
//=====================================================================================================================

static void ExecuteTaskManipulation(uint8 compartment, uint8 task, BOOL_TYPE resume, TASK_EVENT_ACTION_TYPE action)
{
    uint8 task_bit;
    TASK_STOP_FUNCTION_TYPE stop;
    uint8 first_task_bit;

    first_task_bit = compartment * TASK_COUNT;
    task_bit = first_task_bit + task;

    switch (action)
    {
        case TASK_OPTION_PAUSE_RESUME_BG_FUN:
        {
            if (resume == TRUE)
            {
                Utilities__SetBitInArray(Task_Paused, task_bit, FALSE);
            }
            else
            {
                Utilities__SetBitInArray(Task_Paused, task_bit, TRUE);
            }
            break;
        }
        case TASK_OPTION_TERMINATE:
        {
            if (resume == TRUE)
            {
                // Do nothing
            }
            else
            {
                Utilities__SetBitInArray(Task_Active, task_bit, INACTIVE);
                stop = TASK_LIST[task].Stop;
                if (stop != TASK_NULL_STOP)
                {
                    stop(compartment);
                }
            }
            break;
        }

        case TASK_OPTION_NO_CHANGE:
            // Do nothing
            break;

        default:
        {
            LOG_ADD_EXCEPTION(INVALID_TASK_MANIPULATION_PARAMETER, action);
            break;
        }
    }
}


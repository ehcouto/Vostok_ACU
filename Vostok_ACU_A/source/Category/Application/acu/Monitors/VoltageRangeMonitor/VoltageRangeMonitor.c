/**
 *  @brief      It Initializes the module VoltageRange and its variables
 *
 */
void VoltageRangeMonitor__Initialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		SETTINGFILE_LOADER_TYPE ptr;
		if (SettingFile__BasicLoader(SF_PTR_ACU_PRODUCT_CONFIGURATION_DATA, DSPL_VOLTAGE_RANGE_PARAMS, &ptr) == PASS)
		{
			VRM_Data = (VOLTAGERANGE_PARAM_TYPE *)(void *) ptr.Data;
		}
		else
		{
			VRM_Data = (VOLTAGERANGE_PARAM_TYPE *) VOLTAGERANGE_DEFAULT_PARAM_PTR;
		}

		VRM_Good_Range_Counter = 0;
		VRM_Low_Range_Counter = 0;
		VRM_High_Range_Counter = 0;
		Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
		VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
	}
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief		Monitor to check voltage range against thresholds and timeout set
 *
 */
void VoltageRangeMonitor__Execution(void)
{
	BOOL_TYPE update_status;
	uint32 voltage_ref;
	uint16 instability_ref;

	if (VRM_Data != NULL)
	{
		/* in subcycle VOLTAGE RANGE RECOVERY we turn on the PILOT RELAY to keep the monitor enabled */
		if (Interpreter__IsSubCycleRunning(COMPARTMENT_DEFAULT))
		{
			CYCLE_NAME_TYPE subcycle_name;
			subcycle_name = Selector__GetCycleNameId(Interpreter__GetCurrentSFIndex(COMPARTMENT_DEFAULT),
														COMPARTMENT_DEFAULT,
														Interpreter__GetStatus().Sub_Cycle[COMPARTMENT_DEFAULT]);

			if (subcycle_name == CYCLE_NAME_VOLTAGE_RANGE_RECOVERY)
			{
				uint8 pilot_relay_index;
				uint8 relay_enable_index;
				ON_OFF_TYPE value_state = ON;

				pilot_relay_index = Hbl__GetLoadIndexByLoadID(HBL_LOAD_PILOT_RELAY);
				relay_enable_index = Hbl__GetLoadIndexByLoadID(HBL_LOAD_RELAY_ENABLE);

				if (pilot_relay_index != (uint8)HBL_INDEX_INVALID)
				{
					(void)Hbl__SetLoadRequestByIndex(pilot_relay_index, HBL_PRIORITY_SUPERVISOR_EXT_MONITOR, (uint8 *) &value_state); //lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
				}
				if (relay_enable_index != (uint8)HBL_INDEX_INVALID)
				{
					(void)Hbl__SetLoadRequestByIndex(relay_enable_index, HBL_PRIORITY_SUPERVISOR_EXT_MONITOR, (uint8 *) &value_state); //lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
				}
			}
		}

	    if (IsMonitorEnabled())  /* this check point is to verify if ACLINE is present, PILOT RELAY is closed, RELAY ENABLE is closed, DOOR is closed and ZeroCross is present */
		{
			update_status = FALSE;
			voltage_ref = VOLTAGERANGE_GETREFERENCE();

			if (voltage_ref < ENDIANU16_STON(VRM_Data->Low_Voltage_Low_Thres))
			{
				/*Low Voltage condition occurred*/
				VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_LOWVOLTAGE;
				if ((Voltage_Range_Status != VRM_Instant_Voltage_Range_Status) ||
					((VRM_High_Range_Counter + VRM_Good_Range_Counter) > (uint16)0))
				{
					VRM_Low_Range_Counter++;
				}

				if (VRM_Low_Range_Counter > VOLTAGERANGE_PARAM_TIME_CONVERT(VRM_Data->Out_Range_Time))
				{
					update_status = TRUE;
					StatisticsDish__IncreaseLowVoltageEventCounter();
				}
			}
			else if (voltage_ref > ENDIANU16_STON(VRM_Data->High_Voltage_High_Thres))
			{
				/*High Voltage condition occurred*/
				VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_HIGHVOLTAGE;
				if ((Voltage_Range_Status != VRM_Instant_Voltage_Range_Status) ||
					((VRM_Low_Range_Counter + VRM_Good_Range_Counter) > (uint16)0))
				{
					VRM_High_Range_Counter++;
				}

				if (VRM_High_Range_Counter > VOLTAGERANGE_PARAM_TIME_CONVERT(VRM_Data->Out_Range_Time))
				{
					update_status = TRUE;
					StatisticsDish__IncreaseHighVoltageEventCounter();
				}
			}
			else if ((Voltage_Range_Status == VOLTAGERANGE_STATE_UNDEFINED) ||
					((voltage_ref >= ENDIANU16_STON(VRM_Data->Low_Voltage_High_Thres))
					&& (voltage_ref <= ENDIANU16_STON(VRM_Data->High_Voltage_Low_Thres))) )
			{
				/* Good Voltage condition occurred
				   When state is undefined, skip hysteresis thresholds */
				VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_GOODRANGE;
				if ((Voltage_Range_Status != VRM_Instant_Voltage_Range_Status) ||
					((VRM_High_Range_Counter + VRM_Low_Range_Counter) > (uint16)0))
				{
					VRM_Good_Range_Counter++;
				}

				if (VRM_Good_Range_Counter > VOLTAGERANGE_PARAM_TIME_CONVERT(VRM_Data->In_Range_Time))
				{
					update_status = TRUE;
				}
			}
			else
			{
				/* No other actions required */
			}

			if ((uint8)update_status == (uint8)TRUE)
			{
				if (Voltage_Range_Status != VRM_Instant_Voltage_Range_Status)
				{
					LOG_ADD_EVENT(VOLTAGERANGE_STATUS_CHANGED, Voltage_Range_Status);
				}

				if (VRM_Instant_Voltage_Range_Status == VOLTAGERANGE_STATE_GOODRANGE)
				{
					instability_ref = VOLTAGERANGE_PARAM_TIME_CONVERT(VRM_Data->In_Range_Time);
				}
				else
				{
					instability_ref = VOLTAGERANGE_PARAM_TIME_CONVERT(VRM_Data->Out_Range_Time);
					VoltageOutRangeManageSubCycle(VRM_Data->Voltage_Out_Range_Cycle_Priority); /* RUN sub cycle for this particular situation */
				}

				VRM_Instability_Counter = VRM_Good_Range_Counter + VRM_High_Range_Counter + VRM_Low_Range_Counter;

				if (VRM_Instability_Counter > instability_ref)
				{
					LOG_ADD_EVENT(VOLTAGERANGE_INSTABILITY_COUNTER, VRM_Instability_Counter);
				}

				Voltage_Range_Status = VRM_Instant_Voltage_Range_Status;
				StatisticsDish__UpdateVoltageRangeStatus(Voltage_Range_Status);
				VRM_Good_Range_Counter = 0;
				VRM_Low_Range_Counter = 0;
				VRM_High_Range_Counter = 0;
			}
		}
	    else
		{
			Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
			VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
			StatisticsDish__UpdateVoltageRangeStatus(Voltage_Range_Status);
			VRM_Good_Range_Counter = 0;
			VRM_Low_Range_Counter = 0;
			VRM_High_Range_Counter = 0;
		}
	}
	else
	{
		Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
		VRM_Instant_Voltage_Range_Status = VOLTAGERANGE_STATE_UNDEFINED;
		StatisticsDish__UpdateVoltageRangeStatus(Voltage_Range_Status);
	}
}




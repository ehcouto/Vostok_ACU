/**
 *  @brief      Set cycle_heading to location of cycle heading data in setting_file.
 *
 *  @param      uint8 setting_file - index of setting file to get heading data pointer from
 *  @param      uint8 cycle - displacement into cycle heading data
 *  @param      uint8 const **cycle_heading - pointer to pointer to constant uint8 cycle heading data
 *
 *  @return    PASS_FAIL_TYPE load_success: TRUE -  cycle_heading set to desired heading data location
 *                                          FALSE - cycle_heading set to NULL
*/
static PASS_FAIL_TYPE GetProgramHeadingPointer(uint8 setting_file, uint8 compartment, uint8 cycle,  uint8 const **cycle_heading)
{
    SETTINGFILE_LOADER_TYPE sf_loader_data;
    PASS_FAIL_TYPE load_success;

    *cycle_heading = NULL;
    load_success = SettingFile__MSettingBasicLoader(setting_file,
                                                    Program_Heading_Pointers[compartment],
                                                    cycle,
                                                    &sf_loader_data);
    if (load_success == PASS)
    {
        //! Minimum size for program heading data.
        #define PROGRAM_HEADING_ITEM_SIZE   ((uint16)6)

        if (sf_loader_data.Length >= PROGRAM_HEADING_ITEM_SIZE)
        {
            *cycle_heading = sf_loader_data.Data;
            return PASS;
        }
        else
        {
            LOG_ADD_EXCEPTION(SF_PROGRAM_HEADING_ITEM_SIZE, setting_file);
        }
    }
    else
    {
        LOG_ADD_EXCEPTION(SF_PROGRAM_HEADING_TARGET_FAIL, setting_file);
    }

    return (FAIL);
}


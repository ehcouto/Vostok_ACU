/**
 * @brief - This function manages the Crossloads of type Switch Load
 *
 * @param CROSSLOAD_DATA_TYPE * , CROSSLOAD_PARAMETERS_TYPE *
 */
static void SwitchLoadHandler(HBL_CROSS_LOAD_TYPE *cfg_ptr, CROSSLOAD_DATA_TYPE * data_ptr, uint8 *hbl_prio_ptr)
{
    CROSSLOAD_PARAMETERS_TYPE *param_ptr = cfg_ptr->Parameter_Ptr;
    uint8 my_prio = GetLoadPriority(cfg_ptr->Crossload_Position, hbl_prio_ptr);
    uint8 position_index;
    uint8 *load_request_ptr;
    uint8 *load_process_ptr;
    uint8 load_request_len;
    BOOL_TYPE do_switch = FALSE;
    BOOL_TYPE keep_status = FALSE;

    switch (data_ptr->Generic_State)
    {
        case CROSSLOAD_STEADY_STATE:
        case CROSSLOAD_FORCED_STATE:
        {
            uint8 load_check_index; // Load index to check if switch has to be executed
            uint8 load_keep_index;  // Load index to check if switch position should be kept
            uint8 load_check_prio;

            for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
            {
                // Place check and keep positions according to Switch status
                if (data_ptr->Processed == OFF)
                {
                    load_check_index = param_ptr->Load_Position[position_index].On;
                    load_keep_index = param_ptr->Load_Position[position_index].Off;
                }
                else
                {
                    load_check_index = param_ptr->Load_Position[position_index].Off;
                    load_keep_index = param_ptr->Load_Position[position_index].On;
                }


                // If keep position is ON: keep actual status
                load_check_prio = GetLoadPriority(load_check_index, hbl_prio_ptr);
                load_request_ptr = Hbl__GetRequestedLoad(load_keep_index);
                if (load_request_ptr != NULL)
                {
                    load_request_len = Hbl__GetLoadDataSize(load_keep_index);

                    // Perform clear only if Switch load priority is lower than load to keep
                    // otherwise load status is kept as is
                    if ((memcmp(load_request_ptr, Hbl__GetFlushBuffer(), load_request_len) != 0) ||
                        (keep_status != FALSE))
                    {
                        keep_status = TRUE;
                        if (PASS == Hbl__SetLoadRequestByIndex(load_check_index, (HBL_PRIORITY_TYPE)load_check_prio,
                        (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                        {
                            ResetCrossLoadTransition(load_check_index);
                        }
                    }
                }

                if ((keep_status == FALSE) &&
                    (load_check_prio >= my_prio))
                {
                    // If check position is ON: keep actual status
                    load_request_ptr = Hbl__GetRequestedLoad(load_check_index);
                    load_request_len = Hbl__GetLoadDataSize(load_check_index);
                    // Perform Switch load transition only if priorty is higher than maximum of other loads *
                    if ((load_request_ptr != NULL) &&
                        (memcmp(load_request_ptr, Hbl__GetFlushBuffer(), load_request_len) != 0))
                    {
                        load_process_ptr = Hbl__GetProcessedLoad(load_check_index);
                        if ((!IsLoadStatusOnTransition(load_check_index)) &&
                            (!IsCrossLoadTransition(load_check_index, TRUE)) &&
                            (memcmp(load_request_ptr, load_process_ptr, load_request_len) != 0))
                        {
                            do_switch = TRUE;
                        }
                        else
                        {
                            data_ptr->Processed = (data_ptr->Processed == OFF) ? ON : OFF;
                            keep_status = TRUE;
                        }
                    }
                }
            }

            if (keep_status == FALSE)
            {
                // No keep status required: check if Switch Load request changed by
                // direct request or via other load
                if (do_switch == FALSE)
                {
                    if (data_ptr->Processed != OFF)
                    {
                        if ((data_ptr->Requested != OFF) && (my_prio > SRHBL_PRIORITY_MINIMUM))
                        {
                            data_ptr->Generic_State = CROSSLOAD_FORCED_STATE;
                        }
                        else if (data_ptr->Generic_State == CROSSLOAD_FORCED_STATE)
                        {
                            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                            data_ptr->Processed = OFF;
                        }
                        else
                        {
                            do_switch = TRUE;
                        }
                    }
                    else if (data_ptr->Requested != OFF)
                    {
                        data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                        data_ptr->Processed = ON;
                    }
                }

                if (do_switch != FALSE)
                {
                    if (data_ptr->Processed == OFF)
                    {
                        data_ptr->Generic_State = CROSSLOAD_ON_TRANSITION;
                        data_ptr->Processed = ON;
                        data_ptr->Timer = param_ptr->On_Delay;
                    }
                    else
                    {
                        data_ptr->Generic_State = CROSSLOAD_OFF_TRANSITION;
                        if(data_ptr->Timer == 0)
                        {
                            data_ptr->Timer = param_ptr->On_Delay + param_ptr->Off_Delay;
                        }
                    }

                    if(data_ptr->Timer > 0)
                    {
                        for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                        {
                            uint8 load_off_pos = param_ptr->Load_Position[position_index].Off;
                            uint8 load_on_pos = param_ptr->Load_Position[position_index].On;
                            uint8 load_off_prio = GetLoadPriority(load_off_pos, hbl_prio_ptr);
                            uint8 load_on_prio = GetLoadPriority(load_on_pos, hbl_prio_ptr);

                            if (PASS == Hbl__SetLoadRequestByIndex(load_off_pos, (HBL_PRIORITY_TYPE)load_off_prio,
                                    (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                            {
                                ResetCrossLoadTransition(load_off_pos);
                            }
                            if (PASS == Hbl__SetLoadRequestByIndex(load_on_pos, (HBL_PRIORITY_TYPE)load_on_prio,
                                    (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                            {
                                ResetCrossLoadTransition(load_on_pos);
                            }
                        }
                    }
                    else
                    {
                        data_ptr->Processed = (data_ptr->Generic_State == CROSSLOAD_OFF_TRANSITION) ? OFF : ON;
                        data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                    }
                }
            }
            else
            {
                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            }
            break;
        }

        case CROSSLOAD_OFF_TRANSITION:
        case CROSSLOAD_ON_TRANSITION:
        {
            uint8 load_off_pos;
            uint8 load_on_pos;
            uint8 load_off_prio;
            uint8 load_on_prio;
            uint8 new_processed;
            BOOL_TYPE timer_wait = IsLoadStatusOnTransition(cfg_ptr->Crossload_Position);

            //------------------------------------------------------------
            new_processed = (data_ptr->Generic_State == CROSSLOAD_OFF_TRANSITION) ? OFF : ON;
            if (data_ptr->Timer > param_ptr->On_Delay)
            {
                new_processed = (new_processed == OFF) ? ON : OFF;
            }

            if (new_processed != data_ptr->Processed)
            {
                if(data_ptr->Timer <= param_ptr->On_Delay)
                {
                    timer_wait = TRUE;
                }
                else
                {
                    data_ptr->Timer = 0;
                }
            }

            if (data_ptr->Timer > 0)
            {
                //----------- Load-Off & Load-On Keep Off Process ------------
                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    load_off_pos = param_ptr->Load_Position[position_index].Off;
                    load_on_pos = param_ptr->Load_Position[position_index].On;
                    if(new_processed == OFF)
                    {
                        timer_wait |= IsLoadStatusOnTransition(load_off_pos);
                        timer_wait |= IsCrossLoadTransition(load_off_pos, TRUE);
                    }
                    else
                    {
                        timer_wait |= IsLoadStatusOnTransition(load_on_pos);
                        timer_wait |= IsCrossLoadTransition(load_on_pos, TRUE);
                    }

                    if (data_ptr->Timer > param_ptr->On_Delay)
                    {
                        uint8 load_keep_index = (data_ptr->Generic_State == CROSSLOAD_OFF_TRANSITION) ? load_on_pos : load_off_pos;

                        load_process_ptr = Hbl__GetRequestedLoad(load_keep_index);
                        load_request_len = Hbl__GetLoadDataSize(load_keep_index);
                        if (load_process_ptr != NULL)
                        {
                            if(memcmp(load_process_ptr, Hbl__GetFlushBuffer(), load_request_len) != 0)
                            {
                                timer_wait = TRUE;
                                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                                data_ptr->Timer = 0;
                            }
                        }
                    }
                }

                if (timer_wait == FALSE)
                {
                    data_ptr->Timer--;
                    if(data_ptr->Timer == param_ptr->On_Delay)
                    {
                        new_processed =  (new_processed == OFF) ? ON : OFF;
                    }
                }
            }

            if(data_ptr->Timer > 0)
            {
                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    load_off_pos = param_ptr->Load_Position[position_index].Off;
                    load_on_pos = param_ptr->Load_Position[position_index].On;
                    load_off_prio = GetLoadPriority(load_off_pos, hbl_prio_ptr);
                    load_on_prio = GetLoadPriority(load_on_pos, hbl_prio_ptr);

                    if (PASS == Hbl__SetLoadRequestByIndex(load_off_pos, (HBL_PRIORITY_TYPE)load_off_prio,
                            (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                    {
                        ResetCrossLoadTransition(load_off_pos);
                    }
                    if (PASS == Hbl__SetLoadRequestByIndex(load_on_pos, (HBL_PRIORITY_TYPE)load_on_prio,
                            (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                    {
                        ResetCrossLoadTransition(load_on_pos);
                    }
                }
                data_ptr->Processed = new_processed;
            }
            else
            {
                data_ptr->Processed = new_processed;
                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            }
            break;
        }

        default:
            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            break;
    }
}

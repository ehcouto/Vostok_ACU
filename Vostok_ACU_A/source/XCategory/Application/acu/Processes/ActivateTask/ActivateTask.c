//---------------------------------------------------------------------------------------------------------------------
/**
 * Initialize the ActivateTask module.
 * If non-volatile data was corrupted, then deactivate all tasks in all compartments.
 */
void ActivateTask__Initialize(void)
{
    uint8 compartment;
    uint8 task;
    uint8 task_bit;
    TASK_RECOVER_FUNCTION_TYPE recover;

    if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
    {
        if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != NVDATA_RESTORE_OKAY)
        {
            // Corrupt non-volatile memory -- deactivate all tasks without terminating them.
            memset(Task_Active, 0, sizeof(Task_Active));
            memset(Task_Paused, 0, sizeof(Task_Paused));
        }
        else
        {
            // Inform all tasks running in any compartment that a reset has occurred.
            for (compartment = 0; compartment < APPLIANCE_MAX_COMPARTMENTS; compartment++)
            {
                for (task = 0; task < TASK_COUNT; task++)
                {
                    recover = TASK_LIST[task].Recover;
                    if (recover != TASK_NULL_RECOVER)
                    {
                        // Call the task's recover function if it was running before the reset.
                        task_bit = compartment * TASK_COUNT + task;
                        if (Utilities__GetBitInArray(Task_Active, task_bit) == ACTIVE)
                        {
                            recover(compartment);
                        }
                    }
                }
            }
        }
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief       The ActivateTask process.
 *
 * The ActivateTask Process enables and disables tasks in the appliance.
 *
 * @param control = The control parameters provided by the Interpreter.
 * @retval PROCESS_COMPLETE = The process is ready for the Interpreter to proceed to the next step.
 * @retval PROCESS_BUSY = The process is not ready for the Interpreter to proceed to the next step.
 */
PROCESS_RESULT_TYPE ActivateTask__Process(PROCESS_CONTROL_TYPE* control)
{
    PROCESS_RESULT_TYPE result;                     // Return value for the Interpreter.
    SETTINGFILE_LOADER_TYPE loader;                 // The structure filled by SettingFile module.
    ACTIVATE_TASK_PARAMETER_TYPE* parameter;        // Pointer to the setting file data structure.
    uint8 task_bit;                                 // Bit position for the task in the compartment.
    TASK_INIT_FUNCTION_TYPE init;                   // Pointer to the task initialize function.
    ACTIVE_TYPE active;                             // Task response to remain active.
    TASK_STOP_FUNCTION_TYPE stop;                   // Pointer to the task terminate function.

    result = PROCESS_COMPLETE;
    if ((control == NULL) ||
        (control->F1_F0_Flags >= (uint8)TASK_OPTION_COUNT) ||
        (control->Compartment >= APPLIANCE_MAX_COMPARTMENTS))
    {
        LOG_ADD_EXCEPTION(INVALID_PROCESS_CONTROL, (control != NULL));
    }
    else if ((SettingFile__MSettingBasicLoader(control->Setting_File_Index,
                                               SF_PTR_ACU_PROCESS_ACTIVATE_TASK,
                                               control->Data_Field_Index,
                                               &loader) == FAIL) ||
             (loader.Length < sizeof(ACTIVATE_TASK_PARAMETER_TYPE)))
    {
        LOG_ADD_EXCEPTION(INVALID_PROCESS_PARAMETERS, control->Data_Field_Index);
    }
    else
    {
        parameter = (void*)loader.Data;
        if ((parameter->Task_Code >= TASK_COUNT) ||
            (memcmp(&TASK_LIST[parameter->Task_Code], DISABLED_TASK, sizeof(DISABLED_TASK)) == 0))
        {
            LOG_ADD_EXCEPTION(NON_EXISTENT_TASK, COMBINE_BYTES(parameter->Task_Code, control->Data_Field_Index));
        }
        else
        {
            task_bit = control->Compartment * TASK_COUNT + parameter->Task_Code;
            if (control->Step_Initializing == TRUE)
            {
                switch ((ACTIVATE_TASK_OPTION_TYPE)control->F1_F0_Flags)
                {
                    case TASK_OPTION_ACTIVATE:
                        active = ACTIVE;
                        init = TASK_LIST[parameter->Task_Code].Init;
                        if (init != TASK_NULL_INIT)
                        {
                            active = init(control->Compartment, control->Setting_File_Index, parameter->Task_Displacement);
                        }
                        Utilities__SetBitInArray(Task_Active, task_bit, active);
                        Utilities__SetBitInArray(Task_Paused, task_bit, FALSE);
                        break;

                    case TASK_OPTION_DEACTIVATE:
                        stop = TASK_LIST[parameter->Task_Code].Stop;
                        if (Utilities__GetBitInArray(Task_Active, task_bit) == ACTIVE)
                        {
                            if (stop != TASK_NULL_STOP)
                            {
                                stop(control->Compartment);
                            }
                            Utilities__SetBitInArray(Task_Active, task_bit, INACTIVE);
                        }
                        break;

                    default:
                        // F1/F0 flag combination is not supported.
                        break;
                }
            }

            if (Utilities__GetBitInArray(Task_Active, task_bit) == ACTIVE)
            {
                result = PROCESS_BUSY;
            }
        }
    }

    return result;
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Stop all the tasks in a single compartment.
 *
 * Task control states are set to inactive.
 * Task stop functions are not called for tasks that are running.
 *
 * @param compartment = The compartment in which all the tasks should be stopped.
 */
void ActivateTask__ResetCompartment(uint8 compartment)
{
    uint8 first_task_bit;
    uint8 task;
    uint8 task_bit;
    TASK_STOP_FUNCTION_TYPE stop;

    first_task_bit = compartment * TASK_COUNT;
    for (task = 0; task < TASK_COUNT; task++)
    {
        task_bit = first_task_bit + task;
        if (Utilities__GetBitInArray(Task_Active, task_bit) == ACTIVE)
        {
            stop = TASK_LIST[task].Stop;
            if (stop != TASK_NULL_STOP)
            {
                stop(compartment);
            }
            Utilities__SetBitInArray(Task_Active, task_bit, INACTIVE);
        }
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Run all the tasks in a single compartment.
 *
 * Task functions are only called if they are initializing, running, or terminating.
 *
 * @param compartment = The compartment in which all the tasks should be run.
 */
void ActivateTask__RunCompartment(uint8 compartment)
{
    uint8 task;
    TASK_RUN_FUNCTION_TYPE run;
    uint8 task_bit;

    // Call each of the tasks that are active or just changed states.
    for (task = 0; task < TASK_COUNT; task++)
    {
        run = TASK_LIST[task].Run;
        if (run != TASK_NULL_RUN)
        {
            // Call the task function if it needs to be called.
            task_bit = compartment * TASK_COUNT + task;
            if ((Utilities__GetBitInArray(Task_Active, task_bit) == ACTIVE) &&
                (Utilities__GetBitInArray(Task_Paused, task_bit) == FALSE))
            {
                if (run(compartment) == INACTIVE)
                {
                    Utilities__SetBitInArray(Task_Active, task_bit, INACTIVE);
                }
            }
        }
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Pause all the tasks in a single compartment.
 *
 * Task control states are set in accordance with TASK_LIST configuration table .
 * Task stop functions are not called for tasks that are not running. It's stopped if the task is paused.
 *
 * @param compartment = The compartment in which all the tasks should be stopped.
 */
void ActivateTask__PauseCompartment(uint8 compartment)
{
    uint8 task;

    for (task = 0; task < TASK_COUNT; task++)
    {
        ExecuteTaskManipulation(compartment, task, FALSE, TASK_LIST[task].OnPause);
    }
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Resume all the tasks in a single compartment.
 *
 * Task control states are set in accordance with TASK_LIST configuration table .
 * Task stop functions are not called for tasks that are not running. It's stopped if the task is paused.
 *
 * @param compartment = The compartment in which all the tasks should be stopped.
 */
void ActivateTask__ResumeCompartment(uint8 compartment)
{
    uint8 task;

    for (task = 0; task < TASK_COUNT; task++)
    {
        ExecuteTaskManipulation(compartment, task, TRUE, TASK_LIST[task].OnPause);
    }
}




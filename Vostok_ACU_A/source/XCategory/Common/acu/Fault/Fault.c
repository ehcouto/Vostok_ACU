/**
 * Trigger fault event handlers to manage the reaction to a fault detection.
 */
static void TriggerFaultReaction(void)
{
    // Save the fault data in Statistics.
    FAULT_CONFIG_TYPE* config = Fault.Config[Fault.Instance];
    int compartment = config->Compartment;
    Statistics__SaveFaultData(compartment, config->Id, config->Code, config->Sub_Code, config->Engineering_Code);

    // Only trigger the fault if this is the highest priority fault in the current compartment.
    if (GetActiveFaultObserver(compartment) == Fault.Instance)
    {
        // Report the fault to the HMI in either of the following cases:
        //      1. The Supervisor is not in the FAULT state.
        //      2. The new Fault will cause the Supervisor to re-enter the FAULT state.
        if ((BIT_TEST(Fault.Managed_Compartments, compartment) == 0) ||
            (config->Phase != UCHAR_MAX))
        {
            // Inform the HMI of the new fault.
            Fault_Id[compartment] = config->Id;
            #if (API018_SERVER_FEATURE == ENABLED)
                API018Diagnostic__PublishFaultBasic(UCHAR_MAX);
            #endif
        }

        Callback__Notify(&Callback_Control_Fault, Fault.Instance);
    }
}



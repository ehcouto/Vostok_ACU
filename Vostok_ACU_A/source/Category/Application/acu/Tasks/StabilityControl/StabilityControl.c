/**
 * The function
 * @param
 *
 * @return :
 */
static void ResetInstabilityEvent(void)
{
	Instability_Event = SC_NO_INSTABILITY;
	Instability_Counter = 0;
	No_Water_Counter = 0;
	Very_Low_Water_Counter = 0;
	Stable_Water_Counter = 0;
}

/**
 * The function
 * @param
 *
 * @return :
 */
static void UpdateInstabilityEvent(void)
{
	VWI_WATER_SENSE_TYPE vwi_status;

	vwi_status = Vwi__GetWaterStatus();

	// VWI Instability detection
	if ((vwi_status == WS_UNSTABLE_NO_WATER) || (vwi_status == WS_UNSTABLE_WATER))
	{
		if (Instability_Counter < INSTABILITY_DETECTION_TIME_100MS)
		{
			Instability_Counter ++;
		}
		else
		{
			Instability_Event = SC_INSTABILITY;
		}
	}
	else
	{
		if (Instability_Event == SC_INSTABILITY)
		{
			Instability_Event = SC_NO_INSTABILITY;
		}

		Instability_Counter = 0;
	}

	// VWI No Water detection
	if ((vwi_status == WS_UNSTABLE_NO_WATER) || (vwi_status == WS_NO_WATER))
	{
		if (No_Water_Counter < NO_WATER_DETECTION_TIME_100MS)
		{
			No_Water_Counter ++;
		}
		else
		{
			Instability_Event = SC_NO_WATER;
		}
	}
	else
	{
		if (Instability_Event == SC_NO_WATER)
		{
			Instability_Event = SC_NO_INSTABILITY;
		}

		No_Water_Counter = 0;
	}

	// VWI Very Low Water detection
	if ((vwi_status == WS_UNSTABLE_NO_WATER) || (vwi_status == WS_NO_WATER))
	{
		if (Very_Low_Water_Counter < VERY_LOW_WATER_DETECTION_TIME_100MS)
		{
			Very_Low_Water_Counter ++;
		}
		else
		{
			Instability_Event = SC_VERY_LOW_WATER;
		}

		Stable_Water_Counter = 0;
	}
	else
	{
		if (vwi_status == WS_WATER)
		{
			if (Stable_Water_Counter < WATER_PRESENT_TIME_100MS)
			{
				Stable_Water_Counter ++;
			}
			else
			{
				if (Instability_Event == SC_VERY_LOW_WATER)
				{
					Instability_Event = SC_NO_INSTABILITY;
				}

				Very_Low_Water_Counter = 0;
			}
		}
		else
		{
			Stable_Water_Counter = 0;
		}
	}

	// Airlock detection
	if (AirlockMonitor__GetStatus() == ALS_AIRLOCK)
	{
		Instability_Event = SC_RIF_AIR_LOCK;
	}
	else
	{
		if (Instability_Event == SC_RIF_AIR_LOCK)
		{
			Instability_Event = SC_NO_INSTABILITY;
		}
	}

	// Plugging detection
	if (PluggingMonitor__GetStatus() == PL_PLUGGING)
	{
		Instability_Event = SC_RIF_PLUGGED_FILTER;

		if ((STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_Plugged_Filter != (STABILITY_CONTROL_REACTIONS_TYPE)SC_REACTION_NONE)
		{
			Fault__ConditionDetected(FAULT_ID_RIF_FILTER_PLUGGED);
		}
	}
	else
	{
		if (Instability_Event == SC_RIF_PLUGGED_FILTER)
		{
			Instability_Event = SC_NO_INSTABILITY;
		}
	}

	// Stability Control Loop detection
	if (StabilityControlLoopMonitor__GetOutcome() == SCL_PRIMARY_REACTION_REQUESTED)
	{
		Instability_Event = SC_SCL_PRIMARY_REACTION_REQ;
	}
	else if (StabilityControlLoopMonitor__GetOutcome() == SCL_SECONDARY_REACTION_REQUESTED)
	{
		Instability_Event = SC_SCL_SECONDARY_REACTION_REQ;
	}
	else
	{
		if ((Instability_Event == SC_SCL_PRIMARY_REACTION_REQ) || (Instability_Event == SC_SCL_SECONDARY_REACTION_REQ))
		{
			Instability_Event = SC_NO_INSTABILITY;
		}
	}
}


/**
 * The function takes the currently selected reaction and check if it has to be modified according to the specific requirements
 *
 * @param current_reaction_ptr: pointer to the currently selected reaction
 * @param liters_in_tub: the amount of water currently in the tub (used when the reaction requests to add water)
 * @param phase_repetitions: the number of times that the Restart Phase reaction has already been triggered (used when the reaction requests to restart the phase)
 *
 * @return : none
 */
static void PartialReactionUpdate(STABILITY_CONTROL_REACTIONS_TYPE *current_reaction_ptr, sint32 liters_in_tub, sint32 phase_repetitions)
{
	BOOL_TYPE check_subcycle;
	uint8 subcycle_priority;
	uint8 subcycle_execution_counter;
	uint8 subcycle_max_calls;

	if ((*current_reaction_ptr == SC_REACTION_ADD_WATER_THEN_REDUCE_SPEED) && (liters_in_tub >= (sint32)Cliters_Max))
	{
		*current_reaction_ptr = SC_REACTION_REDUCE_MOTOR_SPEED;	// Req 1246164
	}

	if ((*current_reaction_ptr == SC_REACTION_REDUCE_MOTOR_SPEED) && (Speed_Reduction_Status == SC_2ND_SPEED_REDUCTION))
	{
		*current_reaction_ptr = Reaction_After_Max_Speed_Reduct; // Req. 1243484

		if ((*current_reaction_ptr == SC_REACTION_REDUCE_MOTOR_SPEED) || (*current_reaction_ptr == SC_REACTION_ADD_WATER_THEN_REDUCE_SPEED)	// No recursion allowed
			|| (*current_reaction_ptr >= SC_REACTIONS_NUM))	// Default value for backward compatibility with old setting file
		{
			*current_reaction_ptr = SC_REACTION_NONE;
		}
	}

	if (liters_in_tub >= (sint32)Cliters_Max)
	{
		switch (*current_reaction_ptr)
		{
			case SC_REACTION_ADD_WATER:
				*current_reaction_ptr = SC_REACTION_NONE; // Req 307052
				break;

			case SC_REACTION_ADD_WATER_THEN_RESTART_PHASE:
				*current_reaction_ptr = SC_REACTION_RESTART_PHASE; // Req 308251
				break;

			case SC_REACTION_ADD_WATER_THEN_SKIP_PHASE:
				*current_reaction_ptr = SC_REACTION_SKIP_PHASE; // Req 308256
				break;

			default:
				// Nothing to do
				break;
		}
	}

	if (*current_reaction_ptr == SC_REACTION_RESTART_PHASE_THEN_SKIP_PHASE)
	{
		if (phase_repetitions == 0)
		{
			*current_reaction_ptr = SC_REACTION_RESTART_PHASE; // Req 308257
		}
		else
		{
			*current_reaction_ptr = SC_REACTION_SKIP_PHASE; // Req 308258
		}
	}

	// Check the max number of subcycle executions
	check_subcycle = TRUE;

	switch (*current_reaction_ptr)
	{
		case SC_REACTION_ADD_WATER: case SC_REACTION_ADD_WATER_THEN_RESTART_PHASE: case SC_REACTION_ADD_WATER_THEN_SKIP_PHASE:
		case SC_REACTION_ADD_WATER_THEN_REDUCE_SPEED:
			subcycle_priority = Common_Parameters_Ptr->Add_Water_Subcycle_Priority;
			subcycle_execution_counter = SC_Subcycle_Execution_Counters[ADD_WATER_INDEX];
			subcycle_max_calls = SC_Max_Subcycle_Calls.Add_Water;
			break;

		case SC_REACTION_MOTOR_RESTART_SUBCYCLE:
			subcycle_priority = Common_Parameters_Ptr->Motor_Restart_Subcycle_Priority;
			subcycle_execution_counter = SC_Subcycle_Execution_Counters[MOTOR_RESTART_INDEX];
			subcycle_max_calls = SC_Max_Subcycle_Calls.Motor_Restart;
			break;

		case SC_REACTION_FILTER_PLUGGED_SUBCYCLE:
			subcycle_priority = Common_Parameters_Ptr->Filter_Plugged_Subcycle_Priority;
			subcycle_execution_counter = SC_Subcycle_Execution_Counters[FILTER_PLUGGED_INDEX];
			subcycle_max_calls = SC_Max_Subcycle_Calls.Filter_Plugged;
			break;

		case SC_REACTION_DEFOAMING_SUBCYCLE:
			subcycle_priority = Common_Parameters_Ptr->Defoaming_Subcycle_Priority;
			subcycle_execution_counter = SC_Subcycle_Execution_Counters[DEFOAMING_INDEX];
			subcycle_max_calls = SC_Max_Subcycle_Calls.Defoaming;
			break;

		case SC_REACTION_FILTER_PLUGGED_2_SUBCYCLE:
			subcycle_priority = Common_Parameters_Ptr->Filter_Plugged_2_Subcycle_Priority;
			subcycle_execution_counter = SC_Subcycle_Execution_Counters[FILTER_PLUGGED_2_INDEX];
			subcycle_max_calls = SC_Max_Subcycle_Calls.Filter_Plugged_2;
			break;

		default:
			check_subcycle = FALSE;
			// The following initializations are only to silent Lint warning, values are not used since (check_subcycle == FALSE)
			subcycle_priority = 0;
			subcycle_execution_counter = 0;
			subcycle_max_calls = 0;
			break;
	}

	if ((uint8)check_subcycle == (uint8)TRUE)
	{
		if ((subcycle_priority == (uint8)0) ||
			((subcycle_max_calls != (uint8)INVALID_MAX_SUBCYCLE_CALLS) && (subcycle_execution_counter >= subcycle_max_calls)))
		{
			// The requested subcycle is not present in setting file OR the number of subcycle executions has reached its limit
			*current_reaction_ptr = SC_REACTION_NONE;
		}
	}

}



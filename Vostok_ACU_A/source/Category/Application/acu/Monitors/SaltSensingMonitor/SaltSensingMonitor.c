//! The name of this module to be used by the Log module.
#define MODULE_NAME MODULE_SALT_SENSING_MONITOR

//! The number of times that the SaltSensingMonitor__Execution function will be called every second while running.
#define SALT_SENSING_MONITOR_CALLS_PER_SECOND 	10

// If the call rate of the monitor is different from 10, Salt_Stability_Counter has to be rescaled
CT_ASSERT(SALT_SENSING_MONITOR_CALLS_PER_SECOND == 10);

//! A list of log message IDs that are generated by this module.

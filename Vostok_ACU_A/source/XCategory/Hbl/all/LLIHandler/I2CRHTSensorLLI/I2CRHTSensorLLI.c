/**
 * @brief   This function convert raw value to percentage value
 *          Formula for conversion is given in Datasheet SHT20,
 *          section 6.1 Relative Humidity Conversion, page 10
 * @param   input: data related to the instance of the LLI
 */
#if (HBL_LLI_NUM_I2C_RHT_HUMIDITY_SENSOR > 0)
static void RHRawToPercent (HBL_LLI_TYPE * input)
{
    uint16 hum_us = 0;
    uint32 hum_ul = 0;

    hum_ul = ((uint32) SHT2XHumidity_Data[input->Data_Index].Sensor_I2C_RH_Raw_Percentage * 125);
    hum_us = (uint16) (hum_ul / 65536);
    SHT2XHumidity_Data[input->Data_Index].Sensor_I2C_RH_Percentage = hum_us - 6;
}
#endif

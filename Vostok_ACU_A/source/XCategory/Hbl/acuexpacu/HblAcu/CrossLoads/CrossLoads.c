/**
 * @brief   This method checks if a load status is on a transition state or not
 * @param   load_index  Load index to check
 * @return  TRUE if Load status is not on target, FALSE if load status is on target or load is not preset
 */
static BOOL_TYPE IsLoadStatusOnTransition(uint8 load_index)
{
    BOOL_TYPE ret_val = FALSE;
    HBL_LOAD_STATUS_TYPE *load_status = Hbl__GetLoadStatusByIndex(load_index);

    if (load_status != NULL)
    {
        ret_val = (BOOL_TYPE) ((load_status->Load.Status != HBL_LOAD_STATUS_TARGET_COMPLETED) &&
                (load_status->Load.Status != HBL_LOAD_STATUS_TARGET_ALT_TABLE));
    }

    return(ret_val);
}

//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief   This method checks if a Cross Load (referred by its load_index) is on a transition state or not
 * @param   load_index      Load index to check
 *          check_previous  if ENABLED it checks also if previous state is on a transition state or not
 * @return  TRUE if Cross Load status is not on a steady state, FALSE if on a a steady state or it's not a Cross Load
 */
static BOOL_TYPE IsCrossLoadTransition(uint8 load_index, BOOL_TYPE check_previous)
{
    BOOL_TYPE ret_val = FALSE;
    uint8 cross_load_index = GetCrossLoadIndex(load_index);

    if(cross_load_index < Crossloads_Num)
    {
        CROSSLOAD_DATA_TYPE *data_ptr = &Crossload_Data[cross_load_index];
        if((data_ptr->Generic_State != CROSSLOAD_STEADY_STATE) && (data_ptr->Generic_State != CROSSLOAD_FORCED_STATE) &&
            ((check_previous == FALSE) ||
                ((data_ptr->Previous_State != CROSSLOAD_STEADY_STATE) && (data_ptr->Previous_State != CROSSLOAD_FORCED_STATE))))
        {
            ret_val = TRUE;
        }
    }
    return(ret_val);
}



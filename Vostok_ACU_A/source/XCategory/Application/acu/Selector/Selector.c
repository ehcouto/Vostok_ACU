/**
 *  @brief  Get a pointer to the low statement data index length table.
 *
 *  @return uint16 * lo_statement_data_length_ptr - Pointer to the low statement data index length table.
 *  @retval pointer to default lo_statement data index length if setting file data can not be found.
 */
uint16 * Selector__GetLoStatmentIndexLengthPtr(void)
{
    uint16 * lo_statement_data_length_ptr = NULL;
    SETTINGFILE_LOADER_TYPE loader;

    if (SettingFile__BasicLoader((SF_PTR_ACU_CROSS_PRODUCT_CONFIG), SF_DISPL_LO_STATEMENT_DISPLACEMENT_SIZE, &loader) == PASS)
    {
        lo_statement_data_length_ptr = (uint16 *)(void *)loader.Data;
    }
    else
    {
        //LOG_ADD_EXCEPTION(SF_LIGHT_CONFIG_READ_FAIL, 0);
        lo_statement_data_length_ptr = (uint16 *)(void *)&LO_STATEMENT_DATA_INDEX_LENGTH_HARD_CODED;
    }

    return lo_statement_data_length_ptr;
}


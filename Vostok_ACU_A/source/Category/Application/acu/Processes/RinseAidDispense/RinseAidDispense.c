void RinseAidDispense__Initialize(void)
{
	RinseAid__Initialize();
	TridgeDetergent__Initialize();
}

PROCESS_RESULT_TYPE RinseAidDispense__Process(PROCESS_CONTROL_TYPE* control)
{
    PROCESS_RESULT_TYPE result = PROCESS_COMPLETE;

    if (control == NULL)
    {
        LOG_ADD_EXCEPTION(PROCESS_CONTROL_IS_NULL, 0);
    }
    else if ((control->Compartment >= APPLIANCE_MAX_COMPARTMENTS) ||
    		 (control->F1_F0_Flags >= (uint8)RINSE_AID_DISPENSE_OPTION_COUNT))
    {
        LOG_ADD_EXCEPTION(PROCESS_CONTROL_PARAMS_EXCEED_LIMITS, 0);
    }
    else
    {
		switch (control->F1_F0_Flags)
		{
			case RINSE_AID_DISPENSE:
			 	if (control->Process_Instance < RINSE_AID_MAX_INSTANCES)
			 	{
			 		result = RinseAid__Manager(control);
			 	}
			 	else
			 	{
			        LOG_ADD_EXCEPTION(PROCESS_CONTROL_PARAMS_EXCEED_LIMITS, 1);
			 	}
				break;

			case TRIDGE_DETERGENT_DISPENSE:
			 	if (control->Process_Instance < TRIDGE_DETERGENT_MAX_INSTANCES)
			 	{
			 		result = TridgeDetergent__Manager(control);
			 	}
			 	else
			 	{
			        LOG_ADD_EXCEPTION(PROCESS_CONTROL_PARAMS_EXCEED_LIMITS, 2);
			 	}
				break;

			default:
				// No other valid options
				break;
		}

    }

    return result;
}



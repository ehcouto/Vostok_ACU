/**
 * @file
 * @brief       A cross-category process for determining when to jump to a different step, phase, and/or cycle.
 *
 * @details
 *
 * JumpIf Setting File Parameter Data
 *
 * For a simple jump within the current cycle and phase:
 * <pre>
 *      Word Offset     First Byte              Second Byte
 *      -----------     ---------------         ----------------
 *      +0000h          Condition_Index         Statement_Offset
 * </pre>
 *
 * For a jump to a different cycle and/or phase:
 * <pre>
 *      Word Offset     First Byte              Second Byte
 *      -----------     ---------------         ----------------
 *      +0000h          Condition_Index         Statement_Offset
 *      +0001h          Phase_Offset            Cycle_Offset
 * </pre>
 *
 * For a jump that may return or may have special instructions for estimating:
 * <pre>
 *      Word Offset     First Byte              Second Byte
 *      -----------     ---------------         ----------------
 *      +0000h          Condition_Index         Statement_Offset
 *      +0001h          Phase_Offset            Cycle_Offset
 *      +0002h          Bit 7 = Reerved         0
 *                      Bit 6-5 = Predict
 * </pre>
 *
 * Condition Index: An index to the test condition to evaluate. If the condition evaluates to TRUE,
 * then the jump occurs. If the condition is FALSE, then no jump occurs.
 *
 * Cycle Offset: A signed value [-128, 127] that is added to the current cycle to determine the
 * target cycle. A value of 0 indicates that there is no change in the cycle. A value of -128
 * should be used to set the target cycle to zero.
 *
 * Phase Offset: A signed value [-128, 127] that is added to the current phase to determine the
 * target phase. A value of 0 indicates that there is no change in the phase. A value of -128
 * should be used to set the target phase to zero.
 *
 * Statement Offset: A signed value [-128, 127] that is added to the current statement to determine
 * the new location. A value of -128 should be used to set the target statement to zero.
 *
 * Predict: The prediction field contains instructions on how to interpret the JumpIf behavior
 * during cycle time estimation.
 *      0 = Predict that the Jump will not occur. Do not evaluate the condition and never jump.
 *      1 = Predict that the Jump will occur. Do not evaluate the condition and always jump.
 *      2, 3, or unspecified = Evaluate the condition to determine whether or not to jump.
 *
 *
 * $Header: JumpIf.c 1.6 2015/08/05 17:04:36EDT Nelson Ferragut II (FERRANJ) Exp  $
 *
 * @copyright  Copyright 2014 - $Date: 2015/08/05 17:04:36EDT $  Whirlpool Corporation.  All rights reserved - CONFIDENTIAL.
 */


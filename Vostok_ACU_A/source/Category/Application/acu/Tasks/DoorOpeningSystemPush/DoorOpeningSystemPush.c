/**
 * It initializes the Door Opening System Push task and its data.
 *
 * @param  compartment = compartment inside which the task is running
 * @param  sf_index = the index of the setting file from which to load the task data
 * @param  displacement = the displacement for the task data in the setting file (unused, since all the instances
 * 		   of the Door Opening System Push Task use the same data table).
 * @return the status of the task (ACTIVE or INACTIVE)
 *
 */
ACTIVE_TYPE DoorOpeningSystemPush__Initialize(uint8 compartment, uint8 sf_index, uint8 displacement)
{
    SETTINGFILE_LOADER_TYPE loader;
	ACTIVE_TYPE ret_value = INACTIVE;

	if (Hbl__GetLoadIndexByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM) != (uint8)HBL_INDEX_INVALID)
	{
		// Get the DOS Push parameters from the setting file.
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM, &loader) == PASS)
		{
			DOS_Push_Data.SF_DOS_Push_Params_Ptr = (DOOR_OPENING_SYSTEM_SF_PARAMS_TYPE *)(void*)loader.Data;

			DoorOpeningSystemPush__StartPush(DOS_Push_Data.SF_DOS_Push_Params_Ptr->Push_Pre_On_Time);
			ret_value = ACTIVE;
		}
		else
		{
			// Algorithm parameters missing, allow step advancement
			LOG_ADD_EXCEPTION(ERROR_MISSING_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM);
		}
	}
	else
	{
		// Door Opening System not present, allow step advancement
	}

	return ret_value;
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * Function called during the execution of the Door Opening System Push task.
 *
 * @param  compartment = compartment inside which the task is running
 * @return the status of the task (ACTIVE or INACTIVE)
 */
ACTIVE_TYPE DoorOpeningSystemPush__Task(uint8 compartment)
{
	ACTIVE_TYPE ret_value = ACTIVE;
	ON_OFF_TYPE dos_pin_state;

	if (DOS_Push_Data.DOS_Push_Counter > (uint16)0)
	{
		DOS_Push_Data.DOS_Push_Counter --;
	}

	switch (DoorOpeningSystemMonitor__GetDOSPushState())
	{
		case DOS_PUSH_PRE_ON_STEP:

			if (DOS_Push_Data.DOS_Push_Counter > (uint16)0)
			{
				dos_pin_state = OFF;
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM, HBL_PRIORITY_DOOR_OPENING_SYSTEM, (uint8 *) &dos_pin_state);	
				break;
			}
			else
			{
				DOS_Push_Data.DOS_Push_Counter = (uint16)DOS_Push_Data.SF_DOS_Push_Params_Ptr->Push_1st_On_Time * DOS_PUSH_CALLS_PER_SECOND;
				DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_FST_ON_STEP);
			}

		//lint -fallthrough
		case DOS_PUSH_FST_ON_STEP:
			if (DOS_Push_Data.DOS_Push_Counter > (uint16)0)
			{
				dos_pin_state = ON;
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM, HBL_PRIORITY_DOOR_OPENING_SYSTEM, (uint8 *) &dos_pin_state);	
				break;
			}
			else
			{
				DOS_Push_Data.DOS_Push_Counter = (uint16)DOS_Push_Data.SF_DOS_Push_Params_Ptr->Push_Off_Time * DOS_PUSH_CALLS_PER_SECOND;
				DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_OFF_STEP);
			}

			//lint -fallthrough
		case DOS_PUSH_OFF_STEP:

			if (DOS_Push_Data.DOS_Push_Counter > (uint16)0)
			{
				dos_pin_state = OFF;
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM, HBL_PRIORITY_DOOR_OPENING_SYSTEM, (uint8 *) &dos_pin_state);	
				break;
			}
			else
			{
				DOS_Push_Data.DOS_Push_Counter = (uint16)DOS_Push_Data.SF_DOS_Push_Params_Ptr->Push_2nd_On_Time * DOS_PUSH_CALLS_PER_SECOND;
				DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_SND_ON_STEP);
			}

			//lint -fallthrough
		case DOS_PUSH_SND_ON_STEP:
			if (DOS_Push_Data.DOS_Push_Counter > (uint16)0)
			{
				dos_pin_state = ON;
				(void)Hbl__SetLoadRequestByLoadID(HBL_LOAD_DOOR_OPENING_SYSTEM, HBL_PRIORITY_DOOR_OPENING_SYSTEM, (uint8 *) &dos_pin_state);	
			}
			else
			{
				DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_COMPLETE);

				ret_value = INACTIVE;
			}
			break;

		default:
			// Includes DOS_PUSH_COMPLETE
			ret_value = INACTIVE;
			break;
	}

	return ret_value;
}


/**
 * It is called at the initialization of the task after NVdata restore.
 * If it founds that the task was running before the reset, it restarts it.
 *
 * @param  compartment = compartment inside which the task is running
 *
 * @return  None
 */
void DoorOpeningSystemPush__NVDataRestore(uint8 compartment)
{
	if (DoorOpeningSystemMonitor__GetDOSPushState() != DOS_PUSH_COMPLETE)
	{
	    SETTINGFILE_LOADER_TYPE loader;

		// If Push procedure has been interrupted by a cold reset it has to be restarted

		// Get the DOS Push parameters from the setting file.
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM, &loader) == PASS)
		{
			DOS_Push_Data.SF_DOS_Push_Params_Ptr = (DOOR_OPENING_SYSTEM_SF_PARAMS_TYPE *)(void*)loader.Data; 

			DoorOpeningSystemPush__StartPush(DOS_Push_Data.SF_DOS_Push_Params_Ptr->Push_Pre_On_Time);
		}
		else
		{
			// Algorithm parameters missing, allow step advancement
			LOG_ADD_EXCEPTION(ERROR_MISSING_PARAMETERS, DSPL_DOOR_OPENING_SYSTEM);
			DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_COMPLETE);
		}

	}
}



//---------------------------------------------------------------------------------------------------------------------
/**
 * Get the target cycle, phase, and statement for the current JumpIf statement.
 *
 * @param parameter = Pointer to the JumpIf parameter data structure.
 * @param length = Number of bytes in the parameter structure.
 * @param cps = A pointer to the current cycle, phase, and statement.
 *              It will be updated with the new cycle, phase, and statement.
 *
 * @retval TRUE = Valid target found.
 * @retval FALSE = Valid target not found.
 *
 */
static BOOL_TYPE GetTarget(JUMPIF_PARAMETER_TYPE* parameter, uint8 length, CYCLE_PHASE_STATEMENT_TYPE* cps)
{
    BOOL_TYPE valid_target;
    sint16 target_cycle;
    sint16 target_phase;
    sint16 target_statement;

    // Determine the target statement.
    valid_target = TRUE;
    if (parameter->Statement_Offset == TARGET_FIRST_ITEM)
    {
        target_statement = 0;
    }
    else
    {
        target_statement = cps->Statement + parameter->Statement_Offset;
        if ((target_statement < 0) || (MAX_STATEMENTS <= target_statement))
        {
            LOG_ADD_EXCEPTION(TARGET_STATEMENT_OUT_OF_RANGE, (cps->Phase << 8) + cps->Statement);
            valid_target = FALSE;
        }
        else if ((target_statement == 0) && (cps->Statement != 0))
        {
            LOG_ADD_EXCEPTION(GESE_SHOULD_USE_NEG_128, (cps->Phase << 8) + cps->Statement);
        }
    }

    if (length >= (sizeof(parameter->Condition_Index) +
                   sizeof(parameter->Statement_Offset) +
                   sizeof(parameter->Phase_Offset) +
                   sizeof(parameter->Cycle_Offset)))
    {
        // Determine the target phase.
        if (parameter->Phase_Offset == TARGET_FIRST_ITEM)
        {
            target_phase = 0;
        }
        else
        {
            target_phase = cps->Phase + parameter->Phase_Offset;
            if ((target_phase < 0) || (MAX_PHASES <= target_phase))
            {
                LOG_ADD_EXCEPTION(TARGET_PHASE_OUT_OF_RANGE, (cps->Phase << 8) + cps->Statement);
                valid_target = FALSE;
            }
            else if ((target_phase == 0) && (cps->Phase != 0))
            {
                LOG_ADD_EXCEPTION(GESE_SHOULD_USE_NEG_128, (cps->Phase << 8) + cps->Statement);
            }
        }

        // Determine the target cycle.
        target_cycle = cps->Cycle + parameter->Cycle_Offset;
        if ((target_cycle <= 0) || (MAX_CYCLES <= target_cycle))
        {
            LOG_ADD_EXCEPTION(TARGET_CYCLE_OUT_OF_RANGE, (cps->Phase << 8) + cps->Statement);
            valid_target = FALSE;
        }

        // Write the target statement, phase, and cycle.
        if (valid_target == TRUE)
        {
            cps->Statement = target_statement;
            cps->Phase = target_phase;
            cps->Cycle = target_cycle;
        }
    }
    else
    {
        // Write the target statement only.
        // Jump was within the current cycle and phase.
        if (valid_target == TRUE)
        {
            cps->Statement = target_statement;
        }
    }

    return (valid_target);
}


//---------------------------------------------------------------------------------------------------------------------
/**
 * @brief       Read the process parameters and setup the JumpIf process.
 *
 * @param control = The control parameters provided by the Interpreter.
 */
static void ReadProcessParameters(PROCESS_CONTROL_TYPE* control)
{
    SETTINGFILE_LOADER_TYPE loader;

    // Get the JumpIf process parameters from the setting file.
    if (SettingFile__MSettingBasicLoader(control->Setting_File_Index,
                                         SF_PTR_ACU_PROCESS_JUMP_IF,
                                         control->Data_Field_Index,
                                         &loader) == FAIL)
    {
        Parameter = NULL;
        if (control->Step_Initializing == TRUE)
        {
            LOG_ADD_EXCEPTION(INVALID_PROCESS_PARAMETERS, control->Data_Field_Index);
        }
    }
    else
    {
        Parameter = (JUMPIF_PARAMETER_TYPE*)(loader.Data);  //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
        Length = loader.Length;
    }
}


/**
 *  @brief      It Initializes the Stability Control Task module and its variables
 *
 */
ACTIVE_TYPE StabilityControl__Initialize(uint8 compartment, uint8 sf_index, uint8 displacement)
{
	ACTIVE_TYPE ret_value;
    SETTINGFILE_LOADER_TYPE loader;
	CYCLE_PHASE_STATEMENT_TYPE cps;

    ret_value = INACTIVE;

    Common_Parameters_Ptr = NULL;
	Instance_Parameters_Ptr = NULL;
	Reaction_After_Max_Speed_Reduct = SC_REACTIONS_NUM;	// Default value for backward compatibility with old setting file
	Cliters_Max = 0;
	memset(&SC_Max_Subcycle_Calls, INVALID_MAX_SUBCYCLE_CALLS, sizeof(SC_Max_Subcycle_Calls));

	// Get Stability Control common parameters from setting file
	if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DISPL_STABILITY_CONTROL, &loader) == PASS)
	{
		Common_Parameters_Ptr = (const STABILITY_CONTROL_COMMON_PARAMS_TYPE *)(void *)loader.Data;

		if (loader.Length >= (uint16)12)
		{
			SC_Max_Subcycle_Calls.Add_Water = Common_Parameters_Ptr->Max_Add_Water_Subcycle_Calls;
			SC_Max_Subcycle_Calls.Motor_Restart = Common_Parameters_Ptr->Max_Motor_Restart_Subcycle_Calls;
			SC_Max_Subcycle_Calls.Filter_Plugged = Common_Parameters_Ptr->Max_Filter_Plugged_Subcycle_Calls;
			if (loader.Length >= (uint16)16)
			{
				SC_Max_Subcycle_Calls.Defoaming = Common_Parameters_Ptr->Max_Defoaming_Subcycle_Calls;
				SC_Max_Subcycle_Calls.Filter_Plugged_2 = Common_Parameters_Ptr->Max_Filter_Plugged_2_Subcycle_Calls;
			}
			else
			{
				// Old setting files: the parameters are not present in setting file, the default value is kept
			}
		}
		else
		{
			// Old setting files: the parameters are not present in setting file, the default value is kept
		}

		// Get the Fill parameters from setting file.
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DISPL_CLASS_A_FILL, &loader) == PASS)
		{
			// Class A Fill project

			// Convert Liters_Max in cl
			Cliters_Max = (((CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data)->Liters_In_Tub_Max) * (uint16)5;
		}
		else if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DISPL_FILL, &loader) == PASS)
		{
			// Non-Class A Fill project

			// Convert Liters_Max in cl
			Cliters_Max = ((const FILL_PARAMETER_TYPE *)(void *)loader.Data)->Liters_Max * (uint16)5;
		}
		else
		{
			// Fill data not present in setting file;
			loader.Data = NULL;
		}

		if (loader.Data != NULL)
		{
			// Get Stability Control instance specific parameters from setting file
			if (SettingFile__BasicLoader(SF_PTR_ACU_STABILITY_CONTROL_DATA, displacement, &loader) == PASS)
			{
				Instance_Parameters_Ptr = (const STABILITY_CONTROL_INSTANCE_PARAMS_TYPE *)(void *)loader.Data;
				if (loader.Length >= (uint16)8)
				{
					Reaction_After_Max_Speed_Reduct = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_After_Max_Speed_Reduct;
				}

				Stability_Control_Status = ACTIVE;
				ret_value = ACTIVE;

				// Enable and initialize Airlock and Plugging algorithms
				AirlockMonitor__Reset();
				AirlockMonitor__Start();

				PluggingMonitor__Start();
				PluggingMonitor__Reset();

				HeaterCalcificationDetectionMonitor__Reset();
				HeaterCalcificationDetectionMonitor__Start();
			}
			else
			{
				LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)SF_PTR_ACU_STABILITY_CONTROL_DATA);
			}
		}
		else
		{
			LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)DISPL_FILL);
		}

	}
	else
	{
		LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)DISPL_STABILITY_CONTROL);
	}

	Fault__ConditionRemoved(FAULT_ID_VERY_LOW_WATER_OR_SUDS);

	ResetInstabilityEvent();

	cps = Interpreter__GetCurrentStatement(COMPARTMENT_DEFAULT);
	Activation_Cycle = cps.Cycle;

	Task_Suspended = FALSE;

	return (ret_value);
}

/**
 * @brief The function is called at the initialize of the task after NVdata restore
 *        It Initializes the Stability Control module and its variables
 *
 * @param compartment that run the task (unused)
 *
 * @return  None
 */
void StabilityControl__NVDataRestore(uint8 compartment)
{
	ResetInstabilityEvent();
	Task_Suspended = FALSE;
}

/**
 * The function called during the execution of the task
 * @param compartment that run the task (inside)
 * @return the status of the task (ACTIVE or INACTIVE)
 */
ACTIVE_TYPE StabilityControl__Task(uint8 compartment)
{
	CYCLE_PHASE_STATEMENT_TYPE cps;

	cps = Interpreter__GetCurrentStatement(COMPARTMENT_DEFAULT);

	if ((cps.Cycle != Activation_Cycle) || ((uint8)Fault__IsObserverActive(FAULT_ID_VERY_LOW_WATER_OR_SUDS) == (uint8)TRUE))
	{
		// PAUSE or Subcycle running (including reaction subcycle: Add water, Motor Restart, Filter Plugged), or
		// Fault Observer active with specific FAULT_ID_VERY_LOW_WATER_OR_SUDS fault ID: the task keeps running
		// but doesn't update inputs nor trigger any reaction.

		if (Task_Suspended == FALSE)
		{
			PluggingMonitor__Pause();
		}

		Task_Suspended = TRUE;
	}
	else
	{
		STABILITY_CONTROL_REACTIONS_TYPE selected_reaction;

		if ((uint8)Task_Suspended == (uint8)TRUE)
		{
			AirlockMonitor__Reset();

			if (Instability_Event == SC_RIF_PLUGGED_FILTER)
			{
				// The suspension of the Stability Control was caused by a reaction to a  Plugging Event
				PluggingMonitor__Reset();
			}
			else
			{
				// The suspension of the Stability Control wasn't caused by a reaction to a  Plugging Event
				PluggingMonitor__Resume();
			}

			// Inputs reset when coming from suspended state
			ResetInstabilityEvent();

			Task_Suspended = FALSE;
		}

		UpdateInstabilityEvent();

		// Get requested reaction from setting file table depending on Instability_Event
		switch (Instability_Event)
		{
			case SC_INSTABILITY:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_Instability;
				break;

			case SC_VERY_LOW_WATER:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_Very_Low_Water;
				break;

			case SC_NO_WATER:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_No_Water;
				break;

			case SC_RIF_AIR_LOCK:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_Air_Lock;
				break;

			case SC_RIF_PLUGGED_FILTER:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_To_Plugged_Filter;
				break;

			case SC_SCL_PRIMARY_REACTION_REQ:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Loop_Detection_Primary_Reaction;
				break;

			case SC_SCL_SECONDARY_REACTION_REQ:
				selected_reaction = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Loop_Detection_Secondary_Reaction;
				break;

			default:
				selected_reaction = SC_REACTION_NONE;
				break;
		}

		if (selected_reaction != SC_REACTION_NONE)
		{
			// Apply modifications to requested reaction, according to requirements
			StabilityControl__UpdateReaction(&selected_reaction);
		}

		if (selected_reaction != SC_REACTION_NONE)
		{
			// Reset Stability Control Loop counters if any reaction is triggered for any reason.
			StabilityControlLoopMonitor__ResetCounters();
		}

		// Trigger requested reaction
		switch (selected_reaction)
		{
			case SC_REACTION_ADD_WATER: case SC_REACTION_ADD_WATER_THEN_RESTART_PHASE: case SC_REACTION_ADD_WATER_THEN_SKIP_PHASE:
			case SC_REACTION_ADD_WATER_THEN_REDUCE_SPEED:
				if (SC_Subcycle_Execution_Counters[ADD_WATER_INDEX] < (uint8)__UNSIGNED_CHAR_MAX__)
				{
					SC_Subcycle_Execution_Counters[ADD_WATER_INDEX] ++;
				}

				(void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, Common_Parameters_Ptr->Add_Water_Subcycle_Priority);
				// Note that in this case a subcycle is going to be executed, during this subcycle the Stability Control
				// will be "suspended", it means that it will not read any input nor trigger any reaction.

				StatisticsDish__IncreaseWaterAddExecutions();
				BIT_SET(Stability_Control_Flags, SC_WATER_ADDED);
				break;

			case SC_REACTION_RESTART_PHASE:
				if (Interpreter__SetPhase(COMPARTMENT_DEFAULT, Instance_Parameters_Ptr->Phase_Where_To_Restart) == PHASE_STEP_RESULT_PASS)
				{
					sint32 phase_repetitions;
					if (Variable__GetValue(VARIABLE_PHASE_REPETITIONS, 0, &phase_repetitions) == FALSE)
					{
						phase_repetitions = 0;
					}

					if (phase_repetitions < UCHAR_MAX)
					{
						(void)Variable__SetValue (VARIABLE_OPERATION_INCREMET, VARIABLE_PHASE_REPETITIONS, 0, 1);
					}

					if (Instability_Event != SC_RIF_AIR_LOCK)
					{
						AirlockMonitor__Reset();
					}

					ResetInstabilityEvent();

					StatisticsDish__IncreaseInterruptedPhasesNum();
					BIT_SET(Stability_Control_Flags, SC_PHASE_RESTARTED);
				}
				break;

			case SC_REACTION_SKIP_PHASE:
				if (Interpreter__SetPhase(COMPARTMENT_DEFAULT, Instance_Parameters_Ptr->Phase_Where_To_Skip) == PHASE_STEP_RESULT_PASS)
				{
					if (Instability_Event != SC_RIF_AIR_LOCK)
					{
						AirlockMonitor__Reset();
					}

					ResetInstabilityEvent();

					StatisticsDish__IncreaseInterruptedPhasesNum();
					BIT_SET(Stability_Control_Flags, SC_PHASE_SKIPPED);
				}
				break;

			case SC_REACTION_MOTOR_RESTART_SUBCYCLE:
				if (SC_Subcycle_Execution_Counters[MOTOR_RESTART_INDEX] < (uint8)__UNSIGNED_CHAR_MAX__)
				{
					SC_Subcycle_Execution_Counters[MOTOR_RESTART_INDEX] ++;
				}

				(void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, Common_Parameters_Ptr->Motor_Restart_Subcycle_Priority);
				// Note that in this case a subcycle is going to be executed, during this subcycle the Stability Control
				// will be "suspended", it means that it will not read any input nor trigger any reaction.
				break;

			case SC_REACTION_FILTER_PLUGGED_SUBCYCLE:
				if (SC_Subcycle_Execution_Counters[FILTER_PLUGGED_INDEX] < (uint8)__UNSIGNED_CHAR_MAX__)
				{
					SC_Subcycle_Execution_Counters[FILTER_PLUGGED_INDEX] ++;
				}

				(void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, Common_Parameters_Ptr->Filter_Plugged_Subcycle_Priority);
				// Note that in this case a subcycle is going to be executed, during this subcycle the Stability Control
				// will be "suspended", it means that it will not read any input nor trigger any reaction.

				BIT_SET(Stability_Control_Flags, SC_FILTER_CLEANED);
				break;

			case SC_REACTION_ERROR_DETECTED:
				Fault__ConditionDetected(FAULT_ID_VERY_LOW_WATER_OR_SUDS);

				BIT_SET(Stability_Control_Flags, SC_FAULT_DETECTED);
				break;

			case SC_REACTION_REDUCE_MOTOR_SPEED:
				if (Speed_Reduction_Status == SC_NO_SPEED_REDUCTION)
				{
					Speed_Reduction_Status = SC_1ST_SPEED_REDUCTION;
					ResetInstabilityEvent();
					StatisticsDish__IncreaseRPMReductions();
					BIT_SET(Stability_Control_Flags, SC_PUMP_RPM_REDUCED_ONCE);
				}
				else if (Speed_Reduction_Status == SC_1ST_SPEED_REDUCTION)
				{
					Speed_Reduction_Status = SC_2ND_SPEED_REDUCTION;
					ResetInstabilityEvent();
					StatisticsDish__IncreaseRPMReductions();
					BIT_SET(Stability_Control_Flags, SC_PUMP_RPM_REDUCED_TWICE);
				}
				else
				{
					// Nothing to do
				}

				break;

			case SC_REACTION_DEFOAMING_SUBCYCLE:
				if (SC_Subcycle_Execution_Counters[DEFOAMING_INDEX] < (uint8)__UNSIGNED_CHAR_MAX__)
				{
					SC_Subcycle_Execution_Counters[DEFOAMING_INDEX] ++;
				}

				(void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, Common_Parameters_Ptr->Defoaming_Subcycle_Priority);
				// Note that in this case a subcycle is going to be executed, during this subcycle the Stability Control
				// will be "suspended", it means that it will not read any input nor trigger any reaction.
				break;

			case SC_REACTION_FILTER_PLUGGED_2_SUBCYCLE:
				if (SC_Subcycle_Execution_Counters[FILTER_PLUGGED_2_INDEX] < (uint8)__UNSIGNED_CHAR_MAX__)
				{
					SC_Subcycle_Execution_Counters[FILTER_PLUGGED_2_INDEX] ++;
				}

				(void)SubCycle__RequestStart(COMPARTMENT_DEFAULT, Common_Parameters_Ptr->Filter_Plugged_2_Subcycle_Priority);
				// Note that in this case a subcycle is going to be executed, during this subcycle the Stability Control
				// will be "suspended", it means that it will not read any input nor trigger any reaction.

				BIT_SET(Stability_Control_Flags, SC_FILTER_CLEANED);
				break;

			default: // Includes SC_REACTION_NONE
				// Nothing to do
				break;
		}

	}

    return(ACTIVE);
}

/**
 * The function used when the task terminate.
 * @param compartment the compartment that uses the task (unused)
 *
 * @return : None
 */
void StabilityControl__Terminate(uint8 compartment)
{
	Stability_Control_Status = INACTIVE;
	Fault__ConditionRemoved(FAULT_ID_VERY_LOW_WATER_OR_SUDS);
	ResetInstabilityEvent();
	Task_Suspended = FALSE;
	Speed_Reduction_Status = SC_NO_SPEED_REDUCTION;

	// Disable Airlock, Plugging and Heater Calcification algorithms
	AirlockMonitor__Stop();
	PluggingMonitor__Stop();
	HeaterCalcificationDetectionMonitor__Stop();

	// Reset subcycle executions counters
	memset(&SC_Subcycle_Execution_Counters[0], 0, sizeof(SC_Subcycle_Execution_Counters));
}


/**
 * It is called after a reset. It initializes some Stability Control variables even when the task is not active.
 *
 * @return  None
 */
void StabilityControl__VariablesInitialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
		{
			Stability_Control_Status = INACTIVE;
			memset(&SC_Subcycle_Execution_Counters[0], 0, sizeof(SC_Subcycle_Execution_Counters));

			Speed_Reduction_Status = SC_NO_SPEED_REDUCTION;

			Stability_Control_Flags = 0;
		}
	}
}



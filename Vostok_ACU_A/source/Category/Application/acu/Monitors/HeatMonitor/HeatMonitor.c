//! The name of this module for use by the Log module.
#define MODULE_NAME MODULE_HEAT_MONITOR

//! Displacement value that indicates that the feature is not used.
#define UNUSED_FEATURE 0xFFU

//! The number of times that the HeatMonitor__Execution function will be called every second while running.
#define HEAT_MONITOR_CALLS_PER_SECOND 10U

//! A list of log message IDs that are generated by this module.
typedef enum MODULE_HEAT_MONITOR_LOG_MESSAGE_ID_ENUM
{
	ERROR_LOADING_PARAMETERS       = 1,
} MODULE_HEAT_MONITOR_LOG_MESSAGE_ID_TYPE;

typedef struct HEAT_MONITOR_DATA_STRUCT
{
	//! Down counter for the activation delay
	uint16 Activation_Delay_Counter;
	//! Down counter for WS_WATER state
	uint16 Stable_Water_Ctr;
	//! Time VWI_Status has to be continuously WS_WATER
	uint16 Stable_Water_Time;
	//! Minimum RPM the motor must be running at to turn off the pump Integrated heater.
	uint16 Heater_Turn_Off_Speed;
	//! Minimum RPM the motor must be running at to turn on the pump Integrated heater.
	uint16 Heater_Turn_On_Speed;
	//! Displacement of the deactivation condition in the Condition Table Data pointer.
	uint8 Condition_Index;
	//! Heater Type (Calrod/Integrated)
	uint8 Heater_Type;
	//! Delay time for heater to turn back on. Units == seconds.
	uint8 Heater_Activation_Delay;
	//! The state requested by the Interpreter for the heater in the previous iteration.
	uint8 Previous_Requested_State;
	//! True if the heater was being forced off in the previous iteration due to the VWI State
	BOOL_TYPE VWI_Was_Forcing_Heater_Off;
	//! True if the heater was being forced off in the previous iteration due to the wash Pump state
	BOOL_TYPE Wash_Pump_Was_Forcing_Heater_Off;
} HEAT_MONITOR_DATA_TYPE;

//! Data structure for the monitor's management
static HEAT_MONITOR_DATA_TYPE Heat_Monitor_Data;

//! TRUE if the Washing Heater is requested on by the cycle (processes, ...) and the deactivation condition in FALSE.
static BOOL_TYPE Heater_Requested;
//! TRUE if Washing Heater is allowed to be On by everything except VWI value (activation delay, door status, drain pump status, ...).
static BOOL_TYPE Heater_Allowed;
//! TRUE if Washing Heater is allowed to be on by VWI value.
static BOOL_TYPE Heater_VWI_Allowed;
// Pointer to the status of heater load Invert_Pin_Sequence for washing resistor.
static BOOL_TYPE *Inverted_Sequence_Status_Ptr;


//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------
static BOOL_TYPE IsVWIRequestingHeaterOff(void);
static BOOL_TYPE IsWashPumpRequestingHeaterOff(void);


/**
 * @brief
 */
static void FillWater(FSM_CUSTOMIZATION_FUNCTION_ARGUMENT)
{
	switch (Fill__ClassAFillCore(&Fill_Data, &Fill_NV_Data, FILL_OPTION_CLASS_A_FILL))
	{
		case FILL_CORE_NO_WATER_DETECTED:
			DISPATCH_FSM_EVENT(FSM_EVENT_NOWATERDETECTED);
			break;

		case FILL_CORE_WATER_CHECK_NEEDED:
			DISPATCH_FSM_EVENT(FSM_EVENT_WATERCHECKNEEDED);
			break;

		case FILL_CORE_WATER_FILL_OVER:

			switch (AfterFillCheck())
			{
				case AFTER_FILL_WATER_CHECK_NEEDED:
					DISPATCH_FSM_EVENT(FSM_EVENT_AFTERFILLWATERCHECKNEEDED);
					break;

				default:
					DISPATCH_FSM_EVENT(FSM_EVENT_FILLCOMPLETED);
					break;
			}
			break;

		default:	// Includes FILL_CORE_NO_STATE_CHANGE
			break;
	}
}


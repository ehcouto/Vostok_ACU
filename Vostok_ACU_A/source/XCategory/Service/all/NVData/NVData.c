/**
 * @brief   Force the appliance segment of data to be corrupt. The next time the system resets the appliance segment
 *          data will not be restored. This does not affect any other segment.
 *
 * @return PASS_FAIL_TYPE
 *  @retval PASS(1) - Successfully able to Set appliance Segment corrupt
 *  @retval FAIL(0) - Failed to Set appliance Segment corrupt
 */
PASS_FAIL_TYPE NVData__SetApplianceSegmentCorrupt(void)
{
    PASS_FAIL_TYPE status = FAIL;

    // Prevent StoreSegments() call if NVData__Initialize has not been called
    // e.g. firmware with no setting file valid.
    if (NVData_Header.Signature == NVDATA_HEADER_SIGNATURE)
    {
        // If not in Normal Mode NVData could not be initialized
        if (Mode__GetMode() != MODE_NORMAL)
        {
            NVDATA__INITIALIZEDEVICE();
        }
        Force_Appliance_Segment_Corruption = APPLIANCE_SEGMENT_CORRUPT;
        status = NVData__Store();
    }

    return (status);
}


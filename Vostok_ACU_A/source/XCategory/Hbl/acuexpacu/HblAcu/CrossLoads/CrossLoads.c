/**
 * @brief This method loads the Hbl Configuration for Cross Loads from the Setting File.
 */
void CrossLoads__Initialize(void)
{
#if (HBL_CROSS_LOADS_FEATURE == ENABLED)
    uint8 index;
    uint8 *ptr;
    SETTINGFILE_LOADER_TYPE cross_load_table;
    HBL_CROSS_LOAD_TYPE *cfg_ptr;

    memset(Crossload_Data , 0 , sizeof(Crossload_Data));

    SettingFile__BasicLoader(SF_PTR_ACU_IO_CONFIG, SF_DISPL_CROSS_LOADS, &cross_load_table);

    index = (cross_load_table.Length / 2);

    if (index > HBL_XLOAD_NUM_CROSSLOAD)
    {
        LOG_ADD_EXCEPTION(NUM_OF_SETTING_CROSSLOADS_EXCEEDEDS_MAX_NUM, Crossloads_Num);
        Mode__SetModeExtended(MODE_NORMAL, SUBMODE_NORMAL_ERROR);
        index = HBL_XLOAD_NUM_CROSSLOAD;
    }
    SRData__UpdateByte(&Crossloads_Num, &N_Crossloads_Num, index);

    for (index = 0 ; index < HBL_XLOAD_NUM_CROSSLOAD ; index++)
    {
        SETTINGFILE_LOADER_TYPE param_table;
        cfg_ptr = &Crossload_Cfg[index];
        ptr = &cross_load_table.Data[2*index];

        cfg_ptr->Crossload_Position = HBL_LOAD_INVALID;
        cfg_ptr->Parameter_Ptr = NULL;
        cfg_ptr->Number_Of_Couples = 0;

        if (index < Crossloads_Num)
        {
            //Getting the CROSS LOAD parameters from the Settings File.
            if ((ptr[0] < Hbl__GetNumLoads()) &&
                (SettingFile__BasicLoader(SF_PTR_ACU_IO_DEVICE, ptr[1], &param_table) == PASS))
            {
                CROSSLOAD_PARAMETERS_TYPE *new_param_ptr = ((CROSSLOAD_PARAMETERS_TYPE*)(param_table.Data)); //lint !e927 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4];
                if (CrossloadHandlers[new_param_ptr->Type] != (CROSSLOAD_STATE_MACHINE_FUNCTION_POINTER_TYPE) NULL)
                {
                    cfg_ptr->Crossload_Position = ptr[0];
                    cfg_ptr->Parameter_Ptr = new_param_ptr;
                    cfg_ptr->Number_Of_Couples = (param_table.Length - 2) / 2;
                }
            }
            if (cfg_ptr->Crossload_Position == HBL_LOAD_INVALID)
            {
                LOG_ADD_EXCEPTION(INVALID_CROSSLOAD_HANDLER, index);
                Mode__SetModeExtended(MODE_NORMAL, SUBMODE_NORMAL_ERROR);
            }
        }

        Crossload_Crc[index] = Crc8(CRC8_INITIAL_SEED,(uint8 *)&Crossload_Cfg[index],sizeof(HBL_CROSS_LOAD_TYPE)); //lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
    }
#endif
}



/**
 *  @brief		Returns current voltage range Voltage_Range_Status
 *
 */
VOLTAGERANGE_STATE_TYPE VoltageRangeMonitor__GetStatus(void)
{
	return(Voltage_Range_Status);
}

//---------------------------------------------------------------------------------------------------------------------
/**
 *  @brief		Returns if current voltage is within good range or not
 *
 */

BOOL_TYPE VoltageRangeMonitor__IsGoodRange(void)
{
	return((BOOL_TYPE) (Voltage_Range_Status == VOLTAGERANGE_STATE_GOODRANGE));
}


/**
 * Find the setting file fault parameters for the specified fault.
 * @param fault_id = The fault ID used to search the setting file for fault parameters.
 * @param priority = Reference priority to set with the priority of the specified fault.
 * @return A pointer to the setting file data structure with the fault parameters.
 *          If there is no setting file data structure for the specified fault_id, returns NULL.
 */
static FAULT_CONFIG_TYPE* FindConfigParameters(FAULT_ID_TYPE fault_id, uint8* priority)
{
    FAULT_CONFIG_TYPE* config = NULL;
    if (Fault.List == NULL)
    {
        uint8 p = 0;
        while (config == NULL)
        {
            FAULT_CONFIG_TYPE* check = GetConfigParameters(p);
            if (check == NULL)
            {
                break;                                  // fault_id not found -- Return NULL
            }
            if (check->Id == fault_id)
            {
                *priority = p;
                config = check;
            }
            else
            {
                p++;
            }
        }
    }
    else
    {
        for (uint8 p = 0; p < Fault.List->Number_Of_Faults; p++)
        {
            if (Fault.List->Fault_Ids[p] == fault_id)
            {
                *priority = p;
                config = GetConfigParameters(p);
                break;                              // Break out of the for() loop.
            }
        }
    }

    return config;
}



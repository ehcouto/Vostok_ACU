/**
 * @file
 * @brief       Manages delays between detection of abnormal conditions and generation of pre-faults and faults.
 *
 * @details
 *
 * The Fault module runs multiple parallel state machines. One for each actively detected error
 * condition. If there are more actively detected error conditions than there are Fault state
 * machines, then the available state machines only manage the highest priority error conditions.
 *
    @startuml{fault_sm.jpg}
        title Fault Observer State Machine
        ' use state = Fault_Nv.Observers[Fault.Instance].State

        [*] --> Alarm

        ' Alarm State:
        ' System has detected an error condition.
        ' No reaction has been taken.
        ' System is debouncing the error condition to determine if any pre-fault reaction is necessary.
        Alarm : entry / SetupAlarmDebounce()\n\t CheckForNoDebounce()
        Alarm : do / DebounceError()
        Alarm --> Pre_Fault : Debounce(FAULT_DEBOUNCE_EVENT_NEXT)\n/ SetupPreFaultDebounce()
        Alarm --> Fault : Debounce(FAULT_DEBOUNCE_EVENT_NEXT)\n[DirectToFault()]
        Alarm --> [*] : Debounce(FAULT_DEBOUNCE_EVENT_CANCEL)

        ' Pre_Fault State:
        ' System has debounced an error condition and the pre-fault reactions must be taken.
        ' System is debouncing the error condition further to determine if any fault reaction is necessary.
        Pre_Fault : entry / CheckForNoDebounce()\n\t TriggerPreFaultReaction()
        Pre_Fault : do / DebounceError()
        Pre_Fault --> Pre_Fault : Debounce(FAULT_DEBOUNCE_EVENT_REPEAT)
        Pre_Fault --> Fault : Debounce(FAULT_DEBOUNCE_EVENT_NEXT)
        Pre_Fault --> [*] : Debounce(FAULT_DEBOUNCE_EVENT_CANCEL)

        ' Fault State:
        ' System has debounced an error condition and the fault reactions must be taken.
        ' The Fault module is remembering a fault (if necessary) across a cold reset.
        ' The Fault module is preventing the Supervisor from exiting the Fault state (if necessary).
        ' The Fault module is blocking the System's ability to detect the same fault again.
        Fault : entry / TriggerFaultReaction()
        Fault : do / DebounceErrorRemoval()
        Fault --> [*] : Debounce(FAULT_DEBOUNCE_EVENT_CANCEL)

        "Supervisor Interactions": 1. If the Supervisor is in the Supervisor Fault state, then\n\t the fault observer will not leave the Fault state.\n2. When the Supervisor enters the Supervisor Programming\n\t state, all fault observers in the Fault state (for that\n\t compartment) are terminated.
    @enduml
 *
 * The external events in the state machine are generated by a Debounce Method. The Debounce
 * Methods are managed by external modules. There are two primary Debounce Methods:
 *      - FdmCounter counts error detection events to determine when to raise events.
 *      - FdmTimer counts time that an error condition is present or absent to determine when to
 *                  raise events.
 *
 *
 * The Fault module's setting file data is arranged such that the highest priority faults are
 * stored first and faults that are disabled (or deprecated) are not stored at all. The start of
 * each fault's setting file data starts with the following structure:
 *
 *      Start of Data For All Enabled Faults:
 *              |  7 |  6 |  5 |  4 |  3 |  2 |  1 |  0 |  7 |  6 |  5 |  4 |  3 |  2 |  1 |  0 |
 *      +0000h  |                Fault_ID               |      Debounce_Method_Displacement     |
 *      +0001h  |              Compartment              |      Sub_Code     |        Code       |
 *      +0002h  | ACR| CCR| PC | D2F|  Engineering_Code |               Sub-Cycle               |
 *      =0003h  |                 Phase                 |         <Fault-Specific Data>         |
 *
 *          Fault_ID = The ID of the category-specific fault. Refer to Fault_prm.h for specific
 *                      codes.
 *          Debounce_Method_Displacement = The displacement that references the debounce method
 *                      used to transition from Alarm to pre-fault and from pre-fault to fault.
 *              0xFF = Use the default debounce method. Uses single detection event transitions.
 *          Code = A value that describes a group of faults.
 *          Sub_Code = A value that differentiates multiple faults with the same Code. The Code and
 *                      Sub_Code combination identifies a unique serviceable fault.
 *          Engineering_Code = A value that differentiates multiple faults with the same Code and
 *                      Sub_Code. The Code, Sub_Code, and Engineering_Code combination identifies a
 *                      unique fault that may be of interest to Engineering.
 *          D2F (Direct_To_Fault) = Determines whether the Fault ID will ever generate a pre-fault.
 *              0 = Generate a pre-fault when the error condition is debounced.
 *              1 = Generate a fault when the error condition is debounced.
 *          PC (Pause_Cycle) = Determines whether the pre-fault will pause the main cycle.
 *              0 = Do not pause the main cycle when the pre-fault is triggered.
 *              1 = Pause the main cycle when the pre-fault is triggered.
 *          ACR (Allow Clear without Removing the fault)
 *              0 = The error condition needs to be removed before the Supervisor can exit the
 *                  Supervisor Fault state.
 *              1 = The error condition does not need to be removed before the Supervisor can exit
 *                  the Supervisor Fault state.
 *          CCR (Clear the fault after a Cold Reset)
 *              0 = The fault management is not affected by a cold reset.
 *              1 = The fault management is reset if a cold reset occurs.
 *          Phase = The Supervisor runs this phase (macro) of the Fault Cycle when the fault occurs.
 *          Sub_Cycle = The specified sub-cycle is requested when the pre-fault occurs.
 *              0xFF = Do not request any sub-cycle when the pre-fault occurs.
 *
 * Each fault can have additional parameters that are stored after the basic structure.
 *
 *
 * Each fault has a debounce method whose behavior is described by the Debounce_Method_Displacement.
 * The debounce method parameters are described in the following structure:
 *
 *      Debounce_Method_Displacement Data:
 *              |  7 |  6 |  5 |  4 |  3 |  2 |  1 |  0 |  7 |  6 |  5 |  4 |  3 |  2 |  1 |  0 |
 *      +0000h  |    0    |   PDM   |  0 |  Detect PPS  |         Detect Pre_Fault_Delay        |
 *      +0001h  |    0    |   FDM   |  0 |  Detect FPS  |           Detect Fault_Delay          |
 *      +0002h  |            0           |  Remove PPS  |         Remove Pre_Fault_Delay        | (Optional)
 *      +0003h  |            0           |  Remove FPS  |           Remove Fault_Delay          | (Optional)
 *
 *          PDM (Pre_Fault_Debounce_Method) = The method used to debounce an error condition into
 *                      a pre-fault condition.
 *              0 = Use time-based debouncing of the error condition
 *              1 = Use event-based debouncing of the error condition
 *          PPS (Pre_Fault_Prescalar) = The scale used to interpret the Pre_Fault_Delay field.
 *              0 = 1 count in the Delay field corresponds to 100 milliseconds.
 *              1 = 1 count in the Delay field corresponds to 1 second.
 *              2 = 1 count in the Delay field corresponds to 10 seconds.
 *              3 = 1 count in the Delay field corresponds to 1 minute.
 *              4 = 1 count in the Delay field corresponds to 10 minutes.
 *          Pre_Fault_Delay = A time or a count used to debounce the error condition into a pre-
 *                      fault condition.
 *          FDM (Fault_Debounce_Method) = The method used to debounce a pre-fault condition into a
 *                      fault condition.
 *              0 = Use time-based debouncing of the pre-fault condition
 *              1 = Use event-based debouncing of the pre-fault condition
 *          FPS (Fault_Prescalar) = The scale used to interpret the Fault_Delay field.
 *              0 = 1 count in the Delay field corresponds to 100 milliseconds.
 *              1 = 1 count in the Delay field corresponds to 1 second.
 *              2 = 1 count in the Delay field corresponds to 10 seconds.
 *              3 = 1 count in the Delay field corresponds to 1 minute.
 *              4 = 1 count in the Delay field corresponds to 10 minutes.
 *          Fault_Delay = A time or a count used to debounce the pre-fault condition into a fault
 *                      condition.
 *
 *
 * @copyright  Copyright 2016-2018 Whirlpool Corporation.  All rights reserved - CONFIDENTIAL.
 */


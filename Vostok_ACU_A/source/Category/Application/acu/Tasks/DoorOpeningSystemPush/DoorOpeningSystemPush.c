void DoorOpeningSystemPush__Terminate(uint8 compartment)
{

}


/**
 * It sets up the Door Opening System task push data to start the DOS Push sequence.
 *
 * @param  time = the duration of the first step of the Push sequence (in 1/10 seconds)
 *
 * @return  None
 */
void DoorOpeningSystemPush__StartPush(uint8 time)
{
	DoorOpeningSystemMonitor__SetDoorOpenedByDOS(TRUE);
	DoorOpeningSystemMonitor__SetDOSPullState(DOS_PULL_REQUIRED);
	DoorOpeningSystemMonitor__SetDOSPushState(DOS_PUSH_PRE_ON_STEP);
	DOS_Push_Data.DOS_Push_Counter = (uint16)time + (uint8)1; // the +1 is to account for the first decrement inside DoorOpeningSystemPush__Task
}



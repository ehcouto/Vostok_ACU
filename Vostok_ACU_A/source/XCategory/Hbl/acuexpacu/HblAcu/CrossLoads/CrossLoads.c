/**
 * @brief - This function manages the Crossloads of type Transition Load
 *
 * @param CROSSLOAD_DATA_TYPE * , CROSSLOAD_PARAMETERS_TYPE *
 */
static void TransitionLoadHandler(HBL_CROSS_LOAD_TYPE *cfg_ptr, CROSSLOAD_DATA_TYPE * data_ptr, uint8 *hbl_prio_ptr)
{
    CROSSLOAD_PARAMETERS_TYPE *param_ptr = cfg_ptr->Parameter_Ptr;
    uint8 my_prio = GetLoadPriority(cfg_ptr->Crossload_Position, hbl_prio_ptr);
    uint8 *load_request_ptr;
    uint8 *load_process_ptr;
    uint8 load_index;
    uint8 position_index;
    uint8 load_request_len;
    uint8 load_prio;

    switch (data_ptr->Generic_State)
    {
        case CROSSLOAD_STEADY_STATE:
        {
            BOOL_TYPE do_switch = FALSE;
            //------- Load-On Transition Monitor and Relay Trigger Process ---------------
            if ((data_ptr->Requested == OFF) &&
            		((param_ptr->Off_Delay + param_ptr->On_Delay) > 0))
            {
                for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                {
                    load_index = param_ptr->Load_Position[position_index].On;
                    load_request_ptr = Hbl__GetRequestedLoad(load_index);
                    load_process_ptr = Hbl__GetProcessedLoad(load_index);
                    load_request_len = Hbl__GetLoadDataSize(load_index);
                    load_prio = GetLoadPriority(load_index, hbl_prio_ptr);

                    if ((load_request_ptr != NULL) &&
                        (load_prio >= my_prio) &&
                        (!IsLoadStatusOnTransition(load_index)) &&
                        (memcmp(load_request_ptr, load_process_ptr, load_request_len) != 0))
                    {
                        do_switch = TRUE;
                        if(param_ptr->On_Delay > 0)
                        {
                            Hbl__SetLoadRequestByIndex(load_index, (HBL_PRIORITY_TYPE)load_prio, load_process_ptr);
                        }
                    }
                }
            }

            if (do_switch != FALSE)
            {
                data_ptr->Timer = param_ptr->On_Delay + param_ptr->Off_Delay;
                data_ptr->Generic_State = CROSSLOAD_ON_TRANSITION;
                data_ptr->Processed = ON;
            }
            else
            {
                data_ptr->Processed = data_ptr->Requested;
            }
        }
        //----------------------------------------------------------------------------
        break;

        case CROSSLOAD_ON_TRANSITION:
        {
            BOOL_TYPE timer_wait = IsLoadStatusOnTransition(cfg_ptr->Crossload_Position);
            uint8 new_timer = data_ptr->Timer;

            if (data_ptr->Processed == OFF)
            {
                if(new_timer > param_ptr->Off_Delay)
                {
                    timer_wait = TRUE;
                }
                else
                {
                    new_timer = 0;
                }
            }
            else if ((data_ptr->Requested != OFF) && (my_prio > SRHBL_PRIORITY_MINIMUM))
            {
                new_timer = 0;
            }

            if (new_timer > 0)
            {
                new_timer--;

                for(position_index = 0; position_index < cfg_ptr->Number_Of_Couples; position_index++)
                {
                    load_index = param_ptr->Load_Position[position_index].On;
                    load_prio = GetLoadPriority(load_index, hbl_prio_ptr);
                    load_request_ptr = Hbl__GetRequestedLoad(load_index);
                    load_process_ptr = Hbl__GetProcessedLoad(load_index);
                    load_request_len = Hbl__GetLoadDataSize(load_index);
                    timer_wait |= IsLoadStatusOnTransition(load_index);
                    timer_wait |= IsCrossLoadTransition(load_index, FALSE);

                    if ((load_request_ptr != NULL) &&
                        (load_process_ptr != NULL) &&
                        (memcmp(load_request_ptr, load_process_ptr, load_request_len) != 0))
                    {
                        //------- Load-On Keep State Process ---------------
                        if (new_timer > param_ptr->Off_Delay)
                        {
                            Hbl__SetLoadRequestByIndex(load_index, (HBL_PRIORITY_TYPE)load_prio, load_process_ptr);
                        }
                        else
                        {
                            new_timer = param_ptr->Off_Delay;
                            timer_wait = FALSE;
                        }
                    }
                }
                if (timer_wait == FALSE)
                {
                    data_ptr->Timer = new_timer;
                }
            }

            if(new_timer == 0)
            {
                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                data_ptr->Processed = OFF;
            }
            else
            {
                data_ptr->Processed = ON;
            }
        }
        break;

        default:
            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            break;
    }
}

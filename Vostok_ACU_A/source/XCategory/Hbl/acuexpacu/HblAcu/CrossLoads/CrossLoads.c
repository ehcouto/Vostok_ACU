/**
 * @brief - This function manages the Crossloads of type Reverse Switch Load
 *
 * @param CROSSLOAD_DATA_TYPE * , CROSSLOAD_PARAMETERS_TYPE *
 */
static void ReverseLoadRelayHandler(HBL_CROSS_LOAD_TYPE *cfg_ptr, CROSSLOAD_DATA_TYPE * data_ptr, uint8 *hbl_prio_ptr)
{
    CROSSLOAD_PARAMETERS_TYPE *param_ptr = cfg_ptr->Parameter_Ptr;
    uint8 my_prio = GetLoadPriority(cfg_ptr->Crossload_Position, hbl_prio_ptr);
    uint8 position_index;
    uint8 load_check_index; // Load index to check if switch has to be executed
    uint8 load_keep_index;  // Load index to check if swict position should be kept
    uint8 load_check_prio;
    uint8 load_keep_prio;
    uint8 *load_request_ptr;
    uint8 load_request_len;
    BOOL_TYPE do_switch = FALSE;
    BOOL_TYPE keep_status = FALSE;

    for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
    {
        // Place check and keep positions according to Reverse Switch status
        if (data_ptr->Processed == OFF)
        {
            load_check_index = param_ptr->Load_Position[position_index].On;
            load_keep_index = param_ptr->Load_Position[position_index].Off;
        }
        else
        {
            load_check_index = param_ptr->Load_Position[position_index].Off;
            load_keep_index = param_ptr->Load_Position[position_index].On;
        }

        load_keep_prio = GetLoadPriority(load_keep_index, hbl_prio_ptr);
        load_check_prio = GetLoadPriority(load_check_index, hbl_prio_ptr);

        if (load_keep_prio >= my_prio)
        {
            // If keep position is ON: keep actual status
            load_request_ptr = Hbl__GetRequestedLoad(load_keep_index);
            if (load_request_ptr != NULL)
            {
                load_request_len = Hbl__GetLoadDataSize(load_keep_index);

                // Perform clear only if Switch load priorty is lower than load to keep
                // otherwise load status is kept as is
                if (memcmp(load_request_ptr, Hbl__GetFlushBuffer(), load_request_len) != 0)
                {
                    keep_status = TRUE;
                    do_switch = FALSE;
                    if(PASS == Hbl__SetLoadRequestByIndex(load_check_index,
                        (HBL_PRIORITY_TYPE)load_check_prio, (uint8 *)Hbl__GetFlushBuffer()))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                    {
                        ResetCrossLoadTransition(load_check_index);
                    }
                }
            }
        }

        if ((keep_status == FALSE) &&
            (load_check_prio >= my_prio))
        {
            load_request_ptr = Hbl__GetRequestedLoad(load_check_index);
            if (load_request_ptr != NULL)
            {
                load_request_len = Hbl__GetLoadDataSize(load_check_index);

                // Perform Reverse Switch load transition only if priorty is higher than maximum of other loads *
                if(memcmp(load_request_ptr, (uint8 *)Hbl__GetFlushBuffer(), load_request_len) != 0)
                {
                    do_switch = TRUE;
                }
            }
        }
    }

    switch (data_ptr->Generic_State)
    {
        case CROSSLOAD_STEADY_STATE:
        case CROSSLOAD_FORCED_STATE:
        {

            if (keep_status == FALSE)
            {
                // No keep status required: check if Switch Load request changed by
                // direct request or via other load
                if (do_switch == FALSE)
                {
                    if (data_ptr->Processed != OFF)
                    {
                        if ((data_ptr->Requested != OFF) && (my_prio > SRHBL_PRIORITY_MINIMUM))
                        {
                            data_ptr->Generic_State = CROSSLOAD_FORCED_STATE;
                        }
                        else if (data_ptr->Generic_State == CROSSLOAD_FORCED_STATE)
                        {
                            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                            data_ptr->Processed = OFF;
                        }
                        else
                        {
                            do_switch = TRUE;
                        }
                    }
                    else if (data_ptr->Requested != OFF)
                    {
                        data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                        data_ptr->Processed = ON;
                    }
                }

                if (do_switch != FALSE)
                {
                    if (data_ptr->Processed == OFF)
                    {
                        data_ptr->Timer = param_ptr->On_Delay;
                        data_ptr->Generic_State = CROSSLOAD_ON_TRANSITION;
                        if(param_ptr->On_Delay == 0)
                        {
                            data_ptr->Processed = ON;
                        }
                    }
                    else
                    {
                        data_ptr->Processed = OFF;
                        data_ptr->Timer = param_ptr->Off_Delay;
                        if(param_ptr->Off_Delay > 0)
                        {
                            data_ptr->Generic_State = CROSSLOAD_OFF_TRANSITION;
                            for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                            {
                                load_check_index = param_ptr->Load_Position[position_index].On;
                                load_check_prio = GetLoadPriority(load_check_index, hbl_prio_ptr);
                                load_request_ptr = Hbl__GetProcessedLoad(load_check_index);
                                if (load_request_ptr != NULL)
                                {
                                    load_request_len = Hbl__GetLoadDataSize(load_check_index);
                                    if (PASS == Hbl__SetLoadRequestByIndex(load_check_index,
                                       (HBL_PRIORITY_TYPE)load_check_prio, (uint8 *)load_request_ptr))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                                    {
                                        ResetCrossLoadTransition(load_check_index);
                                    }
                                }
                            }
                        }
                        else
                        {
                            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                        }
                    }
                }
            }
            else
            {
                data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            }
            break;
        }

        case CROSSLOAD_OFF_TRANSITION:
        case CROSSLOAD_ON_TRANSITION:
        {
            uint8 new_processed = OFF;

            if (data_ptr->Timer > 0)
            {
                data_ptr->Timer--;
            }

            //----------- OFF Transition: keep ON load requests and clear OFF load requests ------------
            if (data_ptr->Generic_State == CROSSLOAD_OFF_TRANSITION)
            {
                // Back to Steady state ON if one of On loads are ON:
                if( ((data_ptr->Processed == OFF) && do_switch) ||
                    ((data_ptr->Processed != OFF) && keep_status))
                {
                    data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                    data_ptr->Timer = 0;
                    new_processed = ON;
                }
                else if(data_ptr->Processed != OFF)
                {
                    data_ptr->Timer++;
                }

                if(data_ptr->Timer > 0)
                {
                    for(position_index = 0 ; position_index < cfg_ptr->Number_Of_Couples ; position_index++)
                    {
                        // Keep previous state of On load on previous CrossLoad state
                        load_keep_index = param_ptr->Load_Position[position_index].On;
                        load_keep_prio = GetLoadPriority(load_keep_index, hbl_prio_ptr);
                        load_request_ptr = Hbl__GetProcessedLoad(load_keep_index);
                        if ((load_request_ptr != NULL) &&
                            (PASS == Hbl__SetLoadRequestByIndex(load_keep_index, (HBL_PRIORITY_TYPE)load_keep_prio,
                            (uint8 *)load_request_ptr)))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                        {
                            ResetCrossLoadTransition(load_keep_index);
                        }

                        // Clear state of Off load on next CrossLoad state
                        load_check_index = param_ptr->Load_Position[position_index].Off;
                        load_check_prio = GetLoadPriority(load_check_index, hbl_prio_ptr);
                        if((PASS == Hbl__SetLoadRequestByIndex(load_check_index, (HBL_PRIORITY_TYPE)load_check_prio,
                            (uint8 *)Hbl__GetFlushBuffer())))   //lint !e926 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
                        {
                            ResetCrossLoadTransition(load_check_index);
                        }
                    }
                }
                else
                {
                    data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                }
            }
            else
            {
                if(data_ptr->Timer == 0)
                {
                    new_processed = ON;
                }

                if(do_switch == FALSE)
                {
                    data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
                    data_ptr->Timer = 0;
                }
            }

            data_ptr->Processed = new_processed;
            break;
        }

        default:
            data_ptr->Generic_State = CROSSLOAD_STEADY_STATE;
            break;
    }
}

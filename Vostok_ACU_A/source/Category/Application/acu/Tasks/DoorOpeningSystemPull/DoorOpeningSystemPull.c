/**
 *  @file		DoorOpeningSystemPull.c
 *
 *  @brief		A task that triggers the start of the sequence to pull in the DOS pin
 *
 *  @details    Door Opening System (briefly DOS) is a device used to assist with the drying by opening the door of the dishwasher
 *  			releasing the moisture out of the tub at selected moment given by the cycle design.
 *  			Door Opening System Pull Task shall be activated in the point of the cycle when the DOS pin has to be retracted.
 *  			Once started, the Pull sequence is managed by the Door Open System Monitor, so that it is completed even if the task
 *  			is terminated. Moreover, the Pull sequence can be started also outside the DOS Pull task.
 *  			No Deactivate Task call is needed, the task automatically deactivate itself when it is completed.
 *
 */


/**
 *  @brief      Returns the number of virtual pins defined in VIRTUALPIN_TABLE.
 *
 *  @return     the number of virtual pins defined in VIRTUALPIN_TABLE
 */
uint8 VirtualPin__GetNumOf(void)
{
    return (sizeof(VIRTUALPIN_TABLE) / sizeof(VIRTUALPIN_TYPE));
}



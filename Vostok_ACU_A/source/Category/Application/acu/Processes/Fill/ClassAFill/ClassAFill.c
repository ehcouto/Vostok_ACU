static PASS_FAIL_TYPE ReadProcessParameters(PROCESS_CONTROL_TYPE* control)
{
	PASS_FAIL_TYPE result = FAIL;
	SETTINGFILE_LOADER_TYPE loader;

	// Get the Fill process parameters from the setting file.
	if (SettingFile__MSettingBasicLoader(control->Setting_File_Index,
										 SF_PTR_ACU_PROCESS_FILL,
										 control->Data_Field_Index,
										 &loader) == FAIL)
	{
		LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, control->Data_Field_Index);
	}
	else
	{
		Fill_Data.Class_A_Fill_Process_Parameters = (CLASS_A_FILL_PROCESS_PARAMETER_TYPE *)(void *)loader.Data;

		if ( FAIL == SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS,
											  DISPL_CLASS_A_FILL,
											  &loader) )
		{
			LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)SF_PTR_ACU_ALGORITHM_PARAMETERS);
		}
		else
		{
			Fill_Data.Class_A_Fill_Parameters = (CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data;

			result = PASS;
		}
	}

	return result;
}


static AFTER_FILL_CHECK_RET_TYPE AfterFillCheck(void)
{
	AFTER_FILL_CHECK_RET_TYPE ret_value = AFTER_FILL_COMPLETE_FILL;

	if ((Liters_In_Tub_Before_Fill < ((uint16)Fill_Data.Class_A_Fill_Parameters->Liters_For_WI_Empty_Max * (uint16)5)) &&
		(Liters_In_Tub > ((uint16)Fill_Data.Class_A_Fill_Parameters->Liters_For_WI_Full_Min * (uint16)5)))
	{
		if ((Fill_Data.Class_A_Fill_Parameters->Fill_Check_Water_Subcycle_Priority != 0U) && (Interpreter__GetStatus().Sub_Cycle[COMPARTMENT_DEFAULT] == 0U))
		{
			ret_value = AFTER_FILL_WATER_CHECK_NEEDED;
		}
		else
		{
			// Fill Check subcycle is not present in setting file, or Fill is being executed inside a subcycle:
			// it is not possible to start Fill Check Subcycle, process is completed without doing anything else.
		}
	}
	else
	{
		if (Liters_In_Water_Tank < ((uint16)Fill_Data.Class_A_Fill_Parameters->Liters_For_Tank_Empty_Max * (uint16)5))
		{
			sint32 sum = (sint32)Liters_Unchecked + (sint32)Liters_In_Tub - (sint32)Liters_In_Tub_Before_Fill;

			if (sum < 0)
			{
				sum = 0;
			}
			else if (sum > __UNSIGNED_SHORT_MAX__)
			{
				sum = __UNSIGNED_SHORT_MAX__;
			}
			else
			{
				// Do nothing, sum fits in uint16
			}
			Liters_Unchecked = (uint16)sum;

			if (Liters_Unchecked >= ((uint16)Fill_Data.Class_A_Fill_Parameters->Liters_Unchecked_Max * (uint16)5))
			{
				Tank_State_Fill = TANK_STATE_UNKNOWN;
				Tank_State_Drain = TANK_STATE_UNKNOWN;
			}
		}
	}

	return ret_value;
}

static void NoWaterFoundInAfterFillChecks(void)
{
	sint32 sum = (sint32)Liters_In_Water_Tank + (sint32)Liters_In_Tub - (sint32)Liters_In_Tub_Before_Fill;

	if (sum < 0)
	{
		sum = 0;
	}
	else if (sum > __UNSIGNED_SHORT_MAX__)
	{
		sum = __UNSIGNED_SHORT_MAX__;
	}
	else
	{
		// Do nothing, sum fits in uint16
	}

	if (Liters_In_Water_Tank < ((uint16)Fill_Data.Class_A_Fill_Parameters->Liters_For_Tank_Empty_Max * (uint16)5))
	{
		if (sum < ((sint32)Fill_Data.Class_A_Fill_Parameters->Liters_In_Tank_Max * 5))
		{
			Liters_In_Water_Tank = (uint16)sum;
		}
		else
		{
			Liters_In_Water_Tank = (uint16)Fill_Data.Class_A_Fill_Parameters->Liters_In_Tank_Max * (uint16)5;
		}

		Liters_In_Tub_Before_Fill = 0;
		Tank_State_Fill = TANK_STATE_UNKNOWN;
		Tank_State_Drain = TANK_STATE_FAIL;

		Fault__ConditionDetected(FAULT_ID_WATER_TANK_VALVE_NOT_FUNCTIONING);

		Fill__SetBitmapBit(SENSED_DRAIN_REQUESTED_BY_FILL);
	}
	else
	{
		if (sum < ((sint32)Fill_Data.Class_A_Fill_Parameters->Liters_In_Tank_Max * 5))
		{
			Liters_In_Water_Tank = (uint16)sum;
		}
		else
		{
			Liters_In_Water_Tank = (uint16)Fill_Data.Class_A_Fill_Parameters->Liters_In_Tank_Max * (uint16)5;
		}

		Tank_State_Fill = TANK_STATE_FAIL;
		Tank_State_Drain = TANK_STATE_FAIL;

		Fault__ConditionDetected(FAULT_ID_WATER_TANK_VALVE_STUCK_OPEN);
	}
}


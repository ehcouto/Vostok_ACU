/**
 *  @brief      It Initializes the module VwiMonitor and its variables
 *
 */
void VwiMonitor__Initialize(void)
{
#if(VWI_CLIENT_FEATURE == ENABLED)

	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		VwiMonitor_Callback_Active = FALSE;
		ReadSfData();
	}

#endif // (VWI_CLIENT_FEATURE == ENABLED)
}

/**
 * 	@brief 		Main handler for VwiMonitor
 */
void VwiMonitor__Execute(void)
{
#if(VWI_CLIENT_FEATURE == ENABLED)
	uint8 label = (Selector__GetCycleState(0, 0, (Interpreter__GetCurrentStatement(0)).Cycle));

	//In case of cycle is running or we have some fault and we will need VWI information
	if((label == CYCLE_STATE_RUN) || (label == CYCLE_STATE_FAULT))
	{
		if(VwiMonitor_Callback_Active == FALSE)
		{
			API221MotionCtrl__RegisterForEvents(API221HandleMotorEvent);
			VwiMonitor_Callback_Active = TRUE;
		}
	}
	else
	{
		//Unsubscribe Motor state and also turn off remote data
		if(VwiMonitor_Callback_Active == TRUE)
		{
			API221MotionCtrl__UnregisterForEvents(API221HandleMotorEvent);
			VwiMonitor_Callback_Active = FALSE;

			Vwi__RemoteChannelEnable(MOTOR_0, MCISENSORSDW_AI_VWI_CHANNEL_0, FALSE);
			Vwi__RemoteChannelEnable(MOTOR_0, MCISENSORSDW_AI_VWI_CHANNEL_1, FALSE);
			Vwi__RemoteChannelEnable(MOTOR_1, MCISENSORSDW_AI_VWI_CHANNEL_0, FALSE);
			Vwi__RemoteChannelEnable(MOTOR_1, MCISENSORSDW_AI_VWI_CHANNEL_1, FALSE);
		}
	}
#endif // (VWI_CLIENT_FEATURE == ENABLED)
}



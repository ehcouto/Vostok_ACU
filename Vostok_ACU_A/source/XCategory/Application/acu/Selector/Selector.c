/**
 *  @brief      Get Supervisor state cycle (e.g. PROGRAMMING, DELAY, END, PAUSE, FAULT).
 *
 *  @param      uint8 setting_file - setting file to search
 *  @param      uint8 compartment - identifier appliance compartment (0 based, 0 for single compartment appliances)
 *  @param      uint8 state_cycle - state cycle to search for
 *
 *  @retval     0xFF Invalid - state cycle not found
 *  @retval     0-128 Valid state cycle found
 *
*/
uint8 Selector__GetStateCycle(uint8 sf_index, uint8 compartment, uint8 state_cycle)
{

    uint8 cycle = 0xFF;
    uint8 number_of_cycles = Selector__GetNumberOfCycles(sf_index, compartment);

    uint8 const *cycle_header_ptr;

    for (uint8 index = 0; ((index < number_of_cycles) && (cycle == 0xFF)); index++)
    {
        if (GetProgramHeadingPointer(sf_index, compartment, index,  &cycle_header_ptr) == PASS)
        {
            if ((cycle_header_ptr[CYCLE_STATE_OFFSET] & CYCLE_STATE_MASK) == state_cycle)
            {
                cycle = index;
            }
        }
    }

    if (cycle == 0xFF)
    {
        LOG_ADD_EXCEPTION(CYCLE_NOT_FOUND, state_cycle);
    }

    return (cycle);
}


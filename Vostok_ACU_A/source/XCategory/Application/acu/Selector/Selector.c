/**
 *  @brief      Get the cycle indicated by cycle_name.
 *
 *  @param      uint8  setting_file - setting file index for where to find program header
 *  @param      uint8  compartment  - identifier appliance compartment (0 based, 0 for single compartment appliances)
 *  @param      CYCLE_NAME_TYPE cycle_name   - cycle name enumeration id to search for
 *
 *  @retval     0xFF  Invalid - cycle not found
 *  @retval     0-128 Valid   - cycle found
 *
*/
uint8 Selector__GetCycleByName(uint8 sf_index, uint8 compartment, CYCLE_NAME_TYPE cycle_name)
{
    uint8 cycle = 0xFF;
    uint8 number_of_cycles = Selector__GetNumberOfCycles(sf_index, compartment);

    for (uint8 index = 0; ((index < number_of_cycles) && (cycle == 0xFF)); index++)
    {
        if (Selector__GetCycleNameId(sf_index, compartment, index) == cycle_name)
        {
            cycle = index;
        }
    }

    if (cycle == 0xFF)
    {
        LOG_ADD_EXCEPTION(CYCLE_NOT_FOUND, cycle_name);
    }

    return (cycle);
}



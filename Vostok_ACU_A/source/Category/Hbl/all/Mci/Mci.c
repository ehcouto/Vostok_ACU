/**
 * Function for loading parameters from setting file
 *
 */
void Mci__ParameterLoading(void)
{
    MCL_PARAMS_TYPE* mcl_params;
#ifndef MCI_INTERNAL_PARAMETERS
    SETTINGFILE_LOADER_TYPE params;

    //Initializing default variables
    Mci_Params_Loaded = FALSE;

    //Load parameters
    if (SettingFile__BasicLoader(SF_PTR_MCU_CLASS_A_MCI, SF_DISPL_MOTOR_PARAMS_GLOBAL_WASH_SYSTEM, &params) == PASS)
    {
        mcl_params = (MCL_PARAMS_TYPE*)(params.Data); //copying data into stack.
        if(Mcl__SetParams(mcl_params) == TRUE)
        {
            //If parameters properly loaded into Motor Control Module...
            Mci_Params_Loaded = TRUE;
        }
    }
    else
    {
    	LOG_ADD_EXCEPTION(MCI_EXCEPTION_PARAMETERS_NOT_LOADED, (uint16)Mci_Params_Loaded);
    }

#else
    //copying data into stack...
    mcl_params = (MCL_PARAMS_TYPE*)Mci_GWS_Params_SF; //lint !e927 Lint: 927 cast from pointer to pointer [MISRA 2004 Rule 11.4]
    if(Mcl__SetParams(mcl_params) == TRUE)
    {
        //If parameters properly loaded into Motor Control Module...
        Mci_Params_Loaded = TRUE;
    }
#endif
}

/**
 *  @brief      Get the motor load index for the external load.
 *
 *  @param      HBL_LOAD_TYPE *load - Pointer to ice dispenser door load instance.
 *
 *  @return     uint8 motor_load_index - value from setting file for load position in motor ACU loads buffer.
 */
static uint8 GetMotorLoadIndex(HBL_LOAD_TYPE *load)
{
    PASS_FAIL_TYPE load_success;
    uint8 motor_load_index;
    SETTINGFILE_LOADER_TYPE sf_loader_data;

    motor_load_index = 0;
    load_success = SettingFile__BasicLoader(SF_PTR_ACU_IO_DEVICE,
                                            load->Pilot_Type_Params_Offset,
                                            &sf_loader_data);
    if (load_success == PASS)
    {
        if (sf_loader_data.Length > 0)
        {
            motor_load_index = sf_loader_data.Data[0];
        }
    }

    return (motor_load_index);
}

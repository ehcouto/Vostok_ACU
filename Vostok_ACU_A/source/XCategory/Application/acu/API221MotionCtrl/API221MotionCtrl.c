/**
 * Publish event if any failure flags have been removed through the motor interface.
 * @param server_motor = The server-side motor index which is associated with the failure flags.
 */
static void CheckForRemovedFailureFlags(uint8 server_motor)
{
    uint32 next_failure_flags = API221_READ_FAILURE_FLAGS(server_motor);
    uint32 prev_failure_flags = Api221.Failure_Flags[server_motor];

    // Save the new failure flags.
    Api221.Failure_Flags[server_motor] = next_failure_flags;

    // Check if a failure has been removed (any bit changed from 1 to 0)
    if ((prev_failure_flags & (prev_failure_flags ^ next_failure_flags)) != 0)
    {
        API221MotionCtrl__PostEvent(server_motor, API221_EVENT_REMOVED_ERROR);
    }
}


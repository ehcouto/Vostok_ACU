//-------------------------------------- PRIVATE (Variables, Constants & Defines) -------------------------------------
// CRC 32 lookup table
static  const uint32 CRC32_TABLE[ ] =
{
    0x00000000U, 0x1DB71064U, 0x3B6E20C8U, 0x26D930ACU,
    0x76DC4190U, 0x6B6B51F4U, 0x4DB26158U, 0x5005713CU,
    0xEDB88320U, 0xF00F9344U, 0xD6D6A3E8U, 0xCB61B38CU,
    0x9B64C2B0U, 0x86D3D2D4U, 0xA00AE278U, 0xBDBDF21CU
};
//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------


//=====================================================================================================================
//-------------------------------------- Public Functions -------------------------------------------------------------
//=====================================================================================================================

/**
 * @brief This method calculates a CRC32bits for 1 byte each time
 * @param crc Previews CRC value
 * @param data Data to be processed in this Crc interaction
 * @return CRC product of this process
 */
uint32 Crc32__ProcessByte( uint32 crc, uint8 data )
{
    // calculate it
    crc = ( crc >> 4 ) ^ CRC32_TABLE[ (uint8)( crc & 0x0000000FU ) ^ ( data & 0x0FU ) ];
    crc = ( crc >> 4 ) ^ CRC32_TABLE[ (uint8)( crc & 0x0000000FU ) ^ ( data >> 4 ) ];

    // return the new value
    return( crc );
}


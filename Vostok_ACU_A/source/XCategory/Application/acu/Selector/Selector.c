/**
 *  @brief      Get the group using sf_index, compartment and cycle.
 *
 *  @param      uint8 setting_file - setting file index for where to find program header
 *  @param      uint8 compartment - identifier appliance compartment (0 based, 0 for single compartment appliances).
 *  @param      uint8 cycle - displacement of Cycle Name ID in settings file program header
 *
 *  @return     CYCLE_GROUP_TYPE cycle_group - Used to identify related cycles.
 *  @retval     0 - 127  - Cycle group enumeration id for compartment cycle.
 *
*/
CYCLE_GROUP_TYPE Selector__GetCycleGroup(uint8 sf_index, uint8 compartment, uint8 cycle)
{
    uint8 cycle_group = CYCLE_GROUP_GENERIC;

    uint8 const *cycle_header;

    if (GetProgramHeadingPointer(sf_index, compartment, cycle, &cycle_header) == PASS)
    {
        #define CYCLE_GROUP_OFFSET_LOWER_4_BITS 1
        cycle_group = (cycle_header[CYCLE_GROUP_OFFSET_LOWER_4_BITS] & 0xF0) >> 4;

        #define CYCLE_GROUP_OFFSET_UPPER_3_BITS 0
        cycle_group |= (cycle_header[CYCLE_GROUP_OFFSET_UPPER_3_BITS] & 0xE0);
    }

    return ((CYCLE_GROUP_TYPE)cycle_group);
}


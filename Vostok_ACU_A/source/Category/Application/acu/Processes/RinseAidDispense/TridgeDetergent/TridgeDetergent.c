void TridgeDetergent__Initialize(void)
{
	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
		{
			memset(&Tridge_Detergent_NV_Data[0], 0, sizeof(Tridge_Detergent_NV_Data));

			for (uint8 i = 0; i < ELEMENTS_IN_ARRAY(Tridge_Detergent_NV_Data); i ++)
			{
				Tridge_Detergent_NV_Data[i].Process_State = FSM_STATE_NONE;
			}
		}

		memset(&Tridge_Detergent_Data[0], 0, sizeof(Tridge_Detergent_Data));
	}
}

PROCESS_RESULT_TYPE TridgeDetergent__Manager(PROCESS_CONTROL_TYPE* control)
{
    PROCESS_RESULT_TYPE result = PROCESS_COMPLETE;

    TD_Process_Data_Ptr = &Tridge_Detergent_Data[control->Process_Instance];
    TD_Process_NV_Data_Ptr = &Tridge_Detergent_NV_Data[control->Process_Instance];

    if ((control->Step_Initializing != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Reset != (BOOL_TYPE)FALSE) ||
		(control->Resume_From_Pause != (BOOL_TYPE)FALSE))
	{
		ReadProcessParameters(control);

		// Setup state machine and timeout to wait for first Tag data read
		TD_Process_Data_Ptr->Fst_Tag_Status_Timer = FST_TAG_STATUS_TIMEOUT;
		TD_Process_Data_Ptr->Tag_Read_Status = TAG_READ_STATUS_WAITING;
	}

	if ((TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters != NULL) && (TD_Process_Data_Ptr->Tridge_Detergent_Parameters != NULL))
	{
		if (TD_Process_Data_Ptr->Tag_Read_Status == TAG_READ_STATUS_WAITING)
		{
			if (TD_Process_Data_Ptr->Fst_Tag_Status_Timer > 0U)
			{
				TD_Process_Data_Ptr->Fst_Tag_Status_Timer --;

				if (CartridgeDetectionMonitor__GetFstTagStatusReadDone() != FALSE)
				{
					// Tag data received
					TD_Process_Data_Ptr->Tag_Serial_Number = CartridgeDetectionMonitor__GetTagSerialNumber();
					if (CartridgeDetectionMonitor__GetTagData(TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Detergent_Type, &TD_Process_Data_Ptr->Tag_Detergent_Data) != FALSE)
					{
						uint16 local_ml_remaining;
						if (CartridgeDetectionMonitor__GetLocalMLRemaining(TD_Process_Data_Ptr->Tridge_Detergent_Process_Parameters->Detergent_Type, &local_ml_remaining) != FALSE)
						{
							if (local_ml_remaining != 0xFFFFU)
							{
								// Local value of Ml remaining is valid: replace Tag value with local value, since Tag value could not have been updated yet
								TD_Process_Data_Ptr->Tag_Detergent_Data.ML_Remaining = local_ml_remaining;
							}

							TD_Process_Data_Ptr->Tag_Read_Status = TAG_READ_STATUS_GOOD;
						}
						else
						{
							// No data found for selected detergent
						}
					}
					else
					{
						// No data found for selected detergent
					}
				}
			}
			else
			{
				// Timeout expired without receiving valid Tag data
				TD_Process_Data_Ptr->Tag_Read_Status = TAG_READ_STATUS_KO;
			}
		}

		if (control->Step_Initializing != (BOOL_TYPE)FALSE)
		{
			// Initialize this instance of the Tridge Detergent process.
			TridgeDetergentFsmInitialize(&TD_Process_NV_Data_Ptr->Process_State);
		}

		if (control->Resume_From_Reset == (BOOL_TYPE)FALSE)
		{
			// TridgeDetergentFsmExecute() not called in first iteration after a cold reset to avoid a spurious pump turning on in case of
			// long power fail: actually, in this case, Tridge Detergent process would be called for just one iteration before going in PAUSE.
			TridgeDetergentFsmExecute(&TD_Process_NV_Data_Ptr->Process_State);
		}

		result = ((TD_Process_NV_Data_Ptr->Process_State == FSM_STATE_TRIDGE_DISPENSE_COMPLETE) ? PROCESS_COMPLETE : PROCESS_BUSY);
	}
	else
	{
		// No valid setting file parameters, terminate process
	}

	return result;
}



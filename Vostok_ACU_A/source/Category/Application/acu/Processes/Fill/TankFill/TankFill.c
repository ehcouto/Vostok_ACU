static PASS_FAIL_TYPE ReadProcessParameters(PROCESS_CONTROL_TYPE* control)
{
	PASS_FAIL_TYPE result = FAIL;
	SETTINGFILE_LOADER_TYPE loader;

	// Get the Fill process parameters from the setting file.
	if (SettingFile__MSettingBasicLoader(control->Setting_File_Index,
										 SF_PTR_ACU_PROCESS_FILL,
										 control->Data_Field_Index,
										 &loader) == FAIL)
	{
		LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, control->Data_Field_Index);
	}
	else
	{
		Tank_Fill_Data.Class_A_Fill_Process_Parameters = (CLASS_A_FILL_PROCESS_PARAMETER_TYPE *)(void *)loader.Data;

		if ( FAIL == SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS,
											  DISPL_CLASS_A_FILL,
											  &loader) )
		{
			LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, (uint16)SF_PTR_ACU_ALGORITHM_PARAMETERS);
		}
		else
		{
			Tank_Fill_Data.Class_A_Fill_Parameters = (CLASS_A_FILL_PARAMETER_TYPE *)(void *)loader.Data;

			result = PASS;
		}
	}

	return result;
}


static void NoWaterFoundInAfterFillChecks(void)
{
	if (Liters_In_Tub > Liters_In_Tub_Before_Fill)
	{
		Liters_In_Water_Tank = Liters_In_Tub - Liters_In_Tub_Before_Fill;
	}
	else
	{
		Liters_In_Water_Tank = 0;
	}
	Liters_In_Tub = Liters_In_Tub_Before_Fill;

	Tank_State_Fill = TANK_STATE_PASSED;
	Fill__SetBitmapBit(TANK_STATE_CLEAN);
}



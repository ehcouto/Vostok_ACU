/**
 *  @brief      Get number_of_cycles from the project setting file.
 *
 *  @param      uint8 sf_index - setting file to check.
 */
uint8 Selector__GetNumberOfCycles(uint8 sf_index, uint8 compartment)
{
    SETTINGFILE_LOADER_TYPE sf_loader_data;
    PASS_FAIL_TYPE load_success;
    uint8 number_of_cycles;

    number_of_cycles = 0;

    load_success = SettingFile__MSettingBasicLoader(sf_index, SF_PTR_UI_PRODUCT_CONFIG, SF_DISPL_CYCLES_NUMBER, &sf_loader_data);
    if (load_success == PASS)
    {
        if (sf_loader_data.Data[compartment] <= MAX_CYCLES)
        {
            number_of_cycles = sf_loader_data.Data[compartment];
        }
        else
        {
          LOG_ADD_EXCEPTION(NUMBER_OF_CYCLES_OUT_OF_RANGE, sf_loader_data.Data[compartment]);
        }
    }
    else
    {
        LOG_ADD_EXCEPTION(SF_PTR_CROSS_PRODUCT_CONFIG_FAIL, sf_index);
    }

    return (number_of_cycles);
}


/**
 * It updates the value of the reaction that will be triggered by the Stability Control task, resulting from the requested reaction
 * and from the cross checks on the max amount of water that can be added, on the max number of speed reductions, on the max number
 * of phase repetitions. The resulting reaction may be different from the originally requested one.
 *
 * @param  reaction_ptr: pointer to the reaction
 *
 * @return none
 */
void StabilityControl__UpdateReaction(STABILITY_CONTROL_REACTIONS_TYPE *reaction_ptr)
{
	sint32 liters_in_tub;
	sint32 phase_repetitions;

	if (Variable__GetValue(VARIABLE_LITERS_IN_TUB, 0, &liters_in_tub) == FALSE)
	{
		liters_in_tub = 0;
	}

	if (Variable__GetValue(VARIABLE_PHASE_REPETITIONS, 0, &phase_repetitions) == FALSE)
	{
		phase_repetitions = 0;
	}

	PartialReactionUpdate(reaction_ptr, liters_in_tub, phase_repetitions);

	if ((*reaction_ptr == SC_REACTION_RESTART_PHASE) && (phase_repetitions >= (sint32)Common_Parameters_Ptr->Max_Phase_Repetitions))
	{
		*reaction_ptr = (STABILITY_CONTROL_REACTIONS_TYPE)Instance_Parameters_Ptr->Reaction_After_Max_Phase_Repeats; // Req 706787

		// Repeat all previous checks, since reaction has been updated
		PartialReactionUpdate(reaction_ptr, liters_in_tub, phase_repetitions);

		if (*reaction_ptr == SC_REACTION_RESTART_PHASE)
		{
			*reaction_ptr = SC_REACTION_NONE; // No recursion allowed
		}
	}
}



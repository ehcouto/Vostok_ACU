//-------------------------------------- Include Files ----------------------------------------------------------------
#include "SetLoadQueue.h"
#include "string.h"
#include "Mode.h"
#include "Crc8.h"
#include "Log.h"

//! Maximum queue length available
//! NOTE: Every load will require 3 bytes + Load Data size
//! SetLoadQueue_Max_Len variable holds maximum size required for current setting file
#ifndef SETLOADQUEUE_MAX_LENGTH
#define SETLOADQUEUE_MAX_LENGTH     128
#endif

//! A list of log message IDs that are generated by this module.
typedef enum
{
    QUEUE_DATA_SIZE_TOO_SMALL
} MODULE_SETLOADQUEUE_LOG_MESSAGE_ID_TYPE;

#define MODULE_NAME MODULE_SETLOADQUEUE      //!< The name of this module for use by the Log module.

//-------------------------------------- PUBLIC (Variables) -----------------------------------------------------------
uint8 SetLoadQueue_Data[SETLOADQUEUE_MAX_LENGTH];
uint8 SetLoadQueue_Max_Len;
uint8 SetLoadQueue_Index;

//-------------------------------------- PRIVATE (Variables, Constants & Defines) -------------------------------------

//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------



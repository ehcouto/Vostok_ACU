/**
 *  @brief      It Initializes the module RinseAidMonitor and its variables
 *
 */
void RinseAidMonitor__Initialize(void)
{
	SETTINGFILE_LOADER_TYPE loader;

	//Initialize NVData values
	if (NVData__GetLastRestoreStatus()->Bit.Appliance_Data_Recovered != (NVDATA_RESTORE_TYPE)NVDATA_RESTORE_OKAY)
	{
		Last_Rinse_Aid_Status = RINSE_AID_SENSOR_EMPTY;
		RinseAid_Dispenser_Use_Cnt = 0;
		RinseAid_IsUnlocked = FALSE;
		Check_RinsAid_State = FALSE;
	}


	if (Micro__GetResetMode() == MICRO_RESET_MODE_COLD)
	{
		if (SettingFile__BasicLoader(SF_PTR_ACU_ALGORITHM_PARAMETERS, DSPL_RINSE_AID, &loader) == PASS)
		{
			Sf_RinseAid_Data.Door_Close_Timer = ((RINSE_AID_MONITOR_INSTANCE_TYPE*)(void*)(loader.Data))->Door_Close_Timer;
			Sf_RinseAid_Data.Threshold_1 	  = ((RINSE_AID_MONITOR_INSTANCE_TYPE*)(void*)(loader.Data))->Threshold_1;
			Sf_RinseAid_Data.Threshold_2 	  = ((RINSE_AID_MONITOR_INSTANCE_TYPE*)(void*)(loader.Data))->Threshold_2;
		}
		else
		{
			LOG_ADD_EXCEPTION(ERROR_LOADING_PARAMETERS, SF_PTR_ACU_ALGORITHM_PARAMETERS);

			Sf_RinseAid_Data.Door_Close_Timer = 30; //Default value from SettingFile

			//If THR1 and THR2 are set to 0 we we will always have RINSE_AID_STATUS_LED_OFF
			Sf_RinseAid_Data.Threshold_1 = 0;
			Sf_RinseAid_Data.Threshold_2 = 0;
		}

		Dispenser_State = OFF;
		Dispenser_State_Prev = OFF;
		Rinse_Aid_Status = RINSE_AID_STATUS_LED_OFF; //Default value
		Programming_Measurment_Done = FALSE;

	    Millis__Start(&Rinse_Aid_Timer);
	}
}

/**
 * @brief Main handler for RinseAidMonitor module
 */
void RinseAidMonitor__Execution(void)
{
	//In service and EOL test we want to have specific behavior for Rinse_Aid_Status
	if ((uint8)Supervisor__IsTestCycleActive() == (uint8)TRUE)
	{
		RINSE_AID_SENSOR_TYPE temp;

		temp = (RINSE_AID_SENSOR_TYPE)Hbl__GetGIDataConvertedByIndex(Hbl__GetGIIndexByGIID(HBL_GI_RINSE_AID));

		if(temp == RINSE_AID_SENSOR_EMPTY)
		{
			Rinse_Aid_Status = RINSE_AID_STATUS_LED_OFF;
		}
		else if( temp == RINSE_AID_SENSOR_FULL)
		{
			Rinse_Aid_Status = RINSE_AID_STATUS_LED_SOLID_ON;
		}
	}
	else
	{
		//Check RinseAid sensor to detect refill
		ReadRinsAidLevel();

		//Start estimate RinseAid when we will detect empty condition on sensor
		DetectRinseAidInjection();

		//Update RinseAid Regulation value
		UpdateRinseAidStatus();
	}
}


/**
 * @brief	Function which should be invoked when cycle is started or started with delay
 */
void RinseAidMonitor__CheckEvent(void)
{
    Check_RinsAid_State = TRUE;
}

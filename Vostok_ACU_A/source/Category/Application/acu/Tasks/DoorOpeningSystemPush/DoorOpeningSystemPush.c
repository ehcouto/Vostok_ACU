//! The name of this module to be used by the Log module.
#define MODULE_NAME MODULE_DOOR_OPENING_SYSTEM_PUSH

//! A list of log message IDs that are generated by this module.
typedef enum MODULE_DOOR_OPENING_SYSTEM_PUSH_LOG_MESSAGE_ID_ENUM
{
	ERROR_MISSING_PARAMETERS  = 1,
} MODULE_DOOR_OPENING_SYSTEM_PUSH_LOG_MESSAGE_ID_TYPE;

//! The number of times that the DoorOpeningSystemPush__Task function will be called every second while running.
#define DOS_PUSH_CALLS_PER_SECOND (uint8)10

typedef struct DOS_PUSH_DATA_STRUCT
{
	uint16 DOS_Push_Counter; //! Timer used to manage the DOS Push procedure
	DOOR_OPENING_SYSTEM_SF_PARAMS_TYPE *SF_DOS_Push_Params_Ptr; //! Pointer to setting file parameters for DOS Push
} DOS_PUSH_DATA_TYPE;

//! Data for DOS Push management
static DOS_PUSH_DATA_TYPE DOS_Push_Data;

//-------------------------------------- PRIVATE (Function Prototypes) ------------------------------------------------



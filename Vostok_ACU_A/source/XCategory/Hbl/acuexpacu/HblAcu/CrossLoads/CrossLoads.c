/**
 * @brief - This function is the core handler of the Crossload and it should be called every 100ms
 * @param load
 * @return
 */
void CrossLoads__Process(uint8 *hbl_prio_ptr)
{
#if (HBL_CROSS_LOADS_FEATURE == ENABLED)
    CROSSLOAD_STATE_MACHINE_FUNCTION_POINTER_TYPE handler_ptr;
    HBL_CROSS_LOAD_TYPE *cfg_ptr;
    CROSSLOAD_DATA_TYPE *data_ptr;
    uint8 index;
    uint8 cur_prio;

    if (SRDATA_OK == (SRData__CheckByte(&Crossloads_Num, &N_Crossloads_Num)))
    {
        for (index = 0 ; index < Crossloads_Num ; index++)
        {
            cfg_ptr = &Crossload_Cfg[index];

            if (Crossload_Crc[index] == Crc8(CRC8_INITIAL_SEED,(uint8 *) cfg_ptr, sizeof(Crossload_Cfg[0]))) //lint !e928 Suppress Info: cast from pointer to pointer [MISRA 2004 Rule 11.4]
            {
                data_ptr = &Crossload_Data[index];
                handler_ptr = CrossloadHandlers[cfg_ptr->Parameter_Ptr->Type];

                if ((cfg_ptr->Crossload_Position < Hbl__GetNumLoads()) &&
                    (handler_ptr != (CROSSLOAD_STATE_MACHINE_FUNCTION_POINTER_TYPE) NULL))
                {
                    data_ptr->Requested = *(uint8*)Hbl__GetRequestedLoad(cfg_ptr->Crossload_Position);
                    data_ptr->Processed = *(uint8*)Hbl__GetProcessedLoad(cfg_ptr->Crossload_Position);

                    // Call Cross Load Handler defined
                    (*handler_ptr)(cfg_ptr, data_ptr, hbl_prio_ptr);

                    cur_prio = GetLoadPriority(cfg_ptr->Crossload_Position, hbl_prio_ptr);
                    Hbl__SetLoadRequestByIndex(cfg_ptr->Crossload_Position,
                        (HBL_PRIORITY_TYPE)cur_prio,
                        &Crossload_Data[index].Processed);
                }
            }
            else
            {
                SRException__Queue(SREXCEPTION_HBL_DATA_NOT_CONSISTENT,0xFE,index);
            }
        }

        for (index = 0 ; index < Crossloads_Num ; index++)
        {
            cfg_ptr = &Crossload_Cfg[index];
            if (cfg_ptr->Crossload_Position < Hbl__GetNumLoads())
            {
                data_ptr = &Crossload_Data[index];
                data_ptr->Previous_State = data_ptr->Generic_State;
            }
        }

    }
#endif
}

